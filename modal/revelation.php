      <h4>Révélation</h4>
	  <p>voir <a href="https://www.eglise-realiste.org/pdf/Manuel-Handbook_Documents.pdf" target="_blank">Manuel-Handbook Documents, page 69 "...De Dieu Absolu..."</a>, Talib Freddy -2012 </p>
	 

<p>L’ancienne Religion était l’âme de la Civilisation. Rien de moins ; mais rien de
plus.</p>
<p>1- La Civilisation, c’était la RELIGION. C’est-à-dire :
<p> La proclamation véridique par l’homme, que l’Esprit est positivement constitutif
de la Réalité.</p>
<p> La religion donnait à ce fait la forme dogmatique d’une Hégémonie de l’esprit sur
la matière : hégémonie relative dans la Création, et absolue en “Dieu”.</p>
<p>2- La Religion, c’était la RÉVÉLATION. C’est-à-dire :</p>
<p> L’affirmation de l’hégémonie de la Foi sur la raison : hégémonie totale seulement
chez les Agréés de l’autre-monde, dont le corps était dit éthérique.</p>
<p> À cela correspondait une société Bourgeoise – reposant sur la base Marché-État
–, où l’on était enjoint de faire le Bien et de découvrir le Vrai.</p>
<p>3- La Révélation, c’était la RÉVOLUTION. C’est-à-dire :</p>
<p> Tout partait d’une révélation Première, coïncidant avec une révolution Tout Court :
le passage de l’humanité Parentale à l’humanité Bourgeoise ; d’où la “diabolisation” du
Matérialisme Traditionnel.</p>
<p> Des révélations Secondes, d’une toute autre portée, s’enchaînaient par la suite,
ponctuant les révolutions Réformatrices, indispensables à l’élévation par paliers de la
religion, de sa forme Simple initiale à sa forme Pure terminale.</p>
<p>4- La Révolution, c’était la GUERRE. C’est-à-dire :</p>
<p> La guerre Totale fondatrice est bellement figurée par le combat d’Hercule, aidé
des dieux, contre les Géants nés de Terre (Gaea) et du sang de Ciel (Ouranos) émasculé.
C’est le Matérialisme en perdition qui provoque cette guerre.</p>
<p> Des Krachs civilisés périodiques, amenant une domination Païenne passagère,
provoquent ensuite des guerres Saintes relevant le défi ; occasion à tout coup d’une
brillante promotion civilisée.[...]"</p>
		  
