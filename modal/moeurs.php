			  <h4>Moeurs</h4>
			  
			  <p>« Le cri de la vérité morale (sociale) consiste à voir l’histoire toute 
				  entière comme trois étapes de développement : l’état sauvage, l’état de lois et l’état de moeurs. »</p>


				  <p><i><b>• l’état sauvage</b></i></p>
<p>L’état sauvage est l’état de désunion, sans autre union qu’une union d’instinct parce que les premiers hommes se sont attroupés pour leurs besoins et leur défense.</p>
<p>Il est le berceau duquel va grandir et se développer l’état de lois à proportion que le langage s’accroît et les connaissances s’acquièrent.</p>

<p><i><b>• l’état de lois</b></i></p>
<p>L’état de lois, né de l’état sauvage, est “l’état de l’extrême désunion dans l’union” parce qu’il repose sur l’ignorance déclarée des hommes, l’inégalité sociale et la propriété à l’égard des biens. Les lois humaines et divines n’existent qu’à l’appui de ces vices.</p>
<p>Mais il est le berceau duquel va grandir et se développer l’état de moeurs à proportion que l’homme, devenu aussi raisonneur qu’intelligent par sa société, ne trouvera plus que la somme des êtres, que Le tout et Tout.</p>

<p><i><b>• l’état de moeurs</b></i></p>
<p>L’état de moeurs est “l’état d’union sans désunion”, l’état social sans lois, sans tien ni mien, sans langues étrangères, sans pudeur.</p>
<p><b>C’est dans ce dernier état où la vérité seule peut éclairer toute la terre et concourir à ce que nous soyons un seul et même être. Le paradis sera ainsi dans le seul endroit où nous pouvions le faire, dans ce monde.</b></p>
<p><I>C’est par la vérité première et la vérité morale qui en découle que nous verrons tous nos systèmes fondamentaux épurés et réduits au Rienisme ; devenu lui-même une évidence, il cessera d’exister. »</i></p>


