<p>“Moi, Paul apôtre, j’ai été fait docteur parmi les païens. Et je souffre pour mon Évangile jusqu’aux chaînes, comme un criminel.
Mais la Parole divine ne se laisse pas enchaîner !</p>
<p style="text-align:center;"><b>•••</b></p>
Timothée, mon fils !
Ce n’est pas un esprit de timidité que Dieu nous a départi ! Ne rougis pas de témoigner. Sois fort de la puissance de Dieu, et souffre avec moi pour la Bonne Nouvelle.
Timothée, mon fils !
Un paysan qui veut récolter doit d’abord peiner sur le champ ; un sportif qui veut monter sur le podium doit d’abord s’entraîner dur ; un militaire qui veut vaincre doit faire son deuil de la vie civile.
<p style="text-align:center;"><b>•••</b></p>
Timothée !
Conservons le Bon Dépôt de la promesse divine, grâce à l’Esprit-Saint qui habite en nous !
Soyons de bons soldats de Jésus-Christ !” »</p>


