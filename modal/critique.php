      <h4>Critique</h4>
	  	
	  <p>« L’Humanité Primitive s’apprit à Parler, l’Humanité Civilisée s’apprit à Écrire ; notre mentalité Réaliste annonce l’Humanité Communiste qui aura su s’apprendre à Penser au plein sens du mot.</p>
	<p>• Le Réalisme est Vrai, parce qu’il surmonte l’idée de Substance objective qui limitait la Conception de nos ancêtres.</p>
	<p>• Le Réalisme est Critique, parce qu’il surmonte le Préjugé subjectif qui limitait la Méthode de nos ancêtres.</p>
	<p>Avec le Réalisme, la notion de Rapport s’établit pour la première fois dans la mentalité humaine. Ceci lève toutes les difficultés antérieures concernant la liaison entre l’objectif et le subjectif.</p>
	<p>Quant à la Conception, le Réalisme s’incline devant le rapport Matière-Esprit. Quant à la Méthode, il épouse le rapport Symbole-Raison. »</p>


