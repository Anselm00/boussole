			  <h4>Economie Politique</h4>
			  
			  <p>« Domaine de l’action intéressée directe : 
				  Marché-État, Économie-Politique. On part, nous 
				  le savons, d’une situation où l’on est “Propriétaire 
				  parce que Citoyen” pour finir à l’inverse : “Citoyen 
				  parce que Propriétaire” ; et de l’Esclavage direct tout 
				  à fait bénin, pour finir avec le Salariat pratiquant l’exploitation “scientifique” 
				  (exploitation n’est pas pris au sens péjoratif habituel), en passant par le Servage 
				  “petit-bourgeois” (mi-dépendance personnelle/mi-liberté du travail).</p> 
				  
				 <p> Les <b>Anciens</b> étaient simples, civilisés tout à fait inconséquents, naïfs et 
				  “brutaux” au regard de leurs successeurs, mais ont tout mis sur pied, ont 
				  eu la tâche la plus difficile. Vis-à-vis de la TENTATION que représente le corps et 
				  la Nature, ils n’y vont pas par quatre chemins, réclament pour l’Élite Sociale l’<b>OTIUM</b>, une 
				  vie de <b>“Loisir”</b> total <b>pour en préserver la société</b>. Certains de leurs Penseurs iront jusqu’à prêcher qu’il 
				  faut confier toutes les tâches de direction effective, pratique, à des Citoyens de 
				  seconde zone, aidés par des esclaves éduqués pour ce faire (Sénèque en parle 
				  encore quoique pour critiquer cette École). Et c’est en tenant bien 
				  compte de ce fait, que Platon prône le Communisme dans sa 
				  République. C’est pour la même raison que les <b>Chrétiens</b>, 
				  plus tolérants pour les “travaux mécaniques”, prohibent 
				  encore absolument le Crédit. Les Manufacturiers et Fonctionnaires 
				  <b>Modernes</b>, eux, prohibent le Faste et la Prodigalité de la vieille 
				  noblesse, tandis qu’ils font la chasse à la Paresse chez les Salariés, 
				  qu’ils exhortent à l’Épargne (alors qu’on va vers un coût 
				  d’entretien des salariés se réduisant à “presque rien”).</p>
<p>Ne voit-on pas tous les contresens qu’il y a à éviter 
	concernant l’histoire civilisée ! Ainsi, depuis 160 ans, 
	que d’idioties d’un anachronisme (an-historisme) intéressé, 
	n’a-t-on pas répandu sur les Corporations médiévales – et soit 
	pour les vanter, soit pour les stigmatiser !»</p>

