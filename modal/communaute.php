			  <h4>Communauté</h4>

			  
			   <p>« L’ère primitive couvre une immense période qui va de l’apparition de l’homme
(disons le Pithécanthrope) jusqu’à la veille du “miracle grec” (disons Pythagore et
Bouddha). Ceci représente, ne l’oublions jamais, quelque chose comme 99 % de
l’histoire de l’humanité.</p>
<p>Or, nos voyageurs, missionnaires et ethnologues, à qui l’expansion colonialiste a
fourni des sujets de thèses, ne nous donnent, presque exclusivement, que des
monographies concernant des tribus patriarcales ; d’où la tendance à assimiler ces
dernières aux primitifs en général. Dans ces conditions, il est pratiquement
impossible de parvenir à établir une quelconque filiation entre les “sauvages” et les
“civilisés”.</p>
<p>• Une première erreur est de nous laisser penser que les premiers hommes
ressemblaient aux Patriarches de la Bible ; alors que ceux-ci étaient déjà le produit
d’une longue évolution. Avant cela existaient des communautés liées par le sang,
ignorant encore le patriarcat. Ce dernier avait été précédé par l’organisation gentilice
matriarcale, elle-même précédée par des communautés non différenciées
(“promiscuité primitive”).</p>
<p>• Une seconde erreur est qu’on ne semble pas soupçonner que les empires asiates,
qui ont succédé aux tribus patriarcales, font partie intégrante de la même ère
primitive. Ainsi en va-t-il de l’Égypte, la Chaldée, l’Inde brahmanique, la vieille Chine
des empereurs légendaires, et l’Amérique “précolombienne” (Incas, Mayas,
Aztèques). Depuis la crise de l’esprit moderne, on range ces empires dans l’“Orient
Ancien”, que l’on taxe, de la manière la plus ambiguë, de “berceau de la civilisation”.
C’est ainsi que Sumer et d’autres “Mycéniens chalcolithiques” sont dissociés de l’ère
primitive à laquelle ils appartiennent, pour être raccordés tant bien que mal à
l’Athènes de Socrate ! Là-dessus, les <a class="modal-trigger " href="#occultisme">occultistes</a> surviennent pour proclamer que
l’étape asiate, phase finale de l’ère primitive, est en réalité le point de départ absolu
de l’humanité, dont témoigne... le continent disparu des Atlantes !</p>
<p>En réalité, l’étape “asiate” de l’ère primitive porte la vieille communauté de sang à
son sommet, en lui donnant un caractère centralisé et universaliste. C’est la
signification même de l’Empire, doté d’un Code en “langue sacrée”, et dont tous les
éléments sont liés par le Feu Solaire ou “sang cosmique”. Le chef “saint”, “pur”, de
l’Empire, se présente comme “fils du Soleil”, et prétend au titre de “Roi des Rois”. »</p>
<p>(...)</p>

<p>C’est la réussite même, l’épanouissement suprême, de l’ordre Traditionnel
“antédiluvien”, qui l’entraîna ensuite à sa désagrégation, puis à son effondrement
général.</p>
<p>Par sa prétention au règne “universel”, l’Empire asiate était porté à s’annexer toute
communauté étrangère en la plaçant au rang de peuplade tributaire, “inférieure”,
sinon “intouchable”. Ainsi, la vieille “parenté” primitive devenait plus purement
théorique. Ensuite, l’Empire devait nécessairement trouver sa limite dans la
constitution d’autres empires voisins qu’il suscitait, plaçant, eux aussi, à leur tête, un
“Fils du Ciel” ou “Grand Roi”. Ainsi, tandis que l’Empire s’affaiblissait de l’intérieur,
les affrontements entre empires rivaux les épuisaient l’un l’autre.</p>
<p>Mais c’est un “troisième larron” qui donna le coup de grâce à l’ordre traditionnel
immémorial. L’asiatisme provoquait la formation de communautés spécialisées de
navigateurs, reliant les empires entre eux, et ceux-ci aux tribus environnantes.
Dans un premier temps, ces marins collectivistes se contentèrent de “parasiter” le
monde asiate, et adoptèrent pour eux-mêmes les formes théocratiques. Ce fut le cas
des Phéniciens. Avec le temps, la putréfaction de l’asiatisme s’accentuant, en même
temps que les conflits entre “peuples de la mer”, des bandes de “pirates” d’un type
nouveau parurent, forts de leur sève barbare, et défiant l’asiatisme lui-même. Ceux-ci,
associant cabotage et brigandage, depuis l’Ionie jusqu’en Syrie et en Troade,
ignoraient qu’ils allaient accoucher du “miracle grec”. Tout les conduisait,
cependant, à prendre le contre-pied des Phéniciens : au lieu de s’insinuer dans l’aire
asiate, il ne leur était plus permis que d’entreprendre de “sauter” l’étape asiate. Il leur
était donné d’ouvrir la voie toute nouvelle de la Cité, menant tout droit à la révolution
de Clisthène (cf. Victor Bérard : Les Phéniciens et l’Odyssée).</p>
<p>La voie ouverte, il sembla alors que, d’un seul coup d’épaule décisif, l’ordre primitif
vermoulu tombait en pièces pour ne laisser aucune trace. En un éclair, l’empire perse
de Cyrus, aussi bien que l’empire Maurya en Inde furent éclipsés, ouvrant l’ère
civilisée. »</p>
