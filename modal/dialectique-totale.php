      <h4>Dialectique Totale</h4>
	  	
	  <p>« Le Comm-Anar est le régime social d’Égalité-Liberté, les deux choses étant
confondues au point d’être identifiées, alors qu’elles n’ont jamais été qu’exclusives
l’une de l’autre. C’est dire que le Comm-Anar est loin de revenir à la Préhistoire ! Et il
n’est plus question non plus de revenir, ni à l’Idolâtrie traditionnelle, ni à la
Religion civilisée (encore deux CONTRAIRES identiques, il ne faut pas s’y
tromper). Nous affirmons cependant que le Réalisme n’est autre que le
Matérialisme-Spiritualisme ! Ceux qui ne sont pas habitués à la Dialectique Totale
peuvent ne pas s’y retrouver, mais ça viendra. »</p>

<P>Le Comm-Anar est tellement éloigné de la Préhistoire que s’il retient quelque
chose du passé, c’est <b>le NÉGATIF de chacun des vieux systèmes</b>, dont la fusion
produit un Positif posthume. Qu’est ce “négatif” ? C’est non pas ce que la Tradition et
la Civilisation jugeaient essentiels : le Matérialisme et l’Égalité d’un côté, le
Spiritualisme et la Liberté de l’autre côté, mais le côté inverse qu’elles recelaient
<b>effectivement</b>, mais réprouvaient dans l’Absolu et toléraient dans le Relatif,
c’est-à-dire l’Esprit dans la Tradition et la Matière dans la Civilisation (cf.
Résurrection de l’Occident, annexe : Orthodoxie Générale). C’est donc l’ombre de
l’humanité préhistorique que nous retenons seulement, ce qui met en relief que le
Comm-Anar est absolument original, incomparable au bout du compte, <b>COMME</b> le
furent Tradition et Civilisation, les conditions d’existence de chaque humanité étant à
chaque fois essentiellement Neuves, Inédites. Encore une fois : nous ne sommes pas
“progressistes” ! Et c’est pourtant bien le passé qui fait que nous soyons là, et lui
encore qui a produit les conditions faisant que nous n’ayons pas d’autre choix que le
Comm-Anar, celui de Volontariat-Gratuité, suppression des Armes et de l’Argent… »</p>


