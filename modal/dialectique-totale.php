<p>« Notre <b>Nouvelle Dialectique</b>, de part en part Historique, n’est plus du tout formaliste comme celle de Hegel ; avec cela, elle s’émancipe de celle de Marx, qui ne fait de la logique de Hegel qu’un ferment panthéiste du sensualisme orthodoxe de la civilisation moderne, et un ferment utopiste de l’économisme classique de la civilisation moderne.</p>
<p>Nous nous devons donc de <b>préciser soigneusement</b> le contenu Historique de la Double Négation générale Matérialisme-Spiritualisme-Réalisme et Communauté-Cité-Comm-Anar. En effet, ce sont deux ÈRES, primitive et civilisée, qu’ont connues nos ancêtres, et non pas des théories ! De plus, ces deux ères menées chacune à leur Perfection, ont connu un Cataclysme final qui se traduisit par un processus de Dégénération mené à son achèvement. Conséquences :</p>
<p>- Ce sont les formes <b>PLEINES</b> finales du Matérialisme primitif et du Spiritualisme civilisé qui entrent dans la double-négation Réaliste, c’est-à-dire l’Asiatisme Égypto-Chaldéen d’une part et le Déisme Franco-Anglais d’autre part.</p>
<p>- La Dégénération respective du Matérialisme et du Spiritualisme, qui prit plus d’un lustre de générations, les porta tous deux à une forme <b>VIDE</b> finale, dont le Réalisme opère aussi la double-négation.</p>
<p>Ce n’est que Pleines et Vides que les deux mentalités du passé sont MÛRES pour le grand saut du Réalisme. De ce point de vue, la 3ème espèce de la race humaine est celle qui <b>“dépasse” doublement</b> la Préhistoire Achevée et la Barbarie Intégrale, chacun de ces “moments” ayant eu deux formes contraires. (L’effondrement du monde primitif fut une première “barbarie intégrale”, à laquelle il faudrait trouver un autre nom.) »</p>
