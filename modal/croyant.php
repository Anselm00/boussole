      <h4>Croyant</h4>
	  	
	  <p>« Tout d’abord, pour le croyant orthodoxe, la Réalité est double. Croire au minimum
au Créateur d’un côté et à la Création de l’autre est l’expression la plus simple, accessible, de cette conception de la Réalité.
Dans ce couple, le Créateur domine sa Création : voilà le premier point important de l’orthodoxie religieuse.
Nous verrons plus loin qu’il existe une forme complexe de la Réalité orthodoxe.</p>
<p>● Ensuite, le croyant distingue dans la Création deux pôles : l’Humanité d’un “côté” et la Nature de “l’autre”.</p>
<p>Pourquoi “d’un côté… et de l’autre” ? Parce que Humanité-Nature ne fait que souligner deux pôles d’une seule et unique Création ; ces “éléments” sont entrelacés, tissés ensemble de sorte qu’ils sont indissociables.</p>
Ainsi, par son côté corporel, l’Humanité appartient à la Nature, et en est même précisément le Fleuron (1). Inversement, par son côté animé, la Nature appartient à l’Humanité, en tant que son intelligibilité ultime doit être rapportée au fait qu’elle forme un Tout, impulsé et suspendu à une Âme particulière, soeur (2) de celles des hommes.</p>
Dans ce couple, l’Humanité commande à la Nature : le croyant doit se faire “maître de la Nature”. C’est que l’Humanité est nécessairement vue comme relativement Active, à la différence de la Nature, jugée relativement Passive.</p>
<p>Durant les 25 siècles de Civilisation, de - 650 à 1835, il en a toujours été ainsi.</p>

<p>Notes:</p>
<p>(1) Fleuron – sens figuré : Ce qu’on possède de plus précieux, de plus avantageux, de plus productif. (Larousse – 1922)<p>
<p>(2) soeur = soror = cousine.<p>
Voir "Religion" I- L'Orthodoxie - p.2 Freddy Malot -Avril 2007 »</p>


