<p>« On peut nommer le régime domestique de l’avenir un Matriarcat Marital. Cela signifie : que l’apport historique des deux régimes du passé est conservé ; que leur côté unilatéral commun est aboli pour faire place à un vrai rapport ; que le Naturalisme primitif, que répudia l’Humanisme civilisé, retrouve sa place dans le rapport Réel instauré dans la nouvelle Union Sexuelle.</p>
<p>Je précise ce qu’implique le nécessaire néo-Matriarcat à fonder et édifier :</p>
<p>- Le ménage privé, la fameuse “cellule” est à abolir évidemment, puisque c’était le cadre du Maritalat notoirement révolu. Je signale qu’on met à présent en vente en Nouvelle Zélande, des appartements… sans cuisine, cet accessoire étant considéré comme inutile dans un ménage “moderne”… Cependant, il est illusoire de vouloir faire “dépérir” le Ménage privé sans s’attaquer à l’Entreprise. Sous la civilisation, l’Entreprise dominait les Ménages, la “personne morale” de la première étant supérieure aux personnes “physiques” des seconds. Le salut des Ménages ne va pas sans leur mainmise sur l’Entreprise.</p>
<p>- L’abolition du Maritalat, par l’extinction organisée des Ménages est vraiment la seule notion saine qui peut animer le mouvement Féministe. Et pourtant, plus encore que de conquête de vrais “droits” de la Femme, c’est du Salut des Enfants et des Vieux qu’il s’agit, eux dont la masse est absolument sans défense sous le Bestialisme actuellement dominant, et alors que les deux sexes se trouvent confondus dans ces catégories…</p>
<p>- Le Ménage privé aboli, les Personnes qu’il comprenait et dans les limites étriquées où elles pouvaient exister, se trouvent conquérir le vrai “respect” qu’on affichait pour elles seulement théoriquement auparavant. Ainsi, l’Union Sexuelle qui prend la place de l’ancienne union conjugale, délivrée des considérations économiques, patrimoniales, émancipée de la maternité privée, fait enfin primer le sentiment sur le désir, donne une réalité tout à fait ordinaire au mot Amour. Le Proxénétisme, jusqu’à présent “intouchable”, peut enfin être vu raisonnablement comme déracinable.</p>
<p>Voici comment, au total, se présente le tableau des régimes domestiques successifs de l’humanité :</p>
<p>1. Matriarcat – Primitifs ;</p>
<p>2. Maritalat – Civilisés ;</p>
<p>......... Bestialisme</p>
<p>3. Personnalisme – Communistes. »</p>
