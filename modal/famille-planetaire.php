			  <h4>Famille Planétaire</h4>
			  
			  <p>« Une seule chose compte : le Peuple, qu’il ne faut pas confondre avec l’Humanité. Si on sait ce que parler veut dire, nous sommes encore loin de pouvoir parler d’Humanité. Le mot n’aura un sens que dans un monde sans Argent et sans Armes. C’est pourquoi nous ne pouvons parler aujourd’hui que du Peuple. Le Peuple est ce qui peut et qui doit, et lui seul, tourner résolument et fermer le lourd volant de fonte de l’histoire dans le sens de l’avènement de l’Humanité.</p>
<p>Pourquoi aller vers la création de l’Humanité, vers l’établissement de la Famille <img class="img-in-text-right" src="img/paul.jpg"/>humaine délivrée du fléau de l’Argent et des Armes, passe-t-il pour une “utopie”, comme disent “les chefs du Système existant, dont la prétendue sagesse sera anéantie” (Saint Paul – I Cor. 2 : 6). Parce que la tâche de l’heure est de nous former en Peuple. En effet, s’il n’est pas d’Humanité, qui est notre but “final”, nous n’avons pas non plus de Peuple ; mais cela est notre but immédiat, nécessaire et possible, réellement à portée de notre main. »</p>

