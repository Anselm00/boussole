      <h4><b>Monde</b></h4>
	  		
	  <p>« Le Monde se présente comme le rapport “disjoint” <b>Nature-Humanité</b>, et le décalage se réduit au seul fait que nous
sommes en droit de considérer que les deux membres du couple “empiètent” l’un sur
l’autre complètement, qu’ils se chevauchent totalement, comme l’Intelligible et
l’Intelligence. Bref, “en dernière analyse”, la Nature est tout à fait Humaine, de même
que l’Humanité est tout à fait Naturelle. Ou encore : il est absolument légitime
d’aborder le Monde de deux manières opposées mais finalement identiques, parce
que n’étant que deux versions “contraires” de <b>la même SPONTANÉITÉ : celle de
la VIE</b>. C’est en effet la vie même qui se trouve à la base de notre Pensée, que nous
expérimentons sous la forme de la Conscience physique “à l’occasion” de notre travail
mental. Ce qui sépare donc seulement une Personne d’une Chose (comme on disait
autrefois), c’est que notre Conscience “physique” donne lieu à la Réflexion “morale”,
c’est-à-dire à la pensée proprement dite ; que le phénomène de la Conscience peut
être l’“objet” de la Réflexion <b>comme n’importe quoi d’autre</b> ; et donc que nous
SAVONS que la Matière est dotée de Spontanéité. Mais les spiritualistes n’ont pas vu
que ce savoir-là <b>ne ressemble à aucun autre</b>, du fait que nous ne pouvons en faire
AUCUN usage pratique ! Il le fallait cependant pour exalter la “substance” Esprit
exclusive, poser l’Âme humaine, et faire rayonner la pensée étroitement Rationnelle
(intellectualiste et moraliste).</p>
<p>Ainsi, l’Humanité appartient au Monde totalement, exactement au même titre que
la Nature. C’est bien pour cela que, dans les limites du rapport Nature-Humanité, la
Réalité se prête sans réserve à notre intelligence Théorique en tant qu’Absolue, et à
notre maîtrise Pratique en tant que Relative. »</p>
