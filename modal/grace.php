			  <h4>Grâce</h4>
			  
			  <p style="text-align:center;"><b>§ 166</b></p>
			  <p> « Le mystère réapparaît ouvertement dans <b>le Culte</b>, c’est-à-dire dans la question de
l’hommage que nous devons rendre à Dieu. Ici on expose comment <b>le Mérite et la
Grâce</b> se combinent dans <b>nos Œuvres</b>. À ce propos, disons ceci :</p>
<p>- Du fait que les actes mêmes des meilleurs croyants ne sauraient produire un seul
Saint en Ce-monde, il nous faut supposer le Secours divin dans notre Conduite.
<p>- N’empêche que ce que Dieu veut et peut faire pour notre Salut nous est
complètement caché (chap. VI).</p>
<p style="text-align:center;"><b>§ 179</b></p>  
				  <p>Il est admis d’opposer <b>Nature</b> humaine et <b>Grâce</b> divine. Est Naturel ce qu’on doit
et peut faire pour s’améliorer moralement. Est Gracieux ce que l’homme qui fait son
devoir a le droit d’attendre de Dieu pour ce qu’il ne peut pas produire sans son secours.</p>
<p style="text-align:center;"><b>§ 180</b></p>
<p>Que nous pouvons compter sur <b>la Grâce</b> divine, ceci nous est pleinement révélé par notre faculté Morale. Ceci dit, il nous est tout autant assuré que CE QUE Dieu
veut opérer en nous, nous est caché à jamais.</p>
<p style="text-align:center;"><b>§ 181</b></p>
<p>Il faut scrupuleusement s’en tenir à cette position, autrement c’est la porte ouverte
à deux dérives opposées : d’un côté, en venir à se prendre pour des thaumaturges (Celui qui fait des miracles) ;
de l’autre côté, devenir des vauriens qui attendent des cadeaux du ciel.</p>

<p style="text-align:center;"><b>§ 182</b></p>
<p><b>Les Moyens</b> de la Grâce ? Il n’en est qu’un seul : s’en rendre dignes par nos
propres efforts inlassables pour améliorer notre état moral. Par conséquent, les
prétendus “Sacrements” de l’Église chrétienne (y compris les deux seuls conservés par Calvin : le Baptême et le Dîner), vu qu’ils amoindrissent notre Responsabilité, au
lieu d’attirer la Grâce divine, … la repoussent !</p>
<p>Et pourtant, une fois qu’on a clairement donné la primauté au culte Intérieur, l’Église Parfaite ne nie pas l’utilité réelle d’avoir un culte Extérieur.</p>

<p style="text-align:center;"><b>§ 183</b></p>
<p>- Qu’est le <b>culte Intérieur</b> ? C’est le culte personnel, invisible, le “culte du Coeur” qui consiste dans l’observation de tous ses devoirs sociaux, en les assimilant à des
Commandements de Dieu.</p>
<p>- Pourquoi alors, la convenance d’un <b>culte Extérieur</b> ? Par ce que l’homme en
Ce-monde a besoin d’une expression visible de l’invisible, dont on peut user au profit même du culte intime. »</p>
