      <h4>Curieux</h4>
	  	
	  <img class="img-in-text-left" src="img/marat.jpg"/> <p><b> « Le nom</b> de Club. C’est un clin d’oeil aux groupes Radicaux que Marat connut en Angleterre, et
aux Jacobins-Cordeliers du “89” français. Pourquoi <b>Jeunes</b> ? On peut penser à 16-25 ans. Mais en<img class="img-in-text-right" src="img/lenine.jpg"/>
s’attachant à l’attribut précieux de “plaque sensible de la société” (Lénine), qui n’est pas l’apanage
“absolu” d’une tranche d’âge… Notre Jeunesse n’est pas avare d’Indignés-Altruistes-Intrépides, c’est
tout.</p>
		  <p>Le Club se lève, <b>curieux de tout</b> ce qui a pu VRAIMENT légitimer la société Préhistorique
(Parentale-Bourgeoise), ce qui a pu VRAIMENT l’amender ou la gâter ; et prêt à mener le bon combat
contre notre société de Mort. C’est ce qui fait du Clubiste originel le militant <b>généraliste</b>, avide de
tout sonder et embrasser.</p><img class="img-in-text-right-bigger" src="img/Vignette-Manuel.jpg"/>
<p>La contrepartie, c’est que le Club du départ juge l’ERM et son programme essentiellement comme
le dernier cri des utopies, et qui les intègre sans exception. Ainsi le Club anticipe-t-il réellement
l’espèce Convenable dans <b>la forme</b> : par sa Gratitude-Tolérance historique inouïe jusque-là ; bien
que, s’agissant du <b>contenu</b>, il ne l’annonce qu’“en puissance” à tous points de vue. Bref :</p>
<p>• Comme <b>association</b>, le Club se montre sérieux candidat, partisan, du Nouvel Homme, en
prenant la Préhistoire pour tremplin, allant ainsi à la découverte du Réalisme Vrai/Comm-Anar de
A à Z, par le chemin réel qu’il lui reste à découvrir et parcourir.</p>
<p>• Les différences <b>individuelles</b> vont se prononcer par le choix d’un engagement précis dans
un organe du Front, et avant tout dans son Assise (Parti-Syndicat). Car on n’est pas Convenable en
apprenant et récitant personnellement un catéchisme, mais dans l’exacte mesure où l’on aide à ce
que la Masse SE FASSE Peuple. D’où, de même, le “vrai” Peuple.</p>
<p>Je n’insiste pas sur les cas exceptionnels où un Jeune force directement – à juste titre – la porte
de l’ERM. Et des “réorientations” au sein du Peuple se feront bien sûr…</p>
<p>En définitive, le Club de Jeunes restera à jamais la <b>pépinière</b> de tous les Éléments du Peuple
Humain (Église comprise), en tandem avec le Conseil de Vétérans. »</p>

