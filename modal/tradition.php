			  <h4>Tradition</h4>
			  
			 
			 <p>« La Tradition est profondément matérialiste, bien que ce matérialisme soit très
différent du matérialisme philosophique. C’est ce caractère même qu’on évoque, sans
y prendre garde, en parlant de “fétichisme” au sens ordinaire.</p>
<p>Il y a donc un malentendu quand on taxe les primitifs d’“animistes”. C’est
prétendre, d’une part, qu’ils ne sont pas des Sujets comme nous et, d’autre part, qu’ils
admettent nos Objets, mais qu’ils affligeraient chacun, curieusement, d’une petite
“âme”. C’est, évidemment, aborder la Sagesse traditionnelle avec les lunettes du
spiritualisme Philosophique. En fait, on se laisse aveugler par la “méthode” de la
Tradition en refoulant à l’arrière-plan sa “conception”. Les fameux “esprits” des
primitifs ne peuvent avoir de rapport – encore est-il purement formel – qu’avec les
notions qui avaient cours dans la chimie ou la biologie de la civilisation Rurale :
l’“esprit de vin” (alcool), l’“esprit de sel” (acide chlorhydrique), ou les “esprits
animaux ou vitaux” (liant les membres au coeur et au cerveau).</p>
<p>Le matérialisme de la Conception traditionnelle est mis en évidence par le
caractère à la fois Opératoire et Immanentiste de cette dernière. »</p>


		<p>__________________________</p>


			
				  <p>Voir <a href="https://www.eglise-realiste.org/pdf/Sagesse_Trad_ed.pdf" target="_blank"> "La Sagesse Traditionnelle"</a>, p.7 -Freddy Malot - mai 1991 - Association le Sens de l'Histoire - Editions de l'Evidence - 2010</p>

				  <p>__________________________</p>	 
			 
			  <p>« Si la Tradition a précédé la Civilisation, c’est pour <b>une raison “extrinsèque”</b> au
problème social et humain : parce que l’Humanité ne pouvait d’abord surgir que du
monde vivant-animal. Et ce sont de “vrais hommes”, en qui nous pouvons pleinement
nous reconnaître, que furent les “sauvages”, mettant au monde une “vraie société” –
la société Parentale –, exactement au même titre que la Civilisation ultérieure. <b>Plus
même</b>, il faut dire que cet homme-premier, se dépouillant du vieux singe, effectua le
plus gros du travail historique de l’humanité. En effet, une fois l’homme établi,
malgré la difficile et douloureuse transition de la Tradition à la Civilisation, cette
Révolution apparaît un jeu d’enfant à côté de la transformation du singe en Homme.
Commence-t-on à comprendre pourquoi l’Église Réaliste n’est pas du tout
“progressiste” ? »</p>
<p>__________________________</p>


			
				  <p>Voir <a href="https://www.eglise-realiste.org/pdf/Trois_Humanites_ed.pdf" target="_blank"> "Les Trois Humanités"</a>, p.5 - Talib Freddy - janvier 2007 - Editions de l'Evidence - 2010</p>

