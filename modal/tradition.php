				 <p>« Repoussons deux jobarderies inverses qui se valent :</p>
<p>- L’Innocent <b>PARENT</b> (de la Tradition) vivrait en “Harmonie avec la <b>Nature</b>” !</p>
<p>Merci bien ! Hurrah l’Égalité grégaire, entée sur l’économie de proie, les sacrifices rituels et la Loi
du Talion ! Et puis, le destin social dicté par les catastrophes naturelles : “déluges”, séismes, famines,
pestes…</p>
<p>Qu’en est-il donc du Bon Sauvage, pris dans les chaînes de ses <b>Devoirs</b> Ancestraux, de nature
Mythique, et matière à idolâtrie biologique (Totem et Tabou) ?</p>
<p>- Le <b>BOURGEOIS</b> Éclairé (de la Civilisation) jouirait du “Modèle de <b>Société</b>” !</p>
<p>Merci bien ! Hurrah la Liberté réifiée, entée sur l’Esclavagisme, les révolutions et le Dur Cachot
(carcero duro) ! Et puis, la Nature malmenée – nos corps y compris – pour se trouver réduite à une
énorme Machinerie…</p>
<p>Qu’en est-il donc du Vertueux Gentleman, dupe de ses <b>Droits</b> Naturels, de nature Dogmatique,
et matière à fétichisme sociologique ? (Propriété et Sûreté). »</p>

