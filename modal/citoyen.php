			  <h4>Loi</h4>
			  
			  <p>« D’une manière générale, la Constitution de 1791 juge tout à fait “naturel” que les Propriétaires et Citoyens Actifs forment une minorité sociale.
<p>- Les choses se présentent de la façon suivante :</p>
On pose la Citoyenneté active comme déterminée par la Propriété active.</p>
<p>Le Citoyen actif simple, de base, est sélectionné par l’impôt qu’il paie (le Cens). Quant au citoyen actif méritant le titre d’électeur, autorisé à élire les Représentants (députés), il est sélectionné par sa Fortune. La fortune est estimée à partir des Revenus patrimoniaux déclarés, revenus qu’on veut d’un montant quatre fois supérieur pour un Électeur d’une grande ville que pour un Électeur d’un village.</p>
<p>- Il est vrai que la Constitution de 1791 se montrait néanmoins d’une audace extraordinaire, puisque : il y avait une Chambre unique des Représentants, toute puissante vis-à-vis de l’Exécutif (le Roi) ; les électeurs riches pouvaient nommer Députés de simples citoyens actifs ; quantités de responsabilités relevaient directement des citoyens actifs de base (curé, arbitre judiciaire, etc.) ; une foule de fonctionnaires proprement dits étaient élus (juges, chefs de la garde nationale, etc.).</p>
On ne se rend pas compte à quel point la seule Constitution de 1791, bien qu’ignorant le “suffrage universel”, si elle était remise en vigueur aujourd’hui, serait regardée comme absolument “anarchiste” par la Caste Noire dominante ! »</p>
