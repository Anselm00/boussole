			  <h4>Occultisme</h4>

			  
			   <p>«Les primitifs posent la Matière comme la Substance essentielle et dernière de la Réalité. J’insiste sur le fait qu’il ne s’agit évidemment pas de la Matière au sens civilisé, passive, inerte, Non-Être pour-Nous et Néant pour-Dieu. Au contraire, la matière des primitifs se présente au plus haut point comme active, dynamique. Dans la misère mentale à laquelle oblige le Paganisme dominant actuel, nous voyons proliférer l’Occultisme, qui consiste dans la Mystique dégénérée, qui appartient lui-même au paganisme dominant et le creuse de manière débridée, conjointement avec l’“extrémisme” païen opposé, le Cynisme ou Athéisme dégénéré. Or, l’Occultisme, tirant parti du caractère actif de la Matière des primitifs, se rue sur ce fait de mille manières, organise l’amalgame de la Superstition primitive avec la Religion civilisée, et développe un sous-cléricalisme de Désespérés. C’est ainsi que nous voyons s’étendre depuis 150 ans la mystique Nihiliste des Occultistes. Et l’on se toque de Kabbale juive, de “spiritualité” Védique, de Shinto, de Runologie Nordiste, de Mazdéisme, de Celtisme, de Chamans d’Asie ou de Sorciers d’Afrique... L’Occultisme, comme le Cynisme, développe sa gangrène par vagues, de sorte qu’on le voit arriver périodiquement à supplanter le Cléricalisme, ce qui est caractéristique des phases d’avant-guerre, où les Usuriers et les Tueurs qui dominent la planète sont amenés à se polariser en Blocs Démocrates-Fascistes. Nous en sommes là, et au degré le plus extrême qui puisse se concevoir. »</p>
			  
			  <p>__________________________</p>

<p>Voir <a href="https://www.eglise-realiste.org/pdf/Mere_Fondamentale_ed.pdf" target="_blank"> "La Mère Fondamentale"</a>, p.10,11 - Freddy Malot - septembre 1999 - Editions de l'Evidence - 2010</p>

<p>__________________________</p>
			    
<p>Tout d’abord, nous avons la forme “bien-pensante” de la putréfaction
philosophique, bénéficiant du “Nihil Obstat” des ministères successifs de la police des
esprits, auxquels est confiée la tutelle de l’École, de la Presse et de la Culture
décadentes contemporaines. De <b>Nietzsche à Freud</b>, ces coqueluches de la pensée
académique, ne voit-on pas raison/vérité, ou mentalité du pourquoi/comment, se
crisper douloureusement sur eux mêmes, mobilisés rageusement pour se dévorer ?
En lieu et place de ce que connaissait feue la civilisation moderne : la Personne
auréolée d’âme, ordonnée à l’idéal de l’Humanité, nous avons à présent une meute
d’Egos torturés par leur sur-humanité ou leur subconscient ! Et comme ersatz de
l’esprit lumineux de la philosophie épanouie dans le rationalisme déiste, on nous
offre la cruelle “volonté de puissance” ou la trouble “pulsion libidinale”…</p>
<p>Ensuite, en marge des théories qui occupent le devant de la scène, et qui
constituent les modes officielles, un environnement mental pervers se développe en
un double cercle. En premier lieu, c’est le cordon sanitaire de l’administration des
esprits, qui prend la forme, à la fois du pharisianisme de <b>la “laïcité”</b> organisant l’état
de siège cérébral du peuple, et de l’exhumation du cadavre de l’<b>Église</b> médiévale
adonnée à des simagrées d’un culte sans foi. En second lieu, l’esprit civilisé, forcé à la
marginalisation, engendre la vase montante du sectarisme forcené : en même temps
celui du <b>Cynisme</b> éhonté criant “Merde à Dieu”, et celui de l’<b>Occultisme</b> frénétique
qui psalmodie Nostradamus.</p>
<p>Dans cette panique spirituelle contemporaine, il est vraiment urgent de clamer
avec Saint Paul : “Seigneur, ils ont tué tes prophètes !” (Rom. 11), en désignant par là
toute la galerie des grands philosophes civilisés. Mais tout cela est aussi bon signe,
d’une certaine façon ! Eh, oui ! Cela indique que désormais, bien que nous nous
entêtions à penser de manière civilisée, notre propre odeur “philosophique”
commence à nous empester sérieusement. C’est finalement la preuve que le besoin
d’un grand bain spirituel purificateur devient très-pressant !</p>

