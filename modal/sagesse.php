			  <h4>Sagesse</h4>
			  
			  <p>« La première énigme à deviner, dès que l’on s’émancipe du préjugé civilisé selon
lequel la pensée philosophique – dite “logique” – tombe du ciel, est une propriété
intrinsèque de l’“âme” humaine, c’est d’analyser la genèse réelle de ce mode de
pensée.</p>
<p>À ce propos, la pensée philosophique, même quand elle était encore bourrée de
sève, et n’était pas encore cet ectoplasme desséché et couvert d’ulcères qu’elle est
aujourd’hui, refusait tout bonnement de se poser cette question-clef de sa propre
origine sociale. C’est au contraire ce qui doit être notre toute première
préoccupation : déclencher une procédure de “recherche de maternité” s’appliquant à
la Philosophie.</p>
<p>Cet enfant spirituel qu’est la Philosophie cesse d’être de “mère inconnue” sitôt
qu’on entreprend une enquête quelque peu approfondie, engagée de manière
réellement critique, concernant le mode de pensée qui fut celui de l’humanité
Primitive. Ce mode de pensée initial de l’humanité, nous lui donnons le nom de
“Sagesse Traditionnelle”.</p>
			  <p>Étant donné que la Philosophie refuse de s’envisager elle-même comme historique,
c’est-à-dire comme contradictoire et transitoire, même les gens les mieux
intentionnés vis-à-vis de la Sagesse Traditionnelle n’ont jamais pu réellement la
comprendre. Ce n’est pas faute, pourtant, d’avoir accumulé les matériaux nécessaires,
que missionnaires et ethnologues ont entassés à qui mieux mieux. Toutes les gloses
dont ils ont habillé leurs enquêtes si riches, n’ont jamais réussi à élever ces dernières
plus haut que le niveau de la parade folklorique.</p>
<p>Ainsi, nous eûmes la théorie de la <b>mentalité “prélogique”</b> de Lévy-Bruhl. Ici,
l’on fait état d’un semblant de filiation entre la cervelle des primitifs et celle des
civilisés ; mais tout tombe à l’eau, du fait que les premiers se trouvent ravalés au rang
d’enfants vis-à-vis des seconds. La seule histoire concevable dans ce cas consisterait
dans l’accession des sauvages “émotifs” au stade “rationnel” des civilisés. De cette
manière, la mentalité philosophique est présupposée comme l’état “normal” de
l’humanité et, une fois parvenue à cet état, l’humanité se trouve évadée de l’histoire.</p>
<p>Levy-Strauss apparaît ensuite. Il affiche une sympathie pour la pensée primitive,
qui atteint la limite permise par le préjugé civilisé. En effet, cette fois, <b>la “pensée
sauvage”</b> est présentée comme tout autant adulte et valable que la nôtre. Mais de
quel prix se paie cette humilité civilisée ! Maintenant, toute façade historique
disparaît complètement. L’humanité se trouve scindée en deux blocs, dont l’un a
choisi – on se demande pourquoi – la Sagesse, et l’autre la Philosophie. Les deux modes de pensée sont posés comme totalement hétérogènes, comme s’ils étaient
attachés à deux types d’humanité, habitant des planètes différentes ! Ce qui
n’empêche pas l’une de ces humanité, celle de Lévy-Strauss, de raisonner sur l’autre !</p>
<p>En réalité, la Sagesse Traditionnelle est tout simplement la mère des Systèmes
Philosophiques de l’humanité civilisée. N’est-il pas vrai que chaque peuple a eu ses
“gaulois” ? Le mode de pensée civilisé, fondé sur le “spiritualisme logique” est
purement et simplement la <b>négation directe</b> du “matérialisme mystique” des
primitifs. Et, une fois la Philosophie établie, loin que l’histoire soit alors achevée, c’est
seulement la dernière phase de la préhistoire du travail mental qui s’accomplit.
Ultérieurement, grâce à la philosophie mais en la chassant à son tour, l’histoire
humaine proprement dite peut commencer, c’est-à-dire l’époque de la pensée
affranchie du préjugé, de la pensée pleinement digne de ce nom. »</p>

