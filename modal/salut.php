	<p>LES AGRÉÉS DU CIEL</p>
	<p>Ceux-ci sont bien délivrés de l’Angoisse vécue Ici-bas. Mais point du tout pour
	connaître une Confiance passive. Ce dont ils sont délivrés, c’est seulement de tout
	Doute concernant l’existence de Dieu et du décret de ce dernier quant à leur Salut.
	Ceci établi, le Vrai Travail des Élus commence, et c’est ce que signifie la vraie Vie :
	travail cette fois purement Intellectuel, et qui ne connaît ni repos ni sommeil.</p> »

	<p>Voir <a href="https://www.eglise-realiste.org/pdf/Mentalite_Religieuse_ed.pdf" target="_blank">"Mentalité Religieuse"</a>, pages 29-30 - Talib Freddy - Mars 2004 </p>
		  

