      <h4>Salut</h4>
	  
		
		<p>« Confiance et Angoisse travaillaient à juste titre l’âme
du Croyant soucieux Ici-bas de son salut ; mais devait-il pour autant négliger l’étude de ce qu’il advenait de ce Drame dans l’Au-delà ? La question se posait
infailliblement. À ce propos, deux préjugés vulgaires doivent être écartés :</p>
<p>• Le premier préjugé prétend qu’on ne peut rien dire de l’Autre Monde. Ceci est
totalement inadmissible pour la religion vivante : l’autre monde n’est jamais que du
Monde, une contrée du domaine des Créatures, qu’il est donc indigne d’amalgamer
au Mystère même de Dieu. Nous pouvons et Devons nous prononcer précisément sur
l’Au-delà dès que nous nous voulons Croyants.</p>
<p>• Le second préjugé consiste à régler hâtivement le problème de l’Au-delà en
déclarant superficiellement : les Sauvés y éprouveront une Confiance entière,
dépourvue d’Angoisse, et les Réprouvés auront en partage une Angoisse complète
privée de Confiance. N’oublions pas que l’Autre Monde ne se distingue de celui-ci que
par le fait qu’il est le Vrai monde, perpétuel et non éphémère, et en particulier la vraie
patrie des Hommes, où ils connaîtront en plein le sort qu’ils se sont choisi dans
l’Épreuve que propose l’Ici-bas.</p>
<p>Qu’on ne dise donc pas que l’Autre Monde est peuplé d’âmes au sens d’esprits
“nus”, dépourvus de tout corps ; ses habitants ont bel et bien un corps de matière
“subtile” (invisible), lesquels corps appartenant eux-mêmes à une “Nature”
surnaturelle, celle-là même qui constitue la Cité de l’Au-delà.</p>
<p>Qu’on ne dise pas non plus que les habitants de l’Autre Monde sont comme
“minéralisés”, soit dans le Bien soit dans le Mal, les Bienheureux soumis au gavage
des Récompenses et les Damnés soumis au canonnage des Peines. Les païens disent
ou pensent : on doit bien s’ennuyer dans la compagnie des Béats ! C’est ce qui fait leur
perte : le Ciel est le commencement de la Vraie vie pour les Bons, de même que
l’Enfer est le commencement de la vraie Mort pour les Méchants, lesquels sont donc
rémunérés avec justice, selon leur propre vœu, puisqu’ils n’ont jamais pratiqué que le
culte de la Mort, en combattant par tous les moyens la civilisation de la Terre. La
montagne des victimes de leur barbarie en témoigne !  »</p>
