      <h4>Salut</h4>
	  
		
		<p>« La religion établie fut toujours nécessairement travaillée par le double sentiment
de Confiance-Angoisse. Les païens dominants attribuent invariablement et
sournoisement la confiance religieuse à l’Idiotie congénitale de la masse des Fidèles,
et l’Angoisse religieuse au “Haut-mal” héréditaire qui afflige les Saints. Persuadons-nous bien de ce fait, en n’en retenant qu’une seule chose : les célébrités du genre de
Léon XIII, le “pape ouvrier”, et Émile Combes, Défroqué en chef du Grand Orient,
“fils d’un humble prolétaire”, ne doivent pas nous amuser de leurs scènes de ménage ;
elles forment tout bonnement un couple païen monstrueux et, tels les Frères Siamois
Chang et Eng, si la mésintelligence peut parfois troubler l’accord de la paire, il n’est
qu’une seule volonté méphistophélique pour la mouvoir au bout du compte.</p>
L’esprit affranchi de ce redoutable diversionnisme peut enfin s’appliquer à ce qui
est véritablement de son ressort. Confiance et Angoisse travaillaient à juste titre l’âme
du Croyant soucieux Ici-bas de son salut ; mais devait-il pour autant négliger l’étude de ce qu’il advenait de ce Drame dans l’Au-delà ? La question se posait
infailliblement. À ce propos, deux préjugés vulgaires doivent être écartés :</p>
<p>• Le premier préjugé prétend qu’on ne peut rien dire de l’Autre Monde. Ceci est
totalement inadmissible pour la religion vivante : l’autre monde n’est jamais que du
Monde, une contrée du domaine des Créatures, qu’il est donc indigne d’amalgamer
au Mystère même de Dieu. Nous pouvons et Devons nous prononcer précisément sur
l’Au-delà dès que nous nous voulons Croyants.</p>
<p>• Le second préjugé consiste à régler hâtivement le problème de l’Au-delà en
déclarant superficiellement : les Sauvés y éprouveront une Confiance entière,
dépourvue d’Angoisse, et les Réprouvés auront en partage une Angoisse complète
privée de Confiance. N’oublions pas que l’Autre Monde ne se distingue de celui-ci que
par le fait qu’il est le Vrai monde, perpétuel et non éphémère, et en particulier la vraie
patrie des Hommes, où ils connaîtront en plein le sort qu’ils se sont choisi dans
l’Épreuve que propose l’Ici-bas.</p>
<p>Qu’on ne dise donc pas que l’Autre Monde est peuplé d’âmes au sens d’esprits
“nus”, dépourvus de tout corps ; ses habitants ont bel et bien un corps de matière
“subtile” (invisible), lesquels corps appartenant eux-mêmes à une “Nature”
surnaturelle, celle-là même qui constitue la Cité de l’Au-delà.</p>
<p>Qu’on ne dise pas non plus que les habitants de l’Autre Monde sont comme
“minéralisés”, soit dans le Bien soit dans le Mal, les Bienheureux soumis au gavage
des Récompenses et les Damnés soumis au canonnage des Peines. Les païens disent
ou pensent : on doit bien s’ennuyer dans la compagnie des Béats ! C’est ce qui fait leur
perte : le Ciel est le commencement de la Vraie vie pour les Bons, de même que
l’Enfer est le commencement de la vraie Mort pour les Méchants, lesquels sont donc
rémunérés avec justice, selon leur propre vœu, puisqu’ils n’ont jamais pratiqué que le
culte de la Mort, en combattant par tous les moyens la civilisation de la Terre. La
montagne des victimes de leur barbarie en témoigne !</p>
<p style="text-align:center;">• • •</p>

<p>Détails sur la condition des Trépassés.</p>
	<p><b><i>LES AGRÉÉS DU CIEL</i></b></p>
	<p>Ceux-ci sont bien délivrés de l’Angoisse vécue Ici-bas. Mais point du tout pour
	connaître une Confiance passive. Ce dont ils sont délivrés, c’est seulement de tout
	Doute concernant l’existence de Dieu et du décret de ce dernier quant à leur Salut.
	Ceci établi, le Vrai Travail des Élus commence, et c’est ce que signifie la vraie Vie :
	travail cette fois purement Intellectuel, et qui ne connaît ni repos ni sommeil.
	
	<p>Quel est l’objet du Travail des Élus ? Il est parallèle à l’œuvre civilisatrice
poursuivie par les Croyants de l’humanité charnelle de notre monde, et consiste à
dissoudre, abolir toujours plus la double frontière qui délimite le royaume d’“Enhaut” : celle qui sépare l’Au-delà de l’Ici-bas (frontière que nos Croyants attaquent de
l’autre côté) et celle qui sépare l’Au-delà de Dieu.</p>
<p>Comment se présente le Royaume ? En prenant pour exemple le schéma
catholique, disons que la Communauté des Saints y a pour Chef le Christ, l’hommeDieu (lui-même adossé au Verbe Éternel), tandis que la Cité surnaturelle est présidée
par sa Mère, la Vierge “reine du ciel”.</p>
</p>Que fait la Communauté des Saints, animée de la plus grande et croissante
<b>FÉBRILITÉ</b>, sous la direction du couple béni Jésus-Marie ? Elle tend d’abord à se
souder elle-même toujours plus, visant à former un seul Sujet, revêtu d’un corps
glorieux, Unique ; ensuite, parallèlement, s’opère la dissolution incessante de la
matière Subtile de l’Au-delà ; ce qui correspond à la confusion intensifiée des
Habitants et de la Cité du Ciel.</p>
<p>Quel est le résultat de cette sanctification ininterrompue du Ciel ? Il est paradoxal
pour la Raison humaine, mais confirme d’autant le Mystère de l’Esprit absolu. Peuton concevoir que l’œuvre de la Communauté des Saints aboutisse ? Est-il possible que
la Droite Perpétuelle du Temps cesse d’être le “mauvais infini” de Hegel, une
Asymptote du Cercle Immobile de l’Éternité ? Asymptote signifie A-SUN-PIPTEIN,
“sans tomber avec”, s’approcher constamment sans jamais rencontrer. Que se
passerait-il si le Temps “tombait” dans l’Éternité en parvenant à y toucher, se
ramassant alors, pour s’y évanouir, en un seul Point tangent de son Orbe ? Peut-on
imaginer que le Relatif puisse se faire l’Absolu ? Ou bien supposer l’inverse même :
que l’Absolu se noie dans le Relatif ?</p>
<p>Si le Christ peut envelopper la Communauté des Saints pour ne faire qu’un Sujet
Divin dans l’Au-delà, ce même Sujet Divin se dépouille dans le même moment de son
corps glorieux ; par suite tout le non-être matériel de la Création tombe dans le néant
de Dieu ; alors, le Verbe créateur éternel se fond indistinctement dans la Trinité ;
finalement, Dieu n’est plus qu’Un, sans aucune expression Personnelle, et seul
Subsiste le Mystère. Dieu sans Création ? L’abolition de la Création serait-elle la
réussite du plan voulu par Dieu ? Dieu est le Bien, disait Thomas d’Aquin, et “le
propre du Bien est de se diffuser” (Bonum est sui diffusium). Faut-il que la Foi aille
jusqu’à abandonner cette conviction, pour ne pas porter atteinte à la Transcendance
absolue ?</p>
<p>Les Bienheureux du Ciel n’éprouvent plus l’Angoisse qui les tenaillait
périodiquement Ici-bas ; mais à présent, pressant toujours plus le Temps d’accrocher
l’Éternité, ils expérimentent toujours plus le fait qu’ils en reculent d’autant la limite indéfinie, mesurent toujours plus qu’en s’approchant de Dieu-pour-Nous, ils
s’éloignent dans la même proportion de Dieu-en-Lui-même ; bref, la Fébrilité
croissante des Élus s’accompagne d’une <b>STUPEUR</b> croissante devant le Mystère :
Dieu ne leur présentera à jamais que sa “nuque”, et non point sa “face” (Exode, 33 :
20, 23). À moins qu’Il n’en décide autrement, et qu’en un Instant ahurissant tout
s’abîme dans son Néant…</p>
<p><b><i>“Stupeur ! Outre mouvance dont tous les jours sont faits,</i></b></p>
<p><b><i>Un autre mouvement est là, gros d’épouvante :</i></b></p>
<p><b><i>Les choses ne muent plus comme avant désormais…”</i></b></p>
<p style="text-align:right;">Luís de Camões (1525-1580) : Mudança »</p>

