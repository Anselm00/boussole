			  <h4>Matrice</h4>
			  
			  <p>« La mentalité de l’homme primitif était Matérialiste. Cela signifie que le Vitalisme
constituait sa Conception objective, et le Symbolisme constituait sa méthode
subjective.</p>
<p>La mentalité Matérialiste de l’homme primitif était solidaire d’un préjugé
Mythique. Cela signifie que s’imposait invinciblement dans la pensée primitive le
présupposé selon lequel la Réalité toute entière se résorbait, en dernière analyse,
dans la Puissance Fondamentale de la Matière, puissance Secrète en tant que telle.
Cependant, l’homme primitif professait aussitôt que la Puissance Secrète se
donnait spontanément, vis-à-vis du monde, comme Mère Féconde Immanente. Je
précise que “l’immanence”, dans le contexte Matérialiste, se rapportait non pas au
monde immédiat des primitifs, mais au contraire à l’en-deçà de l’En-Deçà, au pardessous
le “royaume des ombres” même.</p>
				  <p>Si nous passons au Monde proprement dit, qui comprenait les deux versants de
l’Ici-haut et de l’En-deçà, le primitif le disait bien sûr Émané de la Mère. Comment
cela ? De la <b>VULVE</b> (matrice) humide et trouble de la mère, émergeait comme il se
doit son fruit : le bouton clos du <b>LOTUS</b>. Et le Monde n’était autre que le lotus
ouvert et parvenu à floraison complète, tendant à se faire la Riche corolle aux “mille
pétales”, perpétuée indéfiniment par cycles.</p>
<p>Sous toutes sortes de formes successives, en Orient comme en Occident, on
retrouve cette figuration du Matérialisme primitif en un couple de type Vulve-Lotus,
ou son analogue. »</p>

