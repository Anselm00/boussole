			  <h4>Territoires</h4>
			  
			  <p>« Du fait que le travail est ainsi dépendant-indépendant de la Nature, une <b>double représentation
sociale</b> – Locale et Générale – s’impose. Ceci explique que les syndicats de salariés français ont tout
à la fois des Unions Locales et des Fédérations Professionnelles (les secondes ayant hégémonie sur
les premières). De même, en politique, le Sénat et l’Assemblée (la seconde ayant hégémonie sur le
premier ; et leur réunion en Congrès Parlementaire étant prévue).</p>
<p>Le Comm-Anar, avec sa <b>double Assise</b> populaire, trouve merveilleusement son compte dans un
tel bicaméralisme ; mais cette fois À PARITÉ ! (comme la position d’un lieu sur la planète demande
la rencontre du Parallèle et du Méridien). Et l’on n’a <b>plus à craindre</b> le rôle réactionnaire des
Landlords anglais de 1642 (les Cavaliers face aux Têtes Rondes), ou des Fédéralistes français de 1792
(les Girondins face aux Montagnards).</p>
<p>Il est “naturellement” à prévoir que les <b>Femmes</b> pèseront plus que les Hommes dans la Chambre
des Territoires, avec le rapport inverse dans la Chambre des Tâches. D’autres particularités
“physiques” auront bien sûr leurs avocats dans le nouveau Sénat : la prime enfance, les vieux perclus,
les handicapés, accidentés et malades. »</p>
