      <h4>Création</h4>
	  	
	  <p>« N’oublions jamais que la Création est le Mystère, que vis-à-vis de nous c’est la Raison 
	  Absurde, la Nécessité Libre, la Légalité Arbitraire. Bien sûr que, d’un côté, rien ne contraint 
	  Dieu, ni à créer le Monde, ni à maintenir sa Création ; mais, d’un autre côté, tout l’y oblige : 
	  étant Esprit, il doit être Sujet, étant Sujet il doit penser quelque chose, et pensant quelque 
	  chose il doit le Parler, et le parler dans la Création ! La preuve ? Elle est bien simple : 
	  n’y a-t-il pas ce Monde ! Et n’est-il pas tel qu’on devait s’y attendre : comprenant l’Homme 
	  avec un grand “H”, tout à fait constitué pour répondre au Créateur, car homme défini par son 
	  esprit à lui, son Âme ; bref l’homme Civilisé, le “Bourgeois” au sens le plus général du terme. 
	  Ceci étant bien établi, il va y avoir bien des problèmes secondaires à régler.</p>
<p>Elle est bien extraordinaire la Parole de Dieu, dont l’expression n’est autre que le <img class="img-in-text-right-bigger" src="img/epiphanie.jpg"/>
Monde même ! Pas tant que cela, pour peu que le Monde soit vu à fond avec l’oeil de 
l’Âme par nous autres bourgeois, c’est-à-dire de part en part comme <b>Épiphanie</b> de l’Être 
Suprême, comme la seule parution possible de son invisibilité, comme le Phénomène propre de son Noumène. 
On ne peut donc comprendre le Monde que comme Création ; et si quelqu’un a des difficultés à le voir ainsi, 
cela ne vient pas de Dieu mais de quelque chose qui ne va pas du côté de l’oeil de l’Âme chez celui qui 
rencontre ces difficultés. »
	  
<p>__________________________</p>

<p>Voir <a href="https://www.eglise-realiste.org/pdf/Societe_Politique_ed.pdf" target="_blank">"La Société Politique"</a>, p.6 - Talib Freddy - avril 2005 - Editions de l'Evidence - 2010</p>	  


