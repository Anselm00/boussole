			  <h4>Humaniste</h4>
			  
			  <p>« Cette situation civilisée des Familles privées prit une 
				  forme radicale à l’époque moderne d’épanouissement de la 
				  propriété privée, s’exprimant dans une différenciation achevée 
				  des entreprises et des ménages, associée à la polarisation des 
				  ménages en ménages de capitalistes et ménages de salariés. 
				  Le rôle intensément civilisateur de l’étape moderne est 
				  fondamentalement lié à cette simplification de la propriété 
				  privée qui lui est inhérente. La conséquence directe de la 
				  nette polarisation de la société civile moderne en familles 
				  privées de capitalistes et de salariés est que la puissance 
				  publique se différencie nettement, de son côté, de la société 
				  civile et prend la forme simple de la monarchie parlementaire. 
				  C’est cet ensemble, intégrant les éléments spirituels et affectifs 
				  qui lui sont inhérents (religion-science, mystique-art), qui constitue 
				  la Nation Humaniste, caractéristique de l’époque civilisée suprême, 
				  celle des Temps Modernes.</p>
			  
				  <p>Avec la décadence Contemporaine qui fit suite à la crise de 
					  1848 de la société Moderne, les choses changent du tout au tout. 
					  Il n’est plus du tout question de Nation Humaniste, et à l’Ouest 
					  encore moins qu’au Sud. Alors, aux capitalistes se substitue 
					  une Oligarchie de ménages parasitaires des entreprises placées 
					  sous le signe du monopolisme ; et aux salariés proprement dits 
					  se substitue une “foule solitaire” de “ménages” purs, n’ayant 
					  absolument aucun lien avec l’entreprise devenue pour eux une 
					  simple caserne, et donc dépouillés de la possession, même 
					  formelle, d’une famille complète. Cette situation proprement 
					  manichéenne de la société civile, scindée en ménages de purs 
					  parasites et ménages soumis au pur travail forcé, que l’entreprise 
					  exclut absolument les uns des autres au lieu de les relier, 
					  cette situation appelle nécessairement une puissance publique 
					  dominatrice, un régime de démocratie dictatoriale, chargée de 
					  maintenir ensemble les éléments civils de force, comme on cercle 
					  de fer un tonneau. La perfidie sadique du système veut que les 
					  masses salariées, interdites de toute expression de leur 
					  citoyenneté civile propre, soient appelées à venir encenser 
					  leur déchéance forcée en apportant leurs “suffrages” politiques 
					  à tel ou tel clan de la ploutocratie régnante qui les méprise 
					  d’autant plus. Tel est le véritable sens du passage du libéralisme 
					  moderne à la “démocratie” contemporaine. La vieille Nation humaniste 
					  ayant perdu toute substance, il n’en reste que la coquille vide, 
					  qui se réduit à un État militariste, maître d’un Territoire dont 
					  la population est livrée à une mafia jouissant de l’impunité. »</p>
