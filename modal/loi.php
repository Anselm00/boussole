			  <h4>Loi</h4>
			  


			  <p>« <i>Dieu étant maître des Rois (..) <b>la loi de Dieu</b> a toujours été une borne. (Les) Rois
doivent plus que tous les autres agir par raison. (..) Un Roi qui a les mains nettes, le
coeur pur et la langue innocente, n’a pas peu de vertu. (..) J’ai toujours estimé que tel
défaut, blâmable en toute sorte de personne, est une dangereuse imperfection aux
souverains qui, étant à titres plus particuliers que tous les autres l’image de leur
Créateur qui, par sa nature, fait du bien à tout le monde, ne peuvent ne le pas imiter
en ce point sans en être responsables devant lui.</i></p>

<p><i>Si <b>les peuples</b> étaient trop à leur aise, il serait impossible de les contenir dans les
règles de leur devoir. Il faut les comparer aux mulets qui, étant accoutumés à la
charge, se gâtent par un long repos plus que par le travail. Certains sujets sont
tombés en la servitude de leurs ennemis pour vouloir trop de liberté sous leur
souverain naturel. Mais le sens commun (apprend) à chacun qu’il doit y avoir
proportion entre le fardeau et les forces qui le supportent.</i></p>
<p><i>Je fais marcher <b>la peine devant la récompense</b>. Le bien devant être embrassé
pour l’amour de soi-même, à la rigueur on ne doit point de récompense à celui qui
s’y porte. Le bien et le mal sont deux ennemis entre lesquels il ne se doit faire ni
quartier ni échange.</i></p>
<p><i>La verge, qui est le symbole de la justice, ne doit jamais être inutile.</i></p>
<p><i>Les <b>nouvelles lois</b> ne sont pas tant des remèdes aux désordres des États que des
témoignages de leur maladie et des preuves assurées de la faiblesse du
gouvernement.</i></p>
<p><i>La punition et les bienfaits regardent le futur plus que le passé. Le Roi sera
puissant par la <b>possession du coeur</b> de ses sujets qui, considérant le soin qu’il
aura de leur bien, seront portés à l’aimer par leur propre intérêt.</i></p>
<p style="text-align:center;"><b>•••</b></p>
<p><i>Si les princes n’ont un soin particulier de se rendre tels que leur exemple soit une
Loi Parlante ;</i></p>
<p><i>S’ils sont paresseux à établir le règne de Dieu, celui de la raison et de la justice
tout ensemble ;</i></p>
<p><i>S’ils manquent à protéger les innocents, à récompenser les signalés services qui
sont rendus au public, et à châtier les désobéissances et les crimes ;
S’ils ne s’appliquent pas, autant qu’ils doivent, à prévoir et prévenir les maux qui
peuvent arriver ;</i></p>
<p><i>Si, en toutes occasions, ils ne préfèrent les intérêts publics aux particuliers ;
Ils se trouveront bien plus coupables que ceux qui transgresseront les lois. (..)
Beaucoup se sauveraient comme personnes privées, qui se damnent comme
personnes publiques.</i></p>
<p style="text-align:right;">Richelieu, Testament Politique (1637)</p>
<p style="text-align:center;"><b>________</b></p>
<p><i>Toutes <b>les révoltes</b> qui se sont produites ont été causées par les injustices
commises par les gouverneurs.</i></p>
<p style="text-align:right;">Al-Ma’mün (813/833)</p>
<p style="text-align:center;"><b>________</b></p>
<p><i>Si vous mettez, me disait-il, les peuples dans l’abondance, ils ne travailleront
plus ; ils deviendront fiers, indociles, et seront toujours prêts à se révolter. Il n’y a
que la faiblesse et la misère qui les rende souples et qui les empêche de résister à
l’autorité.</i></p>
<p><i>En voulant soulager les peuples, me disait-il, vous rabaissez la puissance royale,
et par là vous faites au peuple même un tort irréparable car il a besoin qu’on le
tienne bas pour son propre repos.</i></p>
<p style="text-align:right;">Fénelon, Les Aventures de Télémaque (1699) »</p>
