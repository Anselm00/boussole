			  <h4>Démocratie Centralisée</h4>
			  
			  
<p>La Démocratie signifie que les Membres désignent un Chef parmi eux à tous les
degrés. Ceci n’a rien à voir avec l’“élection” de “candidats” irresponsables. Le poste de
Chef est “imposé” par la majorité, à celui qu’elle considère comme le plus exemplaire.</p>
Qui a peur des Chefs manque de Personnalité. Cet individu fraude contre l’Esprit.</p>
<p>• Le Centralisme se traduit par la soumission de la base au Chef. L’Unanimité
des Membres obéit “aveuglément” au Chef désigné, dans le cadre du mandat qu’il
assume et pour la durée précise de ce mandat.</p>
<p>Qui craint l’Unanimité se dérobe à la Communauté. Cet individu ruse avec la
Matière.</p>

<p>Les Chefs, c’est le Personnalisme ; l’Unanimité, c’est le Totalitarisme.</p>
<p>L’Unanimité, c’est la Démocratie ; les Chefs, c’est le Centralisme. »</p>
