      <h4><b>Réalisme Vrai ?</b></h4>
	  		
	  <p>« Je me vois lancer à la figure : “Tu prétends détenir la Vérité !” Je
réponds :</p>
<p>- Et alors ? Il faut bien que quelqu’un la détienne ! Je te signale
que je suis militant ; et qu’en me “mouillant” je prends le risque soit
d’être ridiculisé ! soit persécuté.</p>
<p>- Quelle est TA position vis-à-vis de la vérité. Tu sembles insinuer
en posséder une supérieure à toutes, celle qui dit : “La seule vérité
c’est que rien n’est certain”… Je récuse ce “doute brouillon” (pas du
tout “méthodique” de Descartes), qui ne t’engage guère ; sauf dans
l’apologie pépère du Préjugé barbare dominant des Païens-Parasites-
Bandits-Jaunes aux commandes !</p>
<p>- Par suite, il ne te reste que le choix suivant : soit te taire, soit
attaquer carrément mon message en vue de le démolir.</p>
<p style="text-align:center;">________</p>
<p>PRÉCISIONS</p>
<p>Ton interpellation penche du côté de la branche païenne de
l’Agnosticisme Hydre-Penseur qui, au nom de la Raison, dit
combattre le “dogmatisme” (“bon sens” !!).</p>
<p>L’autre branche païenne, celle du Cléricalisme Bondieusard, dit
combattre le “relativisme” au nom de la Foi (“révélation”).</p>
<p style="text-align:center;">________</p>

<p>Si aucun fil à la patte ne te retient dans la <a href="https://www.eglise-realiste.org/pdf/Peuple_Masse_Caste_ed.pdf" target="_blank">Caste au pouvoir</a>, et si tu
te trouves Désintéressé-Curieux-Exigeant, plonge-toi donc dans le
Réalisme Vrai.</p>
<p>- En partant même avec la résolution de nous anéantir s’il le
faut ;</p>
<p>- Au cas où cela tournerait de cette façon, comme nous ne
sommes que des “chercheurs de vérité” par-dessus tout (Imam Al
Ghazâly : – 1058-1111), saches que nous serons tes
premiers disciples.</p>
<p>- En attendant, je reste bien convaincu qu’en t’emparant du
Réalisme Vrai, tu feras le grand ménage dans tes méninges et te
délivreras avec la plus grande joie de l’amas d’opinions contraires à
ton propre intérêt dans lesquelles tu te trouves empêtré.</p>
<p style="text-align:center;">________</p>
<p>À ce moment, tu découvriras que nous ne sommes pas du tout
dogmatiques, ni à la sauce idéaliste, ni à la sauce empiriste, mais
précisément des Réalistes ; et que nous comprenons enfin pourquoi
il devait nécessairement y avoir en civilisation des hétérodoxes, soit
sceptiques/éclectiques, soit athées/exaltés (enthousiastes, mystiques
exagérés parce que anti-théologie). »</p>

