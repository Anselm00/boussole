			  <h4>Mystère</h4>
			  
			  <p><b><i>« Le MYSTÈRE</b></i></p>
<p>Diantre ! Nous voilà comme avec deux Dieux ! L’un totalement abscons, l’autre complètement rationnel ; l’un qui nous dépasse trop, l’autre qui nous ressemble trop ! (“nous” veut dire “civilisés spiritualistes”).</p>
<p>Va-t-il donc falloir choisir ? Y répondre est ce qu’on appelle <b>le Mystère</b>.</p>
<p>Le croyant orthodoxe part de l’<b>Évidence troublante</b> que Dieu est à la fois Révélé (“Dieu-Pour-Nous”) et Abscons (“Dieu-En-Lui-même”) ; que par sa Raison, il peut à la fois y accéder (Confiance) et ne rien en dire (Angoisse).</p>
<p>Telle est la Foi vivante, <b>complète</b> ; et complète parce que <b>contradictoire</b>.
<p>Le croyant se doit de croire que ces deux transcendances n’en font qu’une : car là est Le Mystère de Dieu, <b>le Mystère réel</b>, Mystère inhérent à la Religion.</p>
<p>Citation :</p>
<p><i>“Dans la Religion Pure, affranchie de toute Idolâtrie et de culte Servile, il n’y a qu’un seul Mystère : c’est Dieu lui-même. Dieu est incroyable !”</i> – Kant (1793).</p>
<p><b>● Remarque :</b></p>
<p>Dieu tout à la fois En-Lui-Même et Pour-Nous ne se donne à l’homme sous une forme Parfaite qu’au prix d’un labeur collectif acharné, scandé par une Succession <b>de Révélations</b> menant à l’avènement, progressif et par bonds, d’une Société des Âmes et d’une Église tout à fait conséquentes ; bref, jusqu’à l’épanouissement complet de la Civilisation. »</p>


<p>__________________________</p>

<p>Voir <a href="https://www.eglise-realiste.org/pdf/Religion_ed.pdf" target="_blank"> "Religion"</a>, p.14 - avril 2007 - Freddy Malot - Editions de l'Evidence - 2010</p>
			  
