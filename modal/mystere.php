<p><b>Double Mystère : Dieu et sa Grâce (§ 165 et 166)</b></p>

<p style="text-align:center;"><b>§ 165</b></p>
<p>Pas de religion sans mystère ; et le Credo (= je le crois) en est l’aveu solennel. Que
dit ce formulaire des Articles de la Foi, qu’on appelle aussi “Symbole” ? Il expose,
précisément, comment Foi et Raison coopèrent dans le Dogme. À ce propos,
disons ceci :</p>
<p>- Du fait de l’Impératif Moral en nous, notre Raison peut postuler sans problème l’existence de Dieu.</p>
<p>- N’empêche que le pourquoi et le comment de Dieu lui-même nous sont
complètement cachés en Ce-monde (􀂀 chap. V).</p>

<p style="text-align:center;"><b>§ 166</b></p>
<p>Le mystère réapparaît ouvertement dans le Culte, c’est-à-dire dans la question de
l’hommage que nous devons rendre à Dieu. Ici on expose comment le Mérite et la
<p>Grâce se combinent dans nos OEuvres. À ce propos, disons ceci :</p>
<p>- Du fait que les actes mêmes des meilleurs croyants ne sauraient produire un seul
Saint en Ce-monde, il nous faut supposer le Secours divin dans notre Conduite.</p>
<p>- N’empêche que ce que Dieu veut et peut faire pour notre Salut nous est
complètement caché (chap. VI).</p>

