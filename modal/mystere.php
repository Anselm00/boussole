<p>« La métaphysique d’Hésiode, c’est précisément ce qui fait l’objet de la Théogonie. Et la Théogonie d’Hésiode est reconnue comme la première oeuvre de littérature personnelle que nous ait léguée l’antiquité occidentale. Ici, il s’agit d’exposer la révélation de Zeus, le Maître Suprême, de Dieu en lui-même et son affirmation dans l’Éternité. Mais proclamer l’Éternel ne vaut que pour rendre compte de la formation du Cosmos, de la déclaration du Temps. C’est pourquoi le dogme de l’Esprit absolu qu’est Zeus, du grand mystère qu’est l’union en lui du Logos et du Destin, ne s’exprime véritablement qu’à travers son dessein particulier qu’incarne son Fils, Hercule. C’est ainsi que la doctrine de l’Hellénisme juvénile se trouve centrée sur le Héros Hercule, son apparition, ses tribulations consenties jusqu’au sacrifice, et son apothéose. Par suite, le Fils de Zeus nous est donné comme le Sauveur même de l’humanité, principe et règle de notre propre pérégrination ici-bas. Il est un Chant, traditionnellement associé à Hésiode, “Le Bouclier d’Hercule”, qui fait figure d’Évangile apocryphe de l’Hellénisme. On y fait le récit du grand combat d’Hercule pour terrasser Arès, esprit primitif de la guerre envisagée comme pure chasse à l’homme pour le butin. »</p>

