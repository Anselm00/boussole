			  <h4>Morale</h4>
			  
			  <p>« Insistons bien sur le caractère Social et Objectif de la mentalité religieuse.</p>
<p><b>1-</b> Toute l’expérience historique établit un fait indiscutable : la mentalité religieuse est le postulat même de la <b>SOCIÉTÉ CIVILISÉE</b> ; si bien que s’il y a le moindre doute à ce propos, c’est que nous ne sommes plus en Civilisation !
<p>Jamais l’humanité civilisée n’a considéré la conception spiritualiste de la Réalité comme une “idéologie”, ou une “option philosophique” parmi d’autres qui se proposerait aux citoyens. Ne pas être Croyant, en civilisation, ne pouvait être le fait que d’idolâtres, d’hérétiques, d’infidèles, d’athées, de païens et de suppôts de Satan, combattus ou tolérés provisoirement.
<p><b>2-</b> Dans la société civilisée, il était exclu que la Réalité puisse être conçue officiellement autrement que selon l’Esprit. Réciproquement, on ne pouvait pas imaginer que de <b>“VRAIS” HOMMES</b> aient pu être dans le passé, ou puissent être à l’avenir, autre chose que spiritualistes sans sombrer dans la “barbarie” socialement.
<p><b>3-</b> La société civilisée fut toujours animée par les valeurs suivantes, données comme des couples indissociables : <b>FOI-RAISON</b> en théorie et <b>MORALE-LIBERTÉ</b> dans la pratique. Ainsi, dès qu’il était porté atteinte à la Foi, la Raison se trouvait immédiatement malade ; et dès que la Morale se trouvait bafouée, la Liberté était immédiatement menacée. Il est malheureusement bien nécessaire que ces vérités premières soient aujourd’hui rappelées… »</p>


<p>C’est <b>Kant</b> qui alla le plus loin pour prendre en compte toute l’expérience religieuse antérieure, en professant l’Anti-apriorisme dogmatique. C’est la Morale qui fonde la Foi et y mène, et non l’inverse, dit-il. On ne peut pas rigoureusement “démontrer” a priori l’existence de Dieu ; celle-ci est seulement une Supposition (hypothèse) nécessaire et certaine, comme condition de toute notre “expérience” concernant le vrai et le bien. “Dieu est la limite a priori de l’Ici-bas, où l’expérience est le guide de nos connaissances ; l’Au-delà en est la limite a posteriori” (1770).</p>

