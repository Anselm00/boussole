			  <h4>Père Suprême</h4>
			  
			  <p>« Le Père Suprême des civilisés n’est pas tombé du ciel ; il est sorti du ventre de la Mère Fondamentale des primitifs.</p>
<p>De la même façon qu’il est préférable de réserver le mot “Dieu” pour désigner le Père Suprême, si on ne veut pas tout mélanger et perdre les pédales, il vaut mieux parler de Religion exclusivement pour la mentalité théorique des civilisés. Je choisis, pour parler de la croyance en la Mère Fondamentale, le mot Superstition pour bien trancher avec la Religion. Je ne donne à ce mot Superstition aucune valeur péjorative ; il ne fait que souligner le caractère spontané de la croyance primitive.</p>
<p>Il n’est pas du tout suffisant de distinguer la croyance à la Mère et celle au Père, la Superstition et la Religion. Ce qui importe le plus c’est de comprendre que ces deux mentalités théoriques sont directement inverses, comme le furent la Communauté Parentale primitive et la Cité Propriétaire civilisée, le Matriarcat primitif et le Maritalat civilisé.</p>
<p>Sous le règne de la Mère et sous celui du Père, l’humanité découvrit deux Réalités et deux Mondes, elle se donna deux Sociétés et deux Mentalités (ou modes de pensée). À tous points de vues, on a deux “réalités” qualitativement distinctes et se présentant comme deux contraires directs. Pour bien insister sur ce fait, on peut dire non seulement que l’humanité connut deux Mentalités contraires, mais que l’histoire donna deux Humanités distinctes et contraires. »</p>

