			  <h4>Chasse</h4>
			  
			  <p>« Tout comme nos ancêtres simiesques, le Pithécanthrope vivait essentiellement de
cueillette et de ramassage de fruits, racines, etc. Mais dès les premiers temps il commença
à se nourrir de chair animale, et la longue période qui nous occupe n’est que celle de
l’importance croissante prise par la <b>pêche et la chasse</b>.</p>
<p>L’usage, puis la production du <b>feu</b> permirent de faire cuire ou griller la chair animale
ainsi que les autres aliments, les rendant plus variés et plus digestibles. La maîtrise du feu
eut une importance plus grande encore : elle “a donné à l’homme pour la première fois
l’empire sur une force de la nature et, en cela, l’a séparé définitivement du règne animal”
(Engels). Le feu réchauffe l’homme, le protège des fauves ; il l’aide à la chasse et enfin à la
fabrication d’instruments.</p>
<p>Ces <b>outils</b> et armes sont peu à peu moins grossiers : aux simples éclats de silex<img class="img-in-text-left" src="img/silex.jpg"/>
succèdent des instruments taillés sous toutes leurs faces, à la massue se joint la lance à
pointe de pierre.</p>
				  <p>La chasse intensive et la production primitive de subsistances naturelles, la
conservation des vivres permettent pour la première fois un mode de vie sédentaire.
Les hommes érigent de vraies huttes et creusent des abris dans le sol ; puis comme les
outils de pierre perfectionnés améliorent le travail du bois, on voit apparaître des villages
de pêcheurs sur pilotis, des maisons de bois et des villages palissadés.</p>
<P>Comme la production, le ménage est communautaire : la “grande maison” abrite
jusqu’à des centaines de personnes ; c’est une base économique dont les femmes sont les
véritables organisatrices. En effet, leur activité productrice est une source de subsistances
plus sûre et plus régulière que la chasse. Ce sont elles qui gèrent l’entrepôt collectif et
règlent la vie domestique.</p>
<p>Le travail et toute la vie sociale étant communautaires, la propriété des moyens de
production est collective : le sol, les instruments, le bétail, les habitations. Seuls les outils
personnels, fabriqués par les individus et utilisés par eux, appartiennent aux particuliers.
Mais encore ils sont transmis au clan après la mort des “propriétaires”.</p>

<p>Plus la chasse s’affirme comme activité revenant à l’homme, plus se stabilise la
<b>division naturelle du travail</b>, selon le sexe et l’âge.</p>
<p>Simultanément, <b>l’organisation de la tribu</b> sur la base de la différence de sexes se
perfectionne, tandis que l’on connaît les <b>mariages collectifs</b> entre groupes définis par
des usages qui tendent à limiter peu à peu les relations matrimoniales entre consanguins :
d’abord entre parents et enfants collectifs, puis entre frères et soeurs d’une même mère et
enfin plus éloignés.</p>
<p>À mesure que les hommes augmentent leur expérience de la production collective et
organisent leurs liens sociaux selon des coutumes complexes, de véritables langues se
forment et le savoir des hommes s’enrichit, tels ces aborigènes australiens qui connaissent
plus de 300 espèces de plantes comestibles.</p>
<p>Par le travail et le langage, c’est en définitive l’homme tout entier – corps et esprit –
qui se forme, par sa propre activité vitale. L’alimentation carnée eut une importance
décisive sur <b>l’auto-développement</b> de l’homme. “L’homme n’est pas devenu l’homme
sans régime carné” (Engels). Ce qui est au plus haut point caractéristique de l’évolution
humaine, c’est que plus l’homme devenait “carnassier”, plus il perdait ses traits
simiesques.</p>
<img class="img-in-text-left" src="img/pithecanthrope.png"/><p>Le Pithécanthrope au front fuyant, aux maxillaires saillants, marchant le dos voûté en
pliant les genoux, laisse la place, il y a 100 000 ans, à l’homme de Neandertal dont le
cerveau a le volume de celui de l’homme moderne (1 300 cm³). Les hommes achevés, ou
Néanthropes, apparaissent enfin il y a moins de 50 000 ans ; ils sont dotés de la voûte
plantaire qui donne à l’homme moderne sa démarche souple, leur front haut recouvre des
lobes frontaux développés, leur menton est accusé ; ils ont les proportions des membres et
les asymétries typiques de l’homme actuel. Désormais l’espèce humaine est parfaitement
constituée dans ses traits essentiels, tout à fait apte à la production collective, à l’action
réfléchie et au langage articulé. “Dans un sens, il nous faut dire : le travail a créé l’homme
lui-même” (Engels). »</p>


