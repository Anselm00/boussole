<p>	« L’irruption de l’Homme sur <b>notre</b> Terre fut un événement dérisoire au regard de<img class="img-in-text-right-bigger" src="img/ssolaire.png"/><p>
l’Univers ; mais quel fait formidable que, dans le domaine de <b>notre</b> Soleil, il se
trouvât une planète <b>habitée</b> !</p>
<p>Et, avec cela, il y a le fait époustouflant que, la chose largement « artificielle »
dénommée humanité ne pouvait paraître – toute « sauvage » qu’elle fût – sans s’être
dotée d’une <b>valide « conception du monde »</b>, mettant donc en délibération le
Macrocosme qui n’en peut mais !<p>
<p>	En l’occurrence, c’est en professant une mentalité <b>MATÉRIALISTE</b> que
l’humanité première devait s’illustrer. Pourquoi ? La société d’alors fut spontanément
<b>Collectiviste</b>. Elle se voulut du même coup « prisme » réfractant en corps le spectre
de l’Univers se présentant comme le Grand <b>Organisme</b>. De ce fait, la Substance
dernière de la Réalité devait se concevoir <b>MÈRE</b>-matière. Ensuite, cette dernière
était censée parler par la bouche de <u>Voués-à-Vie</u> consacrés (« oint ») sous divers
noms : Poète, Devin, Prophète, Grand-Prêtre, Roi, Messie…</p>
<p>	Ce régime social évolua naturellement <b>« du Simple au Pur »</b> : horde (nomade),
Gens (ancêtre commun), tribu, confédération, Royauté : couple Suprême comptable
de la santé du Cosmos, couronnant un réseau de Castes (fonctions héréditaires
hiérarchisées).</p>
<p>	La « validité » du Matérialisme Traditionnel tenait au fait que la Matière est
<b>effectivement constitutive</b> de la Réalité duelle, en tant qu’une de ses faces.
<b>L’unilatéralité</b> du mythe fit cependant que la Première – et Universelle ! –
formation sociale finit par sombrer. C’était la punition du débordement de ses
possibilités de développement au proche-orient (Europe orientale et Levant).
La suite, on la connaît : sous l’élan donné par Tyrtée (743) et Hésiode (735), les
grecs vont engendrer la Civilisation <b>SPIRITUALISTE</b> occidentale » </p>

