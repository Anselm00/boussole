				<p> « Ceci dit, le Comm-Anar va Planifier la Pratique Directe. Comment ? On se trouve toujours dans une situation
				particulière du rapport Humanité-Nature. C’est de façon concrète que ces deux Libertés – qui peuvent être dites
				tout autant deux Nécessités – se conjuguent. On ne peut pas se proposer une combinaison Moyens-Besoins en l’air.
				À chaque étape il y a simplement une exigence désirable à satisfaire (on ne peut même pas dire que demain doit
				être ou sera “meilleur” qu’hier, car demain aura SON problème). Aujourd’hui, on se donne néanmoins un <b>Plan
				à exécuter</b>, qu’on dit possible par rapport au passé, et souhaitable par rapport à l’avenir. Ainsi, si je meurs dans l’exécution consciencieuse de ce plan, ma vie sera bien remplie, sans égard à un quelconque “demain”.
				En ce sens, le plan en cours est “a posteriori” parce qu’il tient compte des conditions, mais “indicatif” parce que
				c’est “au pied du mur qu’on voit le maçon”. Il est aussi “a priori” parce qu’il porte sur les “vrais besoins” d’hommes libres,
				mais “impératif” parce que les gens du Comm-Anar ne sont pas de ceux “qui disent et ne font pas”.
				Tout cela pour dire que notre planification ne se conçoit qu’“en nature” ; elle met en jeu de libres Capacités visant de nécessaires Utilités. »</p>
			
