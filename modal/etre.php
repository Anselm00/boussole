			  <h4>Être</h4>
			  
			  <p>« Le Monde Spiritualiste se nomme <a class="modal-trigger " href="#creation"><b>Création</b></a>. La Création n’a évidemment
aucun sens si elle n’est pas rapportée, d’une manière ou d’une autre, au Créateur.
Cela vaut sans réserve pour les Athées, qui nomment le Créateur “Matière” avec une
majuscule. Le Monde spiritualiste se donne comme celui de l’“être” Relatif,
Dépendant. Cela ne trouve aucune justification hors l’existence avouée du Grand
Être, de l’Être Absolu, de Dieu.</p>
<p>Ceci dit, ne perdons jamais de vue que l’homme civilisé conséquent reconnaît sans
difficulté que confesser simplement l’existence du Créateur n’est encore que voir Dieu
par le petit bout de la lorgnette. Derrière ce Dieu “pour-nous”, il y a encore le Dieu
“en-lui-même”, proprement Non-créateur, Mystère insondable.</p>
<p>Le Monde spiritualiste, étant celui de l’être Relatif, ou Participé, est celui du
rapport hégémonique esprit-matière. Au monde, nous avons l’esprit Manifeste, qui
est Non-Néant, et la matière Manifeste qui est Non-Être. Cela veut dire que pour le
spiritualiste, ce que nous nommons Matière n’est pas proprement “substantiel”, mais
est au contraire “privation d’être”, ne vaut qu’en considération du Monde, comme
“moyen” de l’Esprit créé. Le mot Matière ne doit donc pas nous égarer. Ce qui doit
retenir véritablement l’attention est le rapport hégémonique qui soutient le Monde,
interne à l’esprit Manifeste, <b>esprit Actif/Passif</b>. L’esprit Actif, c’est l’intelligence du
Monde donnée dans la perspective de la Race d’Adam ; l’esprit Passif, c’est
l’intelligence du Monde donnée dans la perspective du Système des Corps. »</p>
