			  <h4>Emanation Vitale</h4>
			  
			  <p>« La Puissance matérialiste était “sous” son Émanation, laquelle comprenait non
seulement l’Ici-bas, mais aussi l’en-deça de ce dernier : l’Abîme, le SCHÉOL juif par
exemple. C’est le royaume des “ombres”, des “larves”, cet en-deça CACHÉ où, après
s’être “absenté” de l’Ici-bas (qui serait mieux dit Ici-haut), on languit après la
Réincarnation “QUI VIENT” : HABBA’.</p>
<p>Que de mots, simplement pour mettre en ordre nos “concepts” ! Mais il faut savoir
si oui ou non nous voulons commencer à y voir clair dans notre passé, préalable
absolu pour marcher avec assurance vers un avenir quelconque ! »</p>
<p>__________________________</p>

				  

				  <p>Voir <a href="https://www.eglise-realiste.org/pdf/Theorie_Generale_ed.pdf" target="_blank"> "Théorie Générale"</a>, p.4 - Talib Freddy - janvier 2006 - Editions de l'Evidence - 2010</p>
			  
