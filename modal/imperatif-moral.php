			  <h4>Impératif Moral</h4>
			  
			  <p>La vraie Religion est celle que j’expose. Elle est Naturelle et Rationnelle parce
qu’elle prêche ceci : chaque homme sans exception, s’il obéit à l’Impératif Moral avec
ses conséquences prend le chemin d’un “vicaire de Dieu”. Mais lui-même est juge du
fait qu’il emprunte la voie du Bien et s’y maintient, et personne d’autre ; et c’est en
l’Autre-monde seulement que le grade de Lieutenant de Dieu lui sera décerné.</p> 

<p style="text-align:center;"><b>§ 6</b></p>		 
		 
<p>Si la Moralité veut
dire quelque chose, théoriquement, il
faut chez l’homme un Motif des motifs,
indispensable pour que sa conduite acquiert
une cohérence au regard de sa
Raison. Or, notre Raison ne nous fait
pas rencontrer d’autre Motif supérieur
que l’Impératif Catégorique, d’ailleurs
clair et précis, qui s’appelle le <b>DEVOIR</b>
du Bien. Par suite :</p>
<p>1- L’homme est Bon quand il observe
cette Loi Morale, et il est Mauvais
quand il la viole. Un point c’est tout. Du
coup, la théorie Sceptique de l’homme
“Ni bon Ni mauvais” ne vaut pas un
clou !</p>
<p>2- Du fait que la Loi du Devoir est
une loi absolue, qui ne souffre pas d’exception,
le méli-mélo Éclectique de
l’homme “TOUT À LA FOIS bon et mauvais”
ne vaut pas plus cher !</p>

<p style="text-align:center;"><b>§ 23</b></p>
<p>Si l’Impératif Moral pouvait tolérer
une <b>Volonté</b> mauvaise, il faudrait
tenir notre Liberté pour la créature d’un
Anti-Dieu ! Elle serait celle d’un démon,
et d’un démon marié par-dessus le marché
à un corps périssable, ce dont jamais
personne n’a envisagé la possibilité.</p>

<p style="text-align:center;"><b>§ 35</b></p>
<p>La Possibilité du Mal. Elle n’est ni
une tache du Corps (naturelle), ni un
vice de notre faculté Spéculative
(intellectuel). C’est un FAIT. Un fait intemporel
de la Liberté humaine, qui
s’impose à la Conscience en même
temps que l’Impératif du Bien. Après
coup, dans le temps de Ce-monde, notre
Entendement surnaturel, libre de tout
conditionnement de la part de notre
Sensibilité, tire parti de la Possibilité du
Mal pour se forger de toute pièce un
<b>Idéal</b> Négatif qui lui correspond. D’où
l’inversion du rapport Moral normal,
que le Méchant se représente comme
constamment possible (§ 40).</p>
<p>- Maintenant, les maux Relatifs en
Ce-monde. Ce monde n’est rien de plus
que le TÉMOIN de la Possibilité du Mal,
et en aucune manière sa source. C’est
notre Liberté originelle qui est la cause
absolue du Mal. Prétendre donner au
Mal toute autre origine rendrait notre
Liberté elle-même Relative, ce qui reviendrait
à mettre sur le même plan la
Volonté bonne et la Volonté mauvaise,
et donc à rendre notre Conduite complètement
irrationnelle et irresponsable.

<p style="text-align:center;"><b>§ 39</b></p>
<p>À propos de quoi Adam a-t-il trébuché
? On nous apprend qu’Adam
avait été investi d’une Loi Morale, lui
permettant de motiver convenablement
sa Liberté. Il se trouve que le Devoir
qu’Adam devait s’imposer à lui-même
se réduisait à un Interdit très précis,
mais catégorique. L’Écriture veut nous
montrer ici, comment l’homme en Ce-monde
peut trahir bel et bien son Impératif
Moral, en le subordonnant – plus
ou moins subrepticement – à d’autres
motifs, pourtant neutres en eux-mêmes
(et bien sûr les conséquences catastrophiques
que cette manipulation entraîne).
Comment Adam s’y prit-il ? Pour
commencer, il se mit à douter du caractère
absolu de l’Interdit ; puis, il en tordit
le sens d’une manière qui l’arrangeait.
À la sortie, cela donna quoi ? la
consommation du Péché !</p>
	
<p style="text-align:center;"><b>§ 42</b></p>
<p>En matière de Morale, <b>l’Entendement</b>
qui veut des Démonstrations ne
peut nous être d’aucun secours. Exemple
: la Tradition qui déclarait :
“l’homme est méchant par nature”, est
incapable de prouver qu’il puisse devenir
bon. Inversement, les Lumières qui
proclamèrent : “l’homme est bon par
nature”, ne peuvent prouver comment il
a pu devenir mauvais.</p>
<p>C’est <b>la Faculté Volontaire</b> de notre
raison qui nous sort de l’ornière.
Comme elle nous ordonne sans ambiguïté
de travailler à nous rendre bons, à
moins que tout ne soit que ténèbres et
chaos dans la Civilisation, nous sommes
forcés d’admettre la possibilité très réelle
de “remonter du Mal au Bien”.</p>

<p style="text-align:center;"><b>§ 43</b></p>
<p>Remonter du Mal au Bien ne veut pas
dire chercher à réinfuser en la Conscience
l’Impératif Moral dont elle aurait
été dépossédée. Impossible, puisque
l’Impératif est le contenu le plus essentiel
de la Conscience. D’ailleurs, en imaginant
qu’elle ait pu le perdre, jamais
elle ne pourrait le récupérer !</p>
<p>Ce dont il s’agit est donc seulement
de <b>purifier</b> cette disposition, de la dégager
de toute promiscuité avec l’Entendement
et la Sensibilité, et de permettre
à l’Impératif Moral de remplir enfin le
rôle qui lui revient de gouverner toute
notre conduite.</p>

<p style="text-align:center;"><b>§ 50</b></p>
<p>“Saint”, c’est ce que serait l’homme si
tous ses actes étaient le fruit de maximes
rapportées à la Loi du Devoir (à
l’Impératif Catégorique de bien faire).</p>


<p style="text-align:center;"><b>§ 73</b></p>
<p>Or, comme Satan était une créature
spirituelle – sans corps – il ne pouvait
jouir directement des fruits de la Nature
; il lui fallait donc se rendre maître
de l’<b>âme</b> de l’homme, usufruitier de la
Terre, et amener ainsi Adam à “forfaire
son fief”. Et c’est ce qui arriva : Satan
devint “le Prince de Ce-monde” (comme
dit Jean en 12 : 31). Et comme Adam fut
le père de l’espèce humaine, en chacun
de ses descendants le penchant au Mal
prima sur l’Impératif du Bien.</p>

<p style="text-align:center;"><b>§ 144</b></p>		  
<p>« <b>Devoir réglé</b> parfaitement.
D’abord, que veut dire “Aime Dieu par-dessus
tout, et Autrui comme toi-même”
? C’est : aime celui qui est le
Bien même, et qui t’en fait un Impératif
; et contribue au bonheur d’autrui
sans considération d’intérêt personnel.
Ne rabaissons pas cela à deux maximes
ordinaires de la vertu ; c’est la voie de
Sainteté qui est imposée, et toutes les
maximes ne servent que pour y marcher. »</p>

<p style="text-align:center;"><b>§ 163</b></p>
<p>Apportons donc les vraies Lumières
dans la Religion elle-même : tout ce qui
s’y trouve de temporel et d’obligatoire
ne peut valoir que comme moyen
d’éveiller et de développer la Foi Morale.</p>

<p style="text-align:center;"><b>§ 164</b></p>
<p>Puisque la Foi Morale est essentiellement
Libre, et qu’elle est conforme à
l’Impératif de la Raison, l’Église Parfaite
que nous voulons ne peut plus admettre
que seuls sont <b>“Appelés à la Lumière”</b>
divine : quelques Notables laïques
irréprochables, quelques Clercs
privilégiés, quelques Théologiens certifiés,
et quelques Simples fidèles exceptionnellement
inspirés.</p>
<p>Tout au contraire. Le germe de la
vraie religion est bel et bien déposé en
tous, et peut très bien se développer en
tous. Il dépend de chacun de se Convertir,
puis de s’engager sur le chemin de la
Sainteté. L’Idéal de Sainteté est tout
aussi <b>intemporel</b> que la faculté Morale
de la Raison ; et il est immédiatement
<b>évident</b> comme la Conscience d’être
doté de Raison.</p>
<p>Il subsiste bien une difficulté dans
notre affaire. Nous y sommes préparés.
C’est le scandale que doit nécessairement
provoquer l’idée que, désormais,
la Cause de Dieu se trouve remise sans
réserve, entre les mains de la multitude
innombrable des Ignorants. Voilà, il est
vrai, ce qui ne peut que paraître
“Insensé aux yeux du monde”…</p>

<p style="text-align:center;"><b>§ 165</b></p>

<p>Pas de religion sans mystère ; et <b>le
Credo</b> (= je le crois) en est l’aveu solennel.
Que dit ce formulaire des Articles
de la Foi, qu’on appelle aussi
“Symbole” ? Il expose, précisément,
comment <b>Foi et Raison</b> coopèrent
dans le <b>Dogme</b>. À ce propos, disons ceci
:</p>
<p>- Du fait de l’Impératif Moral en
nous, notre Raison peut postuler sans
problème l’existence de Dieu.</p>
<p>- N’empêche que le pourquoi et le
comment de Dieu lui-même nous sont
complètement cachés en Ce-monde
(􀂀 chap. V).</p>



