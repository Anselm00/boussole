<p>«Salariat :</p>
<p>Chez les Modernes, la propriété privée se déploie dans toutes ses dimensions, rien
ne valant plus, directement cette fois, que selon l’Argent.</p>
<p>• <b>Le capitaliste</b>, libre de toute charge politique immédiate, et de toute obligation
personnelle à l’égard de ses employés, peut se consacrer exclusivement à accumuler
des moyens de production. De plus, désormais les moyens de production accaparés
n’ont plus le caractère d’un monopole naturel en usufruit, mais ne sont plus reconnus
comme tels qu’en tant que conditions du travail exclusivement issues de travail
exproprié cristallisé ; car tel est ce qui fait le “capital”. L’on a donc ici une “double
négation” de la seigneurie gothique et du patronage antique : à travers le Capital se
trouve en quelque sorte “restaurée” la prédominance antique de l’homme sur la nature dans le travail, en même temps que se trouve “conservée” la différenciation
gothique entre le ménage et l’entreprise.</p>
<p>• <b>Le salarié</b>, de son côté, accède à la condition suprême de responsabilité
compatible avec l’exploitation, ne devant qu’à sa seule initiative l’espoir de procurer
le nécessaire à son ménage. Il est vrai qu’en contre-partie, son sort est livré à
l’insécurité que le marché fait peser sur l’entreprise, et que son travail fait désormais
l’objet d’une exploitation totalement rationnelle, c’est-à-dire radicale. De plus, sur le
plan subjectif, jamais les relations d’exploitation n’ont été aussi totalement masquées
aux yeux des intéressés, employeurs et employés. En effet, le règne de la “liberté du
travail et de l’industrie” et la règle des lois “équitables” du marché brouillent
complètement les cartes ; mais quel pas incomparable accompli, maintenant que
nous avons des exploités délivrés de tout lien patriarcal, affranchis de toute spécialité
professionnelle bornée, habitant les villes et lisant les gazettes qui annoncent les
nouvelles du monde, c’est-à-dire du vaste marché général auquel sont suspendus
leurs intérêts vitaux…</p>
<p>Relativement au salariat moderne, servage et esclavage apparaissent ensemble
comme un seul et même mode de production grossièrement Rural. De plus, au-delà
du salariat, il n’est aucune forme de perfectionnement ultérieur concevable de
l’exploitation de l’homme par l’homme. Au contraire, le parachèvement acquis de la
propriété privée, que représente la loi suprême du Marché, établit une socialisation
objective du travail matériel qui ne laisse d’autre issue qu’une socialisation délibérée
entraînant nécessairement avec elle la dissolution irréversible de l’exploitation de
l’homme par l’homme.</p>

