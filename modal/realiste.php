			  <p style="text-align:center;"><b> LA PENSEE MENE L'HUMANITE</b></p>	  
			  <p>« <b><u>L’Eglise Réaliste Mondiale</u></b>. Je la suppose valable. Si c’est le cas, sa vocation est <b>Mondiale</b>.
Le nom d’<b>Église</b>, faute de mieux, convient très bien. D’ailleurs l’épithète Réaliste lève toute
ambiguïté. Le mot ecclesia (έκκλησια) – fidèles CONVOQUÉS – est un hommage bien mérité rendu
au passage à la feue Civilisation Religieuse.</p>

<p>Mais l’essentiel est de proclamer haut et fort que, notre espèce étant Travailleuse, “<b>la Pensée
mène l’Humanité</b>”. Or, les mots : Association, Parti, etc., ne soulignent pas du tout ce fait.</p>

<p>- <b><u>Le Préjugé Mental</u></b> de la Préhistoire sociale <b>Vivante</b>. Qui dit Pensée, dit un <b>Présupposé</b>
inévitable qui préside à ladite pensée et, par suite, à toute la vie sociale. Ainsi eut-on, durant la
Préhistoire Vivante : le Mythe de la <b>Matière</b> Absolue chez les Parentaux, et le Dogme de l’<b>Esprit</b>
Absolu chez les Bourgeois. Donc 2 présupposés contraires, exclusifs l’un de l’autre.</p>

<p>Mais dans les 2 cas, le Présupposé était un <b>PRÉJUGÉ</b>. J’appelle Préjugé un <b>Présupposé
Insoupçonné et Partial</b>. Il est insoupçonné subjectivement et partial objectivement. Autrement
dit : les intéressés sont les dupes de leur présupposé ; et il ne rend compte que d’une des deux
faces de la Réalité. Exemple d’<b>absence de lucidité</b> : dans la Religion civilisée, le “NÉANT” donné
comme le revers de l’Esprit Absolu, n’est que le faux-nez de la Matière Absolue. C’est la même chose
à l’envers dans l’Observance Traditionnelle. Quant à l’<b>Unilatéralisme</b> du Préjugé, la Préhistoire
le revendique orgueilleusement. Mais il faudra en payer le prix : incompréhension mutuelle totale
des 2 versions rivales ; et les deux vont à la faillite quand leur perfection finale rend criante leur
étroitesse intrinsèque. »</p>

