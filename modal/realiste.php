			  <h4>Réaliste</h4>
			  
			  <p>« À propos de la 3ème espèce, du Réalisme Théorique et du Communisme Historique en Pratique, on se pose de faux problèmes.</p>
À 90 %, ce sont de tels faux problèmes qu’on met sur le tapis. Cela vient non pas de difficultés objectives, mais de notre cervelle qui rechute sans cesse dans le dogmatisme (ou le Mythisme).</p>
On entend dire :</p>
<p>• <b>“La 3ème espèce est une abstraction”</b>, une construction de l’esprit, dont on ne possède aucune image concrète ! Ce sont précisément des “abstracteurs” qui présentent cette objection : ils réclament une description d’un “état” Communiste, n’abordent la 3ème espèce que comme une “Utopie” civilisée.</p>
<p>• <b>“Nous sommes tragiquement préhistoriques à 100 % à l’heure actuelle”</b> ; quelle misère d’être encore de faux Communistes ! C’est la même chose que l’objection précédente, mais version Intuition et non plus Entendement, Idéalisme et non plus Empirisme.</p>
<p>Bref, si on n’est pas Réaliste, on ne peut que divaguer en se permettant de parler du Réalisme ! On complique une chose simple. </p>
<p style="text-align:center;"><b>•••</b></p>
<p>• Il y a eu l’Homme primitif et l’Homme civilisé, <b>DEUX</b> espèces directement contraires de la race, et qui ne furent fondées historiquement que parce qu’elles étaient <b>DÉJÀ</b> Réaliste, Communistes, mais de manière Unilatérale. Qu’y a-t-il de plus simple que de prendre conscience que cela se dévoile <b>APRÈS-COUP</b>, rétrospectivement ? Et donc : qu’une <b>Troisième</b> espèce, <b>Complète</b> et <b>Maintenant</b>, est fondée historiquement ?</p>
<p>• Les Chrétiens appelèrent à “<b>dépouiller le vieil homme</b>” ; n’est-ce pas demander un NOUVEL homme individuel ? Nous demandons, un nouvel homme historique et collectif. C’est assez simple !</p>
<p>Il y a des exemples à la pelle, de ce type : un Bébé primitif est fait Adulte civilisé. Etc. Etc.</p>
<p style="text-align:center;"><b>•••</b></p>
<p>Les membres de l’Église, ou en tout cas l’Église prise en bloc, ne peuvent qu’être <b>dès à présent, concrètement</b>, Réalistes et Communistes (au sens historique).</p>
<p><b>1-</b> Nous le sommes, certes, de façon “<b>débile</b>” ; mais nous le “sommes” ! C’est la simple conscience aiguë, PRATIQUE (engageant la vie, la responsabilité) que notre époque dit : “3ème espèce ou Mort de l’humanité”. Et donc que nous vaincrons la mort.</p>
<p style="text-align:center;"><b><i>“Le dernier ennemi qui sera anéanti, c’est la mort”</i></b></p>
<p style="text-align:right;">I Corinthiens 15 : 26</p>
<p style="text-align:center;"><b><i>“La mort a été engloutie dans la victoire”.</i></b></p>
<p style="text-align:right;">I Corinthiens 15 : 54</p>
<p><b>2-</b> Nous sommes l’Église de l’Humanité ; la mesure de notre “débilité” est contrôlée <b>mutuellement</b>, et nous faisons dépérir cette débilité pas à pas, dans le combat et en nous épurant.</p>
<p><b>3-</b> Notre Église est propulsée par le Devoir, le sens à vif que la Masse/peuple nous “veut” impérativement. »</p>
