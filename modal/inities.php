			  <h4>Initiés</h4>
			  
			  <p><b>« Unanimité Tolérante</b></p>
Socialement, comment s’exerce le mode de pensée traditionnel ? C’est le dernier
point à examiner. À ce propos, l’on observe une unanimité tolérante.

<p><b>Unanimité</b></p>

			  <p> L’unanimité sociale à l’égard de la tradition est le côté “totalitaire” de celle-ci. Mais
cela se limite au fait que le mode de pensée traditionnel présuppose l’absence de
pensée “réflexive” au niveau des individus. Chacun se contente de penser tout court,
sans aucunement songer à “penser sur la pensée”. Autrement dit, il n’est encore
aucunement question de pensée “abstraite”, à quelque titre que ce soit. Cependant, la
pensée réflexive n’est pas pour cela absente ; elle s’exerce seulement au travers du
cerveau collectif de la communauté, et prend la forme des Mythes communs,
préalable indiscuté de la pensée de chacun.</p>
<p>Ainsi donc, point de chefs d’École, de Systèmes rivaux ni, par suite, d’“hérésies”
possibles. Il est normal que le “Livre des morts Égyptiens” n’ait pas d’auteur désigné.
Si, à la fin, à tel Code ou Loi est attaché un nom – Moïse ou Hammourabi –, ces
derniers ne figurent en fait que comme les oracles, au nom de la communauté, de la
“puissance” raciale ou cosmique.</p>
<p>De toute façon, la vie spirituelle se renferme dans la relation des “sages” aux
“initiés” formant une même “société secrète” solidaire, qui double la société
exotérique. Les Sages ne sont jamais que le corps des sacrificateurs, prédestinés par
l’âge, le sexe ou le sang. Ce sont les Anciens, la “race” sainte des Lévites, ou la
corporation sacerdotale ayant à sa tête le Roi-prêtre. Ils sont les interprètes forcés des
“grands secrets” et subissent la “servitude du rituel”. Il est hors de question de 
trouver alors des relations civilisées de Maître à Disciple, n’en déplaise aux “Adeptes”
de l’occultisme actuel. Exemples :</p>
<p>En Mésopotamie, le “roi”, obligé d’interpréter les “signes” des astres, ses maîtres,
est soumis à des observances rigoureuses et les risques qu’il court sont immenses.
Face à tel ou tel phénomène, il peut lui être imposé : un jeûne d’un mois, si la lune est
masquée et empêche de proclamer le nouveau mois ; en d’autres occasions,
l’obligation de se faire raser tout le corps, de s’isoler sept jours dans une hutte de
roseaux, de se soumettre à des purifications constantes, de subir des interdits
alimentaires complexes et rigoureux, de ne pas mettre de vêtements propres pour un
temps, de ne pas monter sur son char ni de parler en souverain, d’agir en “mendiant”,
etc.</p>
<p>Le “roi” égyptien, désigné par les oracles depuis sa naissance, se trouve de même
prisonnier de sa fonction “théocratique”.</p>
<p>Toutes ces contraintes découlent de ce que “la royauté descendit du Ciel”
(Mésopotamie).</p>

<p><b>Tolérance</b></p>

<p>La tolérance est le côté “démocratique” de la Tradition.
Si la sagesse, par sa nature même, comporte un caractère “secret”, inversement, la
foule des choses que chacun doit connaître, suivant son âge et sa position, est
transmise à chacun, en temps voulu, par l’initiation qui lui est due. Ainsi, chacun
maîtrise identiquement, “comme les pères de ses pères”, toutes prescriptions et
transgressions nécessaires. »</p>
