			  <h4>Eternité</h4>
			  
			  <p>« TOUT existe pourtant, et je suis bien tenté<br>
De croire que tout est de toute éternité.<br>
Si la Création est un acte impossible,<br>
Tout dit à la raison que l’Air irrésistible<br>
N’a ni ne peut avoir aucun commencement ;<br>
Qu’il est parce qu’il est, et que c’est l’Elément<br>
Qui, par son grand pouvoir et par son énergie,<br>
A produit tous les Corps et leur donne la vie,<br>
Ne les laissant jouir de ce don précieux,<br>
Qu’autant qu’il s’assimile et s’organise en eux ;<br>
Qu’autant qu’il fait durer les ressorts qu’il fabrique,<br>
Par l’insensible effet de sa puissance unique :<br>
Ressorts qu’il entretien, qu’il use et qu’il détruit<br>
Pour reproduire encor comme il avoit produit. . »</p>
