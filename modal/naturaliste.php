			  <h4>Naturaliste</h4>
			  
		 <p>« L’histoire d’Ève trompée prouve justement que la Vulve 
			  est déterminante dans le Naturalisme parental. 
			  Seulement, en passant du stade “sauvage” au stade “barbare” 
			  (cf. Morgan), le Matriarcat simple du départ se <b>perfectionne</b> : 
			  les éléments nomades permettent au Phallus de devenir <b>principal</b> 
			  en gardant à la Vulve son rôle <b>fondamental</b>. Ceci dit, la 
			  transgression alimentaire mythique explique pourquoi 
			  la Nature historique réelle est gâtée, salie, 
			  malade. On dit qu’elle est Tortue (pas tordue), 
			  et qu’il va falloir s’user à la Redresser 
			  (les juifs traduisent par “réparation” ce redressement : TIQQOUN), 
			  à la nettoyer, la guérir. Cela explique du même coup pourquoi 
			  les juifs sont environnés de Parentés Futiles, et pourquoi 
			  en leur sein même il y aura des juifs <b>Droits</b> 
			  (ils traduisent par Justes : TSADDIQYM), et 
			  des juifs tortus. Il y a aussi des Devins 
			  (ils disent Prophète : NABY’), “possédés” 
			  communautaires, en temps de crise grave. En temps normal, 
			  il suffit d’avoir des Sacrificateurs (ils disent Prêtre : COHEN), 
			  et les juifs peuvent même avoir des Prosélytes (GUÉR). 
			  De toute façon, tout le Redressement de la Nature consiste 
			  essentiellement en restitutions solennelles d’une part de 
			  ses Dons dans les Sacrifices (y compris dévouer les 
			  premiers-nés de la parenté) et en <b>Diététique</b> 
			  réglée. Que peut-on attendre de ce ritualisme ? D’abord de 
			  limiter la Métempsychose ; ensuite de hâter peut-être 
			  “la Nature qui vient” (ils disent l’Au-delà), 
			  le rétablissement du Parc de Délices (GAN Eden) 
			  étendu à la terre, et où les Parentés Futiles seront 
			  dociles sous la royauté de l’ethnie sacrifiante (ils disent 
			  Peuple-Prêtre). (La même racine : MLQ s’applique à <b>Roi</b> 
			  (vicaire de la Nature), à MOLOCH (Génie-Vampire des Phéniciens), 
			  aux Génies (ils disent Anges), aux Devins, aux Sacrificateurs, … 
			  et à tout Israël (parenté de Jacob).) »</p>



