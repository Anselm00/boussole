				  <p>« Le phénomène de “blocage” de la pensée civilisée se manifesta clairement avec
l’“éclectisme” de Victor Cousin et Jouffroy, à la veille de 1848. Nous savons que le</b><img class="img-in-text-left" src="img/cousin.png"/>
mode de pensée philosophique fut la forme de travail mental que se donna l’humanité
civilisée, et qu’elle forgea laborieusement tout au long de cette étape prolongée de
l’histoire sociale. Or, avec l’Éclectisme on prétendit présenter le philosophisme
comme un pur instinct des hommes, relevant de l’histoire naturelle de l’espèce
humaine. Dès lors, on proclamait du haut des chaires officielles que le véritable
aliment de l’esprit était le préjugé philosophique lui-même, que la grande tâche de la
philosophie était de se mordre la queue. Cousin s’imposa comme le grand maître de<img class="img-in-text-right" src="img/jouffroy.png"/>
cette gymnastique philosophique. Le “truc” consistait à proposer une visite guidée de
la philosophie à travers les siècles, dans le but de démontrer que les philosophes n’avaient 
cessé de faire du sur-place ; que, pour ce faire, ils avaient joué à s’affronter
en Écoles rivales constituées ad hoc ; et que ces mêmes équipes de sportifs de la philo
se retrouvaient périodiquement après avoir changé seulement la couleur de leurs
maillots. Ainsi, au nom de l’histoire, la Philosophie trouvait un dernier prétexte à
s’agiter. En réalité, et c’est ce qui se passe dans tous les musées, l’histoire était
devenue de la géographie. L’Éclectisme n’était que la philosophie se complaisant dans
son épuisement.</p>
<img class="img-in-text-left" src="img/lamennais.png"/>Lamennais et Pierre Leroux s’insurgèrent à juste titre contre l’entreprise
éclectique de construction d’un mausolée de la philosophie, entreprise qu’ils
stigmatisèrent sous le nom d’“Indifférentisme”. En effet, le somptueux monument
funéraire que l’éclectisme érigeait constituait la pire insulte pour toute la lignée des
philosophes véritables, qui s’étaient placés en première ligne dans la lutte pour
fonder, édifier et couronner la pensée civilisée vivante. Et il était heureux qu’une<img class="img-in-text-right" src="img/leroux.jpg"/>
protestation véhémente s’élevât contre la crise radicale du mode de pensée
philosophique que manifestait la philosophie frelatée de l’éclectisme.</p>
<p>Durant 25 siècles, les philosophes n’avaient cessé de se déchirer à coups de
dogmes, y compris au moyen des dogmes les plus sectaires, tel celui de l’Esprit pur,
ou celui de la Matière pure, ou même celui du Doute absolu. Cela est incontestable.
Néanmoins, cela était le symptôme qu’il y avait une oeuvre de l’esprit à réaliser, une
oeuvre humaine, l’oeuvre précisément qui consistait à parachever le mode de pensée
philosophique. Et cela indiquait que dans cette affaire, on était constamment
sensible, sinon conscient, qu’il y avait un enjeu social décisif. Or, voilà que, l’oeuvre
accomplie, ses bénéficiaires, nos contemporains, en fils dénaturés de la pensée
civilisée, proclament que raison/vérité sont de simples facultés de la bête
humaine ! Voilà qu’on se met à présenter insidieusement les philosophes, qui furent
les nourrices ayant allaité notre esprit, comme des monstres à deux têtes : d’un côté
des songe-creux mortellement ennuyeux, de l’autre côté des doctrinaires fanatiques.
Sans vergogne, l’on répand sur le compte des champions de la pensée civilisée une
double rumeur : d’une part, ce n’étaient que des personnages passant leur temps à
ergoter sur “le sexe des anges”, des “abstracteurs de quintessence” étrangers à toute
réalité, heurtant le “bons sens” des masses ; d’autre part, c’étaient des sectaires
furieux occupés à inoculer “l’intolérance” aux âmes populaires, lesquelles sont
présentées alors comme de pauvres proies “crédules” ! »</p>

