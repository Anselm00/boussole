			  <h4>Etat</h4>
			  
			  <p><b>« Qu’était donc la souveraineté au sens proprement civilisé du terme ?</b></p>
<p>• <b>Envisagée d’une manière générale</b>, la notion de Souveraineté désigne la
forme d’organisation sociale qui a succédé au communisme primitif. Durant les
quelques 25 siècles qui embrassent l’ère civilisée, cette forme d’organisation sociale
s’est épanouie par “bonds” : successivement à travers, d’abord la Cité antique, ensuite
la Principauté gothique et enfin la Nation moderne. Alors parachevée, la Souveraineté
civilisée entra en crise puis en décadence depuis le milieu du siècle dernier (1850).
<p>• <b>Envisagée de manière plus précise</b>, la souveraineté civilisée apparaît
comme l’unité antagonique de deux pôles absolument indissociables :</p>
<p>- Quant au fond, ce fut le perfectionnement successif de l’organisation des Familles
dans l’État ;</p>
<p>- Quant à la forme, ce fut l’épanouissement successif de l’Humanisme associé à
l’Individualisme.</p>
			  <p><b>La Souveraineté civilisée, au sens complet du terme, ne se conçoit que
comme l’unité organique des deux couples antagoniques que forment
Familles-État et Humanité-Individus, et les relations mutuelles
qu’entretiennent ces deux couples. »</b></p>

<p>•••</p>
<p>Le contenu de la souveraineté civilisée – la relation Famille-État – constitue son
dynamisme interne. Cette facette de la Souveraineté est la chose la plus connue.
Cependant, la pensée décadente Contemporaine s’interdit de voir dans le
développement du couple Familles-État la simple expression sociale du devenir de la
Propriété privée. À plus forte raison, la pensée contemporaine s’interdit-elle de
considérer que la Propriété privée est inséparable de l’Exploitation de l’homme par La forme que prend nécessairement la souveraineté civilisée – la relation
Humanité-Individus – constitue sa force expansive. La pensée décadente
contemporaine est totalement aveugle à ce second aspect de la souveraineté complète
et à son unité intime avec le précédent. Concernant la relation Humanité-Individus,
chaque homme se trouve posé à la fois comme Citoyen du monde et Exilé,
Cosmopolite et Apatride. Cet aspect essentiel de la Souveraineté est l’expression
même, sur le plan de l’organisation sociale, du couple Universalisme-Personnalisme
que développe la Métaphysique, et du couple correspondant en Morale : AltruismeÉgoïsme.
Pris séparément, le couple de la souveraineté formelle (Humanité-Individus)
forme une unité antagonique. En effet, auprès de la “réalité” de l’Individu singulier,
hic et nunc, la généralité du Genre Humain apparaît comme un simple “nom”, une
abstraction. À l’inverse, auprès de la permanence du Genre Humain, qui apporte
véritablement tout à chaque Individu contingent, qui naît pour bientôt périr, le petit
Ego semble tout à fait inessentiel, et seule l’Humanité dans son ensemble apparaît
solide, le véritable Anthropos pouvant prétendre défier la Nature.
En dépit de leur antagonisme, Humanité et Individus ne sont évidemment qu’une
seule et même chose : l’humanité n’est assurément rien d’autre que l’ensemble des
individus et réciproquement. Cependant, dans la manière civilisée d’envisager les
choses, cette évidence a le plus grand mal à s’imposer, toutes les relations étant
pétries d’antagonisme et se présentant la tête en bas et les pieds en l’air. Tout
d’abord, l’on considère que la Propriété privée, et par suite la Souveraineté – l’argent
et la police – sont des choses “naturelles” et non point du tout historiques. Ensuite,
cette réserve faite, on envisage le contenu de la souveraineté – la relation
Familles/État – de façon rigoureusement “matérialiste”, en donnant à l’aspect
Familles le rôle principal, l’État occupant une position subordonnée dans la relation.

Mais c’est exactement l’inverse qui se passe concernant la forme de la souveraineté
– la relation Humanité-Individus : ici, c’est le général qui prime sur le particulier et il
est présupposé que l’Humanité compte plus, est plus “noble” que les Individus, nés ou
à naître, qui la composent. Dans ce domaine formel, le spiritualisme prend son
éclatante revanche officielle. En réalité, la fiction du caractère “naturel” de la famille
privée contenait cela au départ, mais cela n’était pas apparent dans la relation
Familles-État acceptée sans critique... Finalement, cet écheveau d’antagonismes que
traînait la conception civilisée ne pouvait se démêler, trouver une unité paisible, que
dans la Foi, dans l’affirmation du caractère Personnel de Dieu et de l’immortalité de
l’âme Individuelle des Hommes. Cette résolution des antagonismes fut elle-même
très laborieuse ; en font foi les hésitations Rurales à ce sujet, soit à propos de “l’âme
du monde” chez les platoniciens, soit à propos de l’âme commune au genre humain
chez Ibn Rushd (unité numérique de l’intelligence dans l’espèce humaine).

Notons que tout ceci nous permet de réaffirmer un point essentiel : la
religion au sens précis du terme est spécifique à l’humanité civilisée, et
elle a pour racine directe l’organisation sociale civilisée, alors que la
relation plus large liant Nature et Humanité (création du monde, ordre
naturel, etc.) n’est en réalité pour elle qu’une préoccupation seconde,
indirecte, dérivée.
