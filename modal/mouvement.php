			  <h4>Mouvement</h4>
			  
			  <p>« MALGRÉ tous les ennuis dont notre ame s’irrite,<br><img class="img-in-text-right-bigger" src="img/ether.jpg"/>
L’Homme est le Souverain du Monde qu’il habite,<br>
Il y commande en Maître et tout subit sa loi.<br>
Mais qu’est-il ? d’où vient-il ? Que trouve-t-il en Soi<br>
Qui puisse l’éclairer sur son sort, sur son Etre,<br>
Et lui montre l’agent qu’il aspire à connoître ?<br>
Il est sans contredit l’acte le plus parfait,<br>
Le plus bel œuvre enfin que la Nature ait fait ;<br>
Mais, malgré son orgueil, c’est un être ordinaire.<br>
Qu’apperçoit-on en lui ? Mouvement et Matière ;<br>
Semblable, sur ce point, à tous les végétaux,<br>
Naissant, vivant, mourant comme les animaux.<br>
La Substance de l’Homme est plus harmonieuse ;<br>
Ses Sens sont plus exquis ; sa forme est plus heureuse ;<br>
D’où cela provient-il, Mortels trop abusés ?<br>
De ce que les Humains sont mieux organisés.<br>
Et combien, même entr’eux, voit-on de différence.<br>
L’un brille par l’Esprit, les talens, l’éloquence ;<br>
L’autre est un imbécille, un sot, un étourdi,<br>
Trop vil pour mériter qu’on s’occupe de lui.<br>
A peine celui-ci paroît à la lumière,<br>
Que la mort pour toujours vient fermer sa paupière :<br>
Et tel dont le destin est d’exister long-tems<br>
Croit, décroit, voit périr sa raison et ses Sens.<br>
Le Ton de mouvement fait les uns et les autres ;<br>
Tout être a sa fillière, et nous avons les nôtres,<br>
Et suivant qu’elles sont ou bien ou mal en nous,<br>
Nous sommes des méchans, des Sages ou des fous,<br>
Nous aimons les vertus ou bien les injustices.<br>
Mais qui fait ces couloirs, qui fait ces interstices,<br>
Ce Ton de mouvement qui réglant nos destins<br>
Nous accable de maux ou nous comble de biens ?<br>
N’accusons pas un Dieu d’un si cruel caprice ;<br>
S’il nous avoit créés, nous n’aurions aucun vice ;<br>
Il veilleroit sur nous, il seroit notre appui,<br>
Ses trop heureux enfans seroient dignes de lui.<br>
Il faut donc que ce soit une aveugle Puissance,<br>
Qui forme et qui détruit notre frêle existence ;<br>
Qui défait et refait tous les Etres divers,<br>
Qui remplissent la terre et peuplent l’Univers.<br>
On peut, en admirant sa riche architecture,<br>
Chercher le grand Pouvoir au sein de la Nature ;<br>
Et pourquoi voudroit-on le supposer ailleurs ?<br>
La Nature est par-tout, par-tout sont ses facteurs :<br>
Et pour apprécier cette Nature immense,<br>
Prenons l’Homme à l’instant où son destin commence ;<br>
Suivons-le pas à pas, de degrés en degrés ;<br>
Voyons de quels objets nous sommes entourés ;<br>
Armons-nous du flambeau de la Philosophie,<br>
Qu’elle dicte mes Vers, qu’elle les justifie ;<br>
Pesons les argumens avec sévérité ;<br>
Distinguons bien l’erreur d’avec la vérité ;<br>
Sur-tout ne concluons qu’après l’expérience,<br>
C’est le plus sûr moyen d’atteindre à l’évidence.</p>

<p>PUISQUE dans l’Homme on voit Matière et Mouvement,<br>
Je conçois qu’il existe un éternel Agent<br>
Qui guide, modifie et régit tous les Etres ;<br>
Qui, pour nous procréer, a détruit nos ancêtres ;<br>
Qui produit sans effort ce que nous admirons,<br>
Et fait bien plus encor que nous n’imaginons.<br>
Mais ce Suprême agent qui Seul a pu tout faire,<br>
Est-il un pur Esprit ou bien est-il Matière ?<br>
L’Esprit est sans Contact pour agir sur les Corps,<br>
Il n’en peut ni former, ni briser les ressorts.<br>
Le Corps de son côté, le Corps quoi en dise,<br>
Pour atteindre à l’Esprit ne peut avoir de prise ;<br>
L’unité de Substance est l’infaillible loi<br>
Que dicte la Raison plus sûre que la Foi.<br>
Ainsi donc la Matière, agissante, énergique,<br>
A dû tout enfanter par sa force organique.<br>
Elle existe du moins, ses effets sont constans ;<br>
Ce que l’on nomme Esprit échappe à tous les Sens ;<br>
Et d’où tireroit-il son absurde origine ?<br>
Quel moyen eût-il eu de la rendre Divine ?<br>
Le Pouvoir appartient au Céleste Elément ;<br>
L’Esprit ne peut pas plus que ne peut le Néant.<br>
Disons qu’un pur Esprit est un Etre factice,<br>
Inventé pour servir d’épouventail au vice.<br>
Dieu, c’est du pur Ether l’éternel mouvement,<br>
Non l’Etre de raison que la Raison dément.</p>

<p>LE Dieu que chaque jour nous nommons notre père,<br>
N’est pour nous, dans le vrai, qu’une insensible mère,<br>
La Nature est son nom ; son éternel emploi<br>
Est de créer sans cesse, et sans savoir pourquoi.<br>
Faite pour enfanter par sa force motrice,<br>
En elle on n’apperçoit qu’une énorme matrice,<br>
Qui, des nombreux essais de ses Conceptions,<br>
N’obtient que des fétus rarement sains et bons.<br>
A quelque haut degré que son pouvoir s’élève,<br>
Cette substance ébauche, et par l’Homme elle achève.<br>
Accablé de besoins, livré à mille maux,<br>
A qui sont dûs nos arts ? à Nous, à nos travaux ;<br>
Sans nos pénibles soins que seroit la Nature ?<br>
Que récolteroit-on dans un champ sans culture ?<br>
L’usage de nos Sens est ce qui nous instruit ;<br>
Eux seuls ils font notre ame, et forment notre Esprit ;<br>
Privé de leur secours, errant de plage en plage,<br>
L’Homme n’habiteroit qu’un repaire sauvage,<br>
Et manquant de raison, comme d’entendement,<br>
Il n’auroit que l’instinct sans aucun jugement.<br>
Ce n’est qu’avec le Tact, la Parole et les Signes,<br>
Avantages divins, privilèges insignes,<br>
Que pouvant réfléchir et sachant comparer,<br>
Nous faisons ce qu’un Dieu ne sauroit opérer.<br>
Ne soyez point surpris, Mortels, si votre vie<br>
De tant de maux divers est constamment suivie ;<br>
La Nature a deux Seins, l’un distile le miel<br>
Et de l’autre plus fécond ne produit que du fiel.<br>
Nature ! il est bien vrai que c’est ton influence<br>
Qui des biens et des maux font pencher la balance ;<br>
Mais de tes dons suspects, puisqu’il faut l’avouer,</p> 

<p>QUAND mon art rend ainsi la vérité sensible ;<br>
Quand je dis qu’à l’Ether il n’est rien d’impossible,<br>
Tu t’irrites, Docteur, et ma raison t’aigrit :<br>
Ecoute, et réponds-moi : Qu’est-ce qu’un pur Esprit ?<br>
Peux-tu le définir ? sans réelle existence,<br>
Dois-tu lui supposer la suprême puissance,<br>
Le pouvoir de créer, de vouloir, de sentir,<br>
De penser et de voir, de toucher et d’ouïr ?<br>
Les Corps organisés ont seuls cet avantage,<br>
Que jamais pur Esprit ne reçut en partage.<br>
Dis-moi ? lorsque ton Cœur éprouve un Sentiment,<br>
Penses-tu qu’un esprit survient au même instant,<br>
Qu’il embrasse l’objet, le poursuit, le compare,<br>
Le juge et fait mouvoir le Corps dont il s’empare ?<br>
De semblables effets seroient plus qu’étonnans.<br>
Ah ! n’y reconnois donc que l’ouvrage des Sens,<br>
Un mécanisme pur, un acte Elémentaire,<br>
Qu’un Esprit n’auroit pas la faculté de faire. »</p>

