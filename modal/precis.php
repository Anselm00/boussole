			  <h4>Précis</h4>
			  
			  <p><b>DOM DESCHAMPS</b></p>
<p>Fin du “Précis en quatre thèses” – 1772</p>

<p>“<b>Le mot DIEU est à retrancher de nos langues</b>, et cela
pour deux raisons :</p>
<p><b>1-</b> À cause de l’idée de Moralité et de Rationalité qu’on lui a
attachée ;</p>
<b>2-</b> Et à cause de l’idée de Tout et Du-tout qu’on a confondue
dans lui, en le disant Infiniment Parfait.<w/p>
<p>En effet, <b>il faut absolument DEUX NOMS</b> pour exprimer
la <b>REALITE</b>, qui se présente sous deux aspects contraires, <b><u>In</u></b>fini
niant ce que Parfait affirme” *.</p>

_______
<p>* Dit autrement, penser à In-fini/Fini, vs In-défini/Défini. »</p>

