			  <h4>Sauvage</h4>
			  
			  <p>« Dans le passé, deux humanités ont régné successivement sur le monde : d’abord<img class="img-in-text-right-bigger" src="img/Vignette-Sauvage.jpg"/>
l’humanité <b>Traditionnelle</b>, dite Primitive ; ensuite l’humanité <b>Civilisée</b>. Parler de
“primitifs” ne veut rien dire, sauf que ce furent les premiers hommes ! parler de
“civilisés” ne veut pas dire grand chose non plus ! <b>En grec</b>, le mot civilisé est
ÈMERE, tiré de ÈMERA = JOUR. Ce qui est civilisé est donc “diurne”, opposé à
“nocturne”, et nocturne est assimilé à Sauvage. C’est pour cela qu’on dit ÈMERE,
aussi bien une plante <b>Cultivée</b>, un animal <b>Apprivoisé</b>, qu’un homme appartenant à
une Cité et non pas à une tribu. Ceci ne nous apprend, ni en quoi consiste
Positivement l’humanité Primitive, ni d’où sort Réellement l’humanité Civilisée. Il
fallut attendre l’Église Réaliste pour mettre tout cela au clair.
L’humanité Traditionnelle et l’humanité Civilisée sont, d’une part directement
contraires, d’autre part absolument identiques.</p>
<p>• Elles sont <b>contraires</b> :</p>
<p>En théorie, la première est <b>Matérialiste</b>, et la seconde <b>Spiritualiste</b>.
<p>En pratique, l’humanité Traditionnelle s’appuie sur le <b>Mythe</b>, la <b>Sorcellerie</b> et
les <b>Préceptes</b>. La Sorcellerie n’est pas une idiotie, elle est réellement opérative, soit
par la voie de la Magie, soit par celle de la Divination. Quant aux Préceptes – du type
des Commandements juifs (MITSVOTS) –, ils règlent les comportements selon le Pur
et l’impur. L’humanité Civilisée fonctionne à l’inverse, s’appuyant sur le <b>Dogme</b>,
l’<b>Intellectualisme</b> (Vrai-Faux) et le <b>Moralisme</b> (Bien-mal).</p>
<p>• Les deux humanités en question sont <b>identiques</b> :</p>
<p>En théorie, elles sont ensemble gouvernées par le <b>Préjugé</b>, c’est-à-dire la prise en
compte exclusive, unilatérale, soit de la Matière, soit de l’Esprit.</p>
<p>En pratique, c’est la prétention commune d’assurer jusqu’à la perfection
l’<b>Hégémonie</b> de la “substance” exclusive exaltée. »</p>

