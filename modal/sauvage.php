Transgressions à cet égard, emportant une responsabilité collective, qui amènent le
trouble dans le grand Organisme de la Nature, dont : cette “absence” anormale ou
précipitée des individus que les civilisés appelleront la mort, et la stérilité d’une
lignée parentale, avant tout du côté maternel. On l’a compris, toute ethnie se tient
pour Élue par son Grand-Génie exclusif concernant son Code Traditionnel, par un
Pacte de Sang ; mais cette élection entraîne une responsabilité cosmique, et le sort de
tous les hommes “étrangers”, plus ou moins impurs, en dépend totalement. »</p>

