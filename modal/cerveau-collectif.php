			  <h4>Cerveau Collectif</h4>
			  
			  <p>« Quand nous parlons de Méthode mentale chez les Primitifs, 
nous risquons de tourner en rond interminablement en ne songeant jamais au point essentiel : 
l’“Individu” primitif se représente lui-même comme simple interprète d’une pensée émise par un cerveau
 collectif, celui de sa Parenté, de sa Race. C’est pourquoi ladite pensée émise par l’Individu “adulte” 
(Membre à part entière de la Communauté de sang) n’a rien à voir avec ce que nous appelons une “idée”. 
De fait, nos “idées” s’expriment dans des “propositions”, se présentent dans le cadre écrit-parlé, 
abstrait-concret, et se formulent selon “l’être” (le “verbe d’état” être, ou anti-verbe) et 
des “substantifs” (noms communs-propres). Rien de tout cela chez les Primitifs, où la pensée 
se présente dans le cadre parole-geste, concret-abstrait, et où “verbe” veut effectivement dire action.</p>

<p>Du fait que l’homme primitif “parle” la pensée d’un cerveau collectif, et que l’Humanité parlante 
	se veut elle-même subordonnée à la Nature féconde de manière essentielle, et l’ensemble nature-humanité 
	formant l’Organisme-Monde, je dis que le principe de la pensée Symbolique est celui de l’Altérité, 
	inverse du principe d’Identité de la pensée Logique (rationnelle). C’est que, pour exprimer 
	la chose dans notre langage, chez les primitifs l’Objet domine le Sujet, contrairement à ce qui se 
	passe chez les civilisés... C’est ainsi que les primitifs sont gouvernés par le Naturalisme, et les civilisés 
	par l’Humanisme. De la même façon, l’Initiation des primitifs remplit la fonction qu’a l’Intellectualisme chez les civilisés. 
	Et en dernière analyse, la Mère Fondamentale, ou Puissance Vitale dernière, a le rôle tenu chez nous par le Père Suprême, 
	ou Dieu Mental premier. »</p>
<p>__________________________</p>

				 

