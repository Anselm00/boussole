<p>« Les Dessins hébraïques ne sont qu’un adjuvant d’une expression essentiellement <b>Orale</b>, un support 
mnémotechnique. Ainsi, chez les primitifs, l’expression du cerveau collectif est orale-gestuelle ; les dessins 
n’y ont de place que comme béquille de la verbalisation. C’est tout à l’inverse que fonctionne l’expression civilisée, 
où l’écrit logique, ontologique (selon l’être), impose sa loi à l’oral. Dans le faux “alphabet” juif, il n’y a 
qu’une expression sophistiquée du QUIPU Inca (cf. Document : L’“Écriture” de comptables). Ce n’est pas en ayant 
des “consonnes”, qu’on “voyelliserait” ensuite, qu’on obtient une écriture, mais par une révolution sociale et mentale complète. »</p>
			
