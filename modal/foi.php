			  <h4>Foi</h4>
			  
			  <p><b> a- Foi et Raison</b></p>
<p>● Pourquoi l’Homme civilisé se doit-il de dominer la Nature ?
Parce que les Personnes de l’Humanité sont dotées d’une Âme active, 
contrairement aux Choses de la Nature. Que dire de ces Âmes ? 
Elles apparaissent elles aussi comme doubles, constituées de deux pôles : 
la Foi et la Raison.</p>
<p>● D’abord, que dire du couple Foi/Raison ?</p>
				  <p>La Foi n’est pas une croyance aveugle. Au contraire, 
				  elle consiste dans le fait que la Raison <b>RELATIVE 
				  de l’Homme</b> – cette raison faible, qui peut 
				  dérailler, ou au moins douter d’elle-même – 
				  si elle est conséquente, est conduite à découvrir <img class="img-in-text-right" src="img/leroux.jpg"/>
				  ses limites, et à s’incliner ELLE-MÊME devant une Raison 
				  supérieure à la sienne, une Raison ABSOLUE.
				  <p>Comme disait un Ami du Peuple :</p>
					 <p><i>“L’Homme est croyant parce qu’il est raisonnable”</i> – 
					  Pierre Leroux.</p>
					  <p>● Ensuite, que veut dire “la Foi est hégémonique sur la Raison” ?</p>
<p>- Dans les conditions où la Religion est réellement orthodoxe, SAINE, la Foi prédominant sur la Raison revient à dire que s’il y a “conflit” entre Foi et Raison, ce n’est pas la Raison qui décide, juge, tranche ou prime sur la Foi, mais l’inverse : face au doute d’une démonstration, c’est toujours la Foi qui l’emporte.</p>
Et si la Raison a la prétention de trancher, la Foi n’en n’est nullement atteinte, mais on se retrouve alors avec une Raison MALADE !</p>
<p>- Mais quand la forme existante de la Religion se trouve en crise, tout se renverse ! Alors la Raison devient reine, elle se déchaîne pour ruiner la religion malade, ce qui crée les conditions d’une nouvelle Révélation, d’une résurrection de la Foi au sortir de la crise.</p>
<p>Citation :</p>
<p><i>“Il n’est pas d’antagonisme possible entre la Foi et la Raison, car la Raison ne s’exerce réellement et n’atteint le vrai que dans la lumière de la Foi.” (…) “Inversement, la Foi ne prescrit rien qui puisse être contraire à la Raison.” </i>– St Augustin (354-430).</p>
<p>St Augustin ne confirme-t-il pas merveilleusement notre position d’hégémonisme !</p>
<p>● Résumons. L’Âme est prise entre deux pôles : la Foi et la Raison.</p>
<p>Notez que :</p>
<p>1- L’Âme a une Base : la Raison,</p>
<p>2- Et un Sommet, la Foi, qui a été nécessaire pour “surmonter” l’infirmité de la Raison. »</p>
