			  <h4>Force Féconde</h4>
			  

			  <p> « <b>Ézéchiel I – II – III</b>
<p>“Il y eut une sorte d’Arc-en-Ciel : <img class="img-in-text-left" src="img/ezechiel.jpg"/>c’était la manifestation (spiration) de la Force de YHWH (la CHEKHINAH). Voyant l’Éclat, je m’affalai et entendis une Voix. Celle-ci dit : Fils-d’Homme, relève-toi pour m’écouter. Alors, de la force de la Force me pénétra, de sorte que je pus me mettre debout.</p>
<p>La Voix dit : Je veux t’envoyer chez les Fils d’Israël, vers ces gens rebelles. En effet, eux et leurs pères n’ont toujours fait que Transgresser le pacte juré avec Moi. À ces gens à la face insolente et au coeur têtu, tu déclareras ceci : Élohim-Yahweh l’a décidé, que cela vous plaise ou non : c’est maintenant à un Prophète que vous avez affaire !</p>
<p>La Voix dit : toi, Fils d’Homme, ne crains ni eux ni ce qu’ils diront ; ce ne sont que des rebelles ! N’aie donc pas peur d’avoir à marcher dans les ronces et à te trouver au milieu de scorpions ; ce ne sont que des rebelles !</p>
<p>La Voix dit : toi, Fils d’Homme, écoute-moi bien : tu ne seras pas un rebelle parmi l’engeance rebelle ! Pour cela, ouvre la bouche et mange ! Je regarde et vois une main tendant un Rouleau marqué de signes. La Force déroule la feuille, dont chaque côté est couvert de paroles de Pleurs, de Reproches et de Gémissements. La Voix dit : Fils d’Homme ! mange ce Rouleau, et va haranguer l’engeance d’Israël. Ma bouche prend et mâche la feuille ; et c’est à mon palais aussi doux que le miel.</p>
<p>La Voix dit : sache que tu ne vas pas parler à des idiots ; ceux-là, ils t’écouteraient ! Hélas, l’engeance d’Israël, elle, refusera de t’entendre ! Mais vois comme je rends ton visage hardi, et comme je te fais un front de diamant, plus dur que le roc ! Ainsi, tu n’auras pas à trembler devant l’engeance de Rébellion”. »</p>

