			  <h4>Mère Fondamentale</h4>
			  
			  <p>« La Mère, prise séparément de son Monde, 
			  n’est intelligible que si on l’aborde comme un 
			  Couple, par exemple Matière-Secrète avec Mère-Matière 
			  exotérique. Ensuite, il faut laisser de côté 
			  la Matière-Secrète. Alors, Mère-Matière exotérique, 
			  mais encore seulement susceptible d’Émanation d’un monde, 
			  devient la Mère-Émanatrice effective du Monde précis 
			  existant. Mais à ce moment, c’est aussitôt dans une 
			  Triade qu’elle se dévoile, genre Osiris-Isis-Horus, 
			  ou Isis-Osiris-Thôht (Hermès). Dans le premier cas, 
			  la Mère-Secrète est Sev (Cronos) ; dans le second cas, 
			  c’est Natphé (Rhéa). Dans les deux cas, Osiris-Isis 
			  ou Isis-Osiris sont un Couple d’enfants engendrés par 
			  la Mère-Secrète. »</p>

			  <p>« En émanant le monde comme matérialité Relative, la Mère le donne en totalité comme double : fécond substantiellement et stérile accidentellement. C’est ainsi que se présente l’hégémonie de la matière sur l’esprit. La relation unilatérale fécond-stérile est ce qui tient lieu, chez les primitifs de la relation bien-mal chez les civilisés. On voit que les deux relations sont complètement hétérogènes par leur contenu, et même directement inverses ; seule la forme hégémonique-unilatérale permet d’identifier les deux rapports.</p>
<p>On dit la même chose que fécond-stérile si on dit : fertile-aride, humide-sec, époux-veuf, organique-mécanique, vivant-inerte, sérié-désert. “Sérié” veut dire lié, attaché ; et “désert” abandonné, banni. “Époux” veut dire engagé, fiancé ; et “veuf” séparé, privé de, vide (cf. conjugale).</p>
<p>Ne pas oublier que, de même que les civilisés n’accordèrent jamais d’existence réelle au Mal, jugé seulement “privation” de Bien, de même les primitifs ne donnèrent pas une existence réelle à la stérilité, aridité, etc., toujours jugée comme simple “manquement” de la fécondité ou fertilité. »</p>


