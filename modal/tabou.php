			  <h4>Tabou</h4>
			  
			  <p>« Quand on parle de “nom” tabou, interdit, c’est en réalité d’un Verbe tabou qu’il s’agit, c’est-à-dire d’un mot agissant, efficace ; c’est cela que nous représentent les “imprécations” et “malédictions” primitives. Le Verbe tabou proféré pourrait pétrifier la Nature et le Monde, fossiliser l’“existence” en “être”. Une image est donnée de ceci par ce qui arriva à la femme de Lot : transgressant l’ordre de Yaweh, à l’insu de son mari elle se retourna, ce qui la fit se transformer immédiatement en une “colonne de sel”.</p>
<p>La Matière pour-le-Monde possède un nom, mais est Verbe Muet. C’est la Mère Fondamentale pure et simple, susceptible de se rendre elle-même grosse du Monde, de l’accoucher et de l’allaiter. Mais en-Elle-même, qu’a-t-elle besoin d’Émaner la Vie, d’une Descendance sous la forme de l’Œuf du monde !…</p>
<p>La Matière ou Mère Fondamentale est cependant pour-le-Monde l’Objet Absolu, l’Océan de toute source, la Racine de tout germe, le Sang de toute vie.</p>
<p>La double face de la Matière apparaît très précisément dans l’Hindouisme, avec les difficultés que cela entraîne pour les exégètes pour expliquer en quoi se distingue Brahm, l’Absolu de genre neutre, et Brahmâ, l’Émanateur virtuel sexué. On voit d’ailleurs qu’aussitôt Brahmâ posé, on le trouve s’associer au couple Shiva-Vichnou pour former une triade. À ce moment, les commentateurs peuvent se convaincre qu’ils abordent le territoire “religieux” familier (bien qu’“étrange” !), et ils se trouvent à l’aise pour broder à volonté à l’intention de la clientèle friande d’exotisme oriental... Je précise au passage que, concernant le Monde Émané, pour la multitude hindouiste, la Mère Fondamentale Pârvatî l’emporte sur tout ; et que l’hindouisme ne se gêne pas pour affirmer l’hégémonie de la Nature sur l’humanité en proclamant la primauté de la matière du monde sous le nom de Purusha. »</p>


