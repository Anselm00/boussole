			  <h4>Saint Bon Intègre</h4>
			  
			  <p> « <b>a- Trinité</b> (§ 167 à 170)</p>

			  <p style="text-align:center;"><b>§ 167</b></p>
<p>C’est comme Souverain MORAL de la Création que Dieu nous est donné. L’analyse
en déduit trois Attributs ; Dieu est :</p>
<p><b>1- SAINT</b>. Concernant <b>la Création</b> dans son ensemble, nous découvrons que les
Lois (de Liberté humaine et de Nécessité naturelle) qui sont à son Fondement
conspirent au bien ; ce qui fait de leur auteur le Bien Absolu = Saint.</p>
<p><b>2- BON.</b> Concernant <b>l’Humanité</b> au sein de la Création, nous la découvrons tout
spécialement Favorisée par sa Raison surnaturelle-divine (et jusque dans la
constitution corporelle qui lui convient) ; sollicitude particulière de Dieu en montrant
la Bonté absolue.</p>
<p><b>3- INTÈGRE</b>. Concernant <b>les Personnes</b> dans leur conduite au sein de
l’Humanité, nous sommes assurés que notre Destinée sera jugée avec une Intégrité
absolue.</p>
<p style="text-align:center;"><b>§ 168</b></p>
<p>Cette triade Saint-Bon-Intègre ne fait que préciser le soin Moral que Dieu prend de
l’Humanité, but même de la Création. Il ne faut donc y voir aucun mystère. Et
pourtant, avant l’Évangile, c’était comme si on n’y avait jamais pensé. À cette
question, il est permis de donner la réponse suivante : jusque-là, et par leur propre
faute, la voix de la Raison s’était tue chez les hommes ; et c’est pourquoi le message
de Josué a pu apparaître comme une Révélation.</p>
<p style="text-align:center;"><b>§ 169</b></p>
<p>Précisons que dans ses trois Attributs Moraux, Dieu évite tout autant le Rigorisme
que le Laxisme :</p>
<p>- Il est <b>Saint</b> en donnant à la Création des lois proportionnées à notre Raison,
c’est-à-dire ni en Tyran, ni en “Roi fainéant” ;</p>
<p>- Il est <b>Bon</b> en secourant des partisans de la Loi Morale, et pas des déserteurs ;</p>
<p>- Il est <b>Intègre</b> en n’admettant pas que nous rusions avec notre Responsabilité,
mais en ne réclamant de nous que ce qui est possible sur Terre.</p>

<p style="text-align:center;"><b>§ 170</b></p>
<p>Tel est donc le Credo complet de la vraie religion : Dieu est Une seule Personne, qui se découvre Trine moralement. Voilà ruiné totalement l’anthropomorphisme
grossier qui voyait en Dieu un chef politique.</p>

<p><b>b- Peuple de Dieu</b> (§ 171 à 178)</p>
<p style="text-align:center;"><b>§ 171</b></p>
<p>Aux trois Attributs de Dieu correspondent point par point trois Attributs du Peuple
de Dieu. Ici encore, ce qui était mystère jusqu’ici s’établit par la simple analyse
rationnelle. L’expression “Peuple de Dieu” sous-entend trois choses :</p>
<p><b><i>1- La Vocation</i></b></p>
<p style="text-align:center;"><b>§ 172</b></p>
<p>C’est le fait que le croyant se trouve “appelé à la Sainteté” (Romains 1 : 7), c’est-à-
dire appelé à devenir citoyen de la République des Agréés en l’Autre-monde, où chacun fait preuve d’une soumission absolue à la Loi de Dieu.
Mais ici se présente une difficulté insurmontable pour l’Entendement. Selon ce
dernier, l’état de Bienheureux ne peut se concevoir que comme le triomphe direct de la Loi Morale, celle-ci ayant opéré comme une Cause Finale. Ceci aboutirait, si on ne
faisait pas appel au mystère pour faire une place à la liberté humaine, à présenter les
Agréés de Dieu comme des citoyens n’ayant que des devoirs et pas de droits, à
l’inverse de leur père n’ayant que des droits et pas de devoirs.</p>
<p style="text-align:center;"><b>§ 173</b></p>
<p>La faculté Morale de la Raison fait sauter la contradiction. Elle dit : notre
<b>Liberté</b> est innée, et la Loi Morale est le contenu de notre Conscience. Par suite, en
tant que citoyen de la République divine, c’est Moi qui décide ; mais je décide “comme si” c’était Dieu qui le faisait. “Il n’y a pas d’Impératif pour Dieu et les
Saints”, dit Kant.</p>
<p><b><i>2- La Satisfaction</i></b></p>
<p style="text-align:center;"><b>§ 174</b></p>
<p>Ceci est le fait de la Bonne <b>Volonté</b>, à l’oeuvre en Ce-monde, prenant pour modèle
le Héros Parfait du Bien (§ 57).</p>
<p>Là encore, un problème insoluble se pose à l’Entendement (§ 60 à 70). Selon ce
dernier, il est inconcevable d’admettre que, même une Personne ayant détruit en elle
l’Idéal Négatif (§ 35) et ayant toujours plus revêtu le nouvel homme, puisse satisfaire
suffisamment pour prétendre être rangée parmi les agréés ; et seul l’appel au mystère
le tire d’affaire.</p>
<p style="text-align:center;"><b>§ 175</b></p>
<p>La faculté Morale de la Raison résout aisément le problème : la Bonté divine serait
un vain mot si elle ne donnait pas lieu à une “satisfaction substituée”, les Œuvres
bonnes du Nouvel Homme lui “méritant” l’extinction des dettes du Vieil Homme.</p>
<p><b><i>3- L’Élection</i></b></p>
<p style="text-align:center;"><b>§ 176</b></p>
<p>C’est le fait “radical”, la racine de tout : notre Conversion dans le temps de Ce-
monde va-t-elle jusqu’à empiéter sur le <b>Jugement</b> de Dieu, relativement au Penchant au Mal que nous nous sommes donnés en même temps que notre Liberté
nous était donnée ?</p>
Nouvel écueil pour l’entendement. Et nouvel appel au mystère de sa part.
<p style="text-align:center;"><b>§ 177</b></p>
<p>La faculté Morale nous ordonne ce “changement du coeur” ; donc il doit être possible. Aussi, malgré la contradiction que peut invoquer la Logique Formelle (le
temps terrestre “mord” sur le perpétuel ; le phénoménal sur le nouménal), on peut
déclarer avec assurance : tout homme faisant son devoir peut espérer se trouver
Prédestiné à la Conversion.</p>
<p><b><i>Conclusion</i></b></p>
<p style="text-align:center;"><b>§ 178</b></p>
<p>Le Credo de l’Église Parfaite est entièrement rationnel. Bien sûr, on nous
demandera encore : Pourquoi le Bien et le Mal sont-ils aux prises en Notre-monde ?
Comment du Mal absolu peut-on sortir pour embrasser l’Idéal de Sainteté ? Et
pourquoi cela arrive-t-il chez certains et pas à d’autres ? Réponse : ceci revient à
vouloir percer le mystère de Dieu en lui-même ; il ne nous en a rien dit en Ce-monde
et, le ferait-il que nous ne pourrions pas le comprendre !</p>
<p>Mais, et n’est-ce pas ce <b>qui importe</b> ?, en ce qui concerne notre Conduite à tenir, Dieu nous a révélé tout ce dont nous avons besoin ; et cette révélation est pleinement
compréhensible par n’importe qui. »</p>
