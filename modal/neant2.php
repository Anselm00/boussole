			  <h4>Néant</h4>
			  
		 <p>« selon la mentalité civilisée, tout ce qui peut “manquer” d’esprit, à
un titre ou degré quelconque, est non-être ou néant, c’est-à-dire relève de la matière
d’une façon ou d’une autre. Retenons donc bien que quand il est question d’un être
“matériel” du point de vue spiritualiste, cela ne se rapporte pas à une “substance” que
serait véritablement la matière, mais à une simple déficience quant à l’être
proprement dit, qui ne saurait être que spirituel.</p>
<p>Cette démarche, appliquée à <b>Dieu</b>, qu’on désigne précisément comme l’Être
Suprême, ne pose pas de problème. Dieu est l’Esprit Absolu, ce qui veut dire qu’en lui
la Matière figure comme Néant Absolu. Il faut avouer que cette assertion ultime de la
Raison devient du même coup affaire de Foi, que l’enquête menée auprès de Dieu à propos de l’affaire de l’être, amène à écarter cette piste pour cause de Mystère. Mais
cela suffit : concernant “l’être”, si l’on s’adresse à Dieu, il faut classer l’affaire,
prononcer un non-lieu avant tout jugement.</p>
<p>Mais l’homme civilisé est au <b>Monde</b> et du Monde, il n’est pas Dieu et le Monde
non plus ; et au Monde, “il y a” de la Matière partout, matière qu’il nous est certes
permis de traiter en Non-être mais point du tout en Néant. Cela fait une énorme
différence. Voir le Monde selon l’être va se compliquer diablement ! Vis-à-vis de Dieu,
le Monde-même ne peut s’envisager que comme le plus éminent des Êtres Infimes.
Cela va devenir encore plus embrouillé quand il va falloir “mettre en examen”
l’indéfinité des “petits êtres” qu’on dit constituer le Monde dans le détail. L’ontologie
mondaine est un sacré problème ! Par quel bout le prendre ? »</p>



