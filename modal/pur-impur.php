			  <h4>Pur/Impur</h4>
			  
			  <p>« Le Tabou recouvre les notions de “saint”, “licite” et “prohibé”. Il manifeste que le
mode de pensée traditionnel ne conçoit que des contraires relatifs, qui se recueillent
finalement dans la Communion de l’Un ultime. Selon le Tabou, les contraires sont du
type bénéfique/maléfique. La sagesse traditionnelle portera ce couple à sa plus haute
expression sous la forme pur/impur. Précisons la nature de ce couple suprême de la
“logique” primitive :</p>
<p>• Primo, il s’agit ici d’une polarité effective, tout à fait différente de l’antagonisme
philosophique que nous connaissons. Notre opposition de type Oui/Non est non
seulement un rapport de pure exclusion, mais en outre cette exclusion se doit d’être
unilatérale : le “non” ne peut avoir que la réalité d’une “privation d’être”, de “néant”.
Dans la méthode traditionnelle, il en va autrement. Les “ténèbres” opposées à la
“lumière”, ou la “femelle” opposée au “mâle” ne permettent pas d’absolutiser le pôle
négatif ; ils conservent à ce dernier tout autant de réalité qu’au pôle positif.</p>
<p>• Secundo, il est admis qu’il est tout autant possible de “neutraliser” le “néfaste”
que de s’annexer le “faste”, étant donné, précisément, que les deux pôles n’ont
d’existence que par la tension qu’ils entretiennent. Entre les pôles du “saint” et du
“souillé”, par exemple, il y a toujours un “milieu” d’Indifférence que les rites
coutumiers (intangibles) peuvent ménager. Ceci peut s’obtenir par le jeûne, des
purifications, ou toute autre cérémonie adéquate. Exemple asiate : “Si un enfant
naissait sous un signe néfaste, on attendait quelques jours avant de lui imposer un
nom, jusqu’à un signe favorable”.</p>
<p>• Tertio, la polarité de type bénéfique/maléfique embrasse tout à la fois les
relations naturelles et humaines, le monde “physique” et le monde “moral”. Ceci crée
un abîme avec nos distinctions antagoniques absolument différenciées comprenant,
d’une part le Vrai/Faux et d’autre part le Bien/Mal. »</p>




