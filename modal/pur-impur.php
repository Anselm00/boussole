			  <p>« Chez les Primitifs, l’Ordre matériel général détermine le Bien social particulier.
Par suite, socialement, chez les primitifs, le système général des “esprits” n’est rien
d’autre que le système des expressions contrastées et diversifiées de la spontanéité
matérielle ; c’est seulement ce qui embrasse les manifestations qualitatives ramifiées
de cette sorte de “mouvement premier” répandu dans tout l’univers sensible. Mais du
même coup, la forme indifférenciée de ce “mana” général et qui, selon nous, pourrait
être “Dieu” en la circonstance, ne peut présenter le moindre intérêt. À la limite, on
s’arrêterait à l’évocation d’un couple explicite, qui resterait dans l’ordre du manifeste,
tel que : mâle/femelle, pur/impur, ou sec/humide. Bref, on peut bien avoir ce que
nous avons baptisé “des esprits”, mais en aucun cas “de l’esprit”, c’est-à-dire des
âmes, des lois abstraites et enfin Dieu. Une deuxième conséquence, c’est que les
fameux “esprits” des primitifs désignent principalement un réseau “d’influences”
envisagées comme “malignes”. Ceci permet d’expliquer certaines réflexions de
missionnaires déroutés : “Les Brésiliens ne croient point à l’existence d’un Dieu, mais
admettent très bien l’existence d’un Diable” (Letourneau). De fait, la “droiture”, le
comportement du “juste”, consiste dans le respect scrupuleux de prescriptions ayant
pour but avoué la fonction pratique de conjurer et maîtriser les puissances
diversifiées du “néfaste” et de se mettre à l’abri des “souillures”. D’où le “légalisme”
déclaré que traduisent aussi bien le talion que la vendetta. D’où le caractère “médical”
prononcé des ablutions et autres purifications, qui permettent d’annuler les effets des
“transgressions” inévitables des “interdits” que la “tradition” perpétue. Ce contexte
mental primitif, dans son expression sociale, traduit dans le langage inadapté de la
civilisation, on le dirait faisant primer le Droit sur la Morale. Et cela, nous le trouvons en vigueur jusque dans le célèbre “Livre des Morts Égyptien”, quelque détournement
que veuillent en faire les spiritualistes décadents de notre époque.  »</p>

