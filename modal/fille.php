				  «	Chez les Primitifs, il y avait une Mère fondamentale du monde, telle Cybèle, “La Dame” ; et de cette Mère s’auto-fécondant, émanait le monde. Chez les Civilisés, il y a un Père du monde, qui s’avoue lui-même niant et relayant la Mère, preuve en est que Zeus est dit fils de Cybèle-Rhéa. Zeus, créateur des dieux et des hommes, Démiurge (“ouvrier” Suprême), a pour première épouse Métis, fille de l’Abîme liquide. Métis enceinte, Zeus craint que l’enfant ne soit plus sage et puissant que lui-même ; il avale la mère au premier mois de la grossesse, et c’est de la tête de Zeus (non pas de la matrice maternelle) que sort Athéna, qui institue l’Aréopage d’Athènes, préside donc à la Cité civilisée. On voit ici le retournement du matérialisme primitif en spiritualisme civilisé. Alors que Cybèle était la source de toute conception matérielle et physique, de fécondité manifeste, Zeus “conçoit”, au sens spirituel et moral, du travail manifeste. L’inversion de sens du verbe “concevoir” est caractéristique. »</p>	  

