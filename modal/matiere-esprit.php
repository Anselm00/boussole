			  <h4>Matière Esprit</h4>
			  
			  <p>« Il faut distinguer la <b>Réalité</b> et le <b>Monde</b>, et bien voir la relation entre les deux 
				  “choses” (faut bien utiliser les mots à notre disposition !).</p>
<p><b>1- La Réalité est Théorique, “abstraite”</b>. On ne peut mieux la définir que comme identité 
	ABSOLUE Matière-Esprit, “confusion” totale de ces deux vieilles “Substances” préhistoriques, qui se voulaient chacune exclusive ; 
	la première sous la société Parentale et la seconde sous la société Politique. Dans les deux cas, on disait que l’une des 
	deux Substances était exclusive, exempte de toute trace de l’autre, “en dernière analyse” ; d’où la Mère fondamentale des primitifs 
	et le Père suprême des civilisés. Mais sous ces noms de Mère et Père, il y avait déjà allusion, soit à une Émanation du Monde, 
	soit à une Création du Monde. Et, s’agissant du Monde pris à part, il fallait faire une place à la Substance adverse, qu’on disait 
	alors se trouver sous l’Hégémonie de DROIT de la première, quoique portée à la rébellion de FAIT, par les Transgressions physiques 
	ou les Péchés moraux des hommes, respectivement.</p>
<p>Notre Théorie, <b>le Réalisme Vrai</b>, déclare que Matière et Esprit sont deux contraires absolument Identiques quant à la Réalité “en elle-même”, 
	la réalité “absolue” échappant à notre pratique. Bref, la Réalité au sens strict du mot est Mère-Père indissolublement ; on ne peut la concevoir 
	que sous la forme de ce RAPPORT. Voilà pour notre Théorie. La Réalité est donc “abstraite”, mais pas au sens de purement Nominale, n’existant que 
	dans notre tête ; au contraire rien n’est plus Réel que la Réalité ; c’est “le fin du fin”, “le fort et le fin” du Monde ; “le fin fond”, “le fond du sac” du Monde. 
	On ne le découvre que PAR la pensée, mais c’est tout le contraire d’un simple “être de raison”. Ceci est vrai à un point tel que l’“idée” de Réalité, 
	déroutante pour les hommes de la société Politique, précisément parce que Rapport mental, est la seule “communion” psychique avec la Réalité qui nous est permise.</p>
<p>Ceci nous ramène à <b>la Conscience</b>. Nous savons que le FAIT de la conscience est matériel, naturel, Physique. C’est la sensation PURE (ni toucher, odorat, etc.) 
	que les hommes <b>seuls</b> éprouvent, parce qu’à l’occasion de la pensée. C’est la sensation de la Vie même attachée à la Matière, à la Réalité saisie sous l’angle 
	matériel, vie présente de la même manière en tout corps. Cette sensation atteste que notre corps Particulier appartient non seulement à la Nature Générale, 
	mais encore à la Matière Universelle. Il y a encore autre chose, si on considère le privilège humain de cette Conscience, venant du fait que nous ressentons 
	la Vie universelle “à l’occasion” de la pensée, propre seulement à l’Humanité.</p>
<p>La pensée effective, “en acte”, n’est autre que <b>TRAVAIL mental, c’est-à-dire “mise en rapport”</b> ; ce qui ne fait qu’épouser, à la mesure du Monde, 
donc de manière Relative, le Rapport de la Réalité dans ses expressions pratiques. Or, le travail mental de base, celui qui “ne sert à rien”, mais conditionne 
tous les autres, est celui de la <b>RÉFLEXION sur la Conscience</b>, la pensée qui prend pour “objet” le fait de conscience. Cette pensée de la conscience vide, 
de <b>l’éphémérité même</b>, est le Rapport mental fondamental. Cette pensée n’est pas une “connaissance” ordinaire ; c’est bien plutôt la seule GNOSE (Théologie) 
qui nous soit permise. Et on peut dire que cette Gnose (connaissance suprême) <b>porte</b> sur un “Savoir”, si on se souvient que ce mot veut dire une SAVEUR, 
un “goût”, puisqu’il s’agit de la Conscience physique sentie. Si donc nous partons de la “pensée première”, la réflexion sur la Conscience, nous n’y trouvons 
d’autre contenu que l’Éphémère, du “présent qui passe”, du <b>Temps</b> (durée générale, pas celle du “temps” étroit des civilisés). Ceci est très important, 
parce que ce rapport fondamental de la pensée (donc chez les Hommes), coïncidant avec l’Intuition du Temps (intuition = vision immédiate) ne concerne pas 
seulement les hommes, mais également la Nature et la Réalité : c’est l’autre manière de désigner la Vie par le mot Pensée, l’autre manière de désigner la Matière 
par le mot <b>Esprit</b>. Ce n’est pas par hasard que ce sont les civilisés, spiritualistes, qui ont mis à jour cette “autre face” de la Réalité. Seulement, à cause de 
“l’occasion de la pensée”, ils n’ont voulu voir dans l’Esprit (et son avatar Éternité-Temps) que la “vraie” Substance détrônant la Matière de la société Parentale. 
La Durée appartient bien autant à la Réalité ; et l’humanité préhistorique en a fait des usages décisifs dans son travail mental appuyé sur le couple Mémoire-Imagination, 
soit par ses Devins, soit par ses Prophètes (devins Possédés et prophètes Inspirés). »</p>

