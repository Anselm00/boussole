			  <h4>Néant</h4>
			  
		 <p>« Le Monde émané de la Mère Fondamentale ne peut être, comme Elle, qu’essentiellement matériel, maternel, fécond. Cependant le Monde n’est pas la Mère, laquelle est seule Immanence absolue. Le Monde n’est que matérialité dépendante, dérivée, seconde, matière Relative. Ceci a les plus grandes conséquences. Au Monde, il n’est de Matière que grevée d’Esprit, d’Instinct vital grevé d’Intelligence, d’Existence que grevée d’Être. En effet, l’Existence, qui prime au monde, est déjà toute autre que le Néant absolu de la Mère. Quand nous disons que le monde est marqué d’“Intelligence” au titre d’“accident nécessaire” de l’Instinct matériel qui y préside, il faut se garder de rapporter directement et exclusivement l’intelligence à l’humanité, comme les civilisés sont tentés de le faire. Chez les primitifs, l’intelligence qui se donne comme “privation” d’instinct, marque l’Humanité seulement à un degré supérieur ; mais à un moindre degré, elle “souille” également la Nature ; de plus, l’esprit reste même présent en l’En-Deçà de notre monde immédiat où, pourtant, la Vieillesse que nous connaissons trouve à se guérir en s’y abreuvant d’Eau de Jouvence (cf. l’Hydromel gaulois, alcool d’Eau-Miel). J’insiste au passage sur le fait que nos idées de vie-mort n’ont aucune place dans la mentalité primitive. »</p>



