				  <p>Qu’est la Société Parentale, la société Primitive, 
					  celle des Gaulois par exemple ?</p>
<p>Un seul mot la définit pratiquement : Égalité. <b>Chose inouïe 
	que cette Égalité</b> : elle était un non-sens dans la horde 
	antérieure de quadrumanes ; elle sera méconnue et traquée 
	dans la société Politique, civilisée postérieure, sauf à se 
	retrouver falsifiée dans les utopies “réactionnaires” de Cités 
	altruistes nécessairement ascétiques.</p>
<p>Les membres “organiques” de la Communauté Parentale 
	n’ont pas du tout voulu cette Égalité constitutive de la 
	première humanité ! Ils naissent avec elle. Pourquoi ? 
	Parce qu’ils ne peuvent voir le monde auquel ils appartiennent 
	que comme constitué d’une substance : la <b>Matière</b>, et cette 
	substance n’ayant qu’une expression : la <b>Vie</b>. Cette conception 
	est tout à fait fondée, puisque la Réalité comprend réellement 
	la Matière. Il n’en reste pas moins qu’il s’agit d’une conception, 
	d’une découverte humaine, et que cette conception est unilatérale, 
	car la Réalité comprend également l’Esprit, le “contraire identique” 
	de la Matière. Il y a donc une grossière confusion de la part de ceux 
	qui prétendent que “les Sauvages sont près de la Nature” ! Ils sont Matérialistes, 
	c’est tout. Établir la réalité historique est tout autre chose qu’un défoulement romantique.</p>
<p>On ne trouve d’Égalité sociale dans le passé que dans l’humanité 
	Parentale, chez les “sauvages” comme on dit rapidement. C’est le 
	point dont il ne faut pas démordre. Mais, ceci dit, QUELLE Égalité ? 
	Voilà ce qu’il importe de bien saisir.</p>
<p>Disons que l’Égalité Parentale est d’abord Négative, 
	et ensuite Régressive.</p>
<p>• Elle est <b>Négative</b> parce que ne présente d’Égalité 
	que vis-à-vis de la horde animale (ou bien, plus largement, 
	vis-à-vis de toute branche de “l’Arbre du Monde” tel que le 
	voient les Primitifs, y compris ce que nous appelons les 
	minéraux, l’inerte vu alors vivant et “sexué” comme le reste).</p>
<p>C’est une rupture historique décisive que celle de la Horde 
	Animale devenant Tribu Humaine. Il y a entre la Horde et la 
	Tribu l’abîme qui sépare le Rut (rugissement) périodique animal 
	et la Sexualité humaine. La sexualité parentale ne relève en 
	aucune façon d’un instinct biologique, mais du souci social 
	d’une progéniture. De plus, la filiation selon la Fécondité 
	délibérée (étroitement maternelle au départ) se trouve réglée 
	par la polarisation interne à la tribu, entre clans mariables 
	collectivement, l’Inceste étant prohibé au sein de chacun d’eux. 
	Notons qu’Inceste (incestum en latin) veut dire Impur, le contraire 
	de Chaste (castum) : Pur (de là Châtiment = Purification). C’est sur 
	cette base négative, “anti-animale”, que reposera la société 
	Parentale tout au long de son histoire.</p>

	<p>• La société Parentale est ensuite <b>Régressive</b>. 
		Cela veut dire que son développement même est 
		guidé par l’idée d’être toujours plus absolument 
		et universellement Pure. Telle est la démarche Matérialiste. 
		On va vers le perfectionnement de la société Parentale en 
		intensifiant l’hégémonie des conditions sociales jugées 
		“naturelles”, sur les conditions animales biologiques 
		(celles des primates). C’est ainsi que Cro-Magnon produisit 
		à la fin Pharaon !</p>
<p>L’histoire de la société Parentale, de la communauté pratiquant 
	le commerce charnel “exogame” entre parents, s’acheva quand 
	l’humanité entière fut décrétée théoriquement ne comprendre que 
	des Congénères. C’est le Fils-du-Soleil, à la tête de l’Empire 
	Asiate qui professa cela. En attendant, la masse humaine de son 
	Empire ne se composait plus que de Domestiques, masse indifférenciée 
	de “pourvoyeurs de sacrifices”. Tels furent en Inde (la “Terre Élue”), 
	<b>les SHUDRA</b>, ces “Noirs”, “pieds de Brahmâ”, la mince caste supérieure 
	des Brahmanes, les “Blancs”, étant la “bouche de Brahmâ”.</p>
<p>Quand la société Parentale s’effondra, devint Asociale, 
	l’ancienne <b>Égalité d’Obligeance, simplement Despotique</b>, 
	devint monstrueuse : un monde de sacrifices humains, de 
	la veuve et l’orphelin sans recours, de mendiants et de 
	bannis. Pensons aux Intouchables indiens, CHANDÂL, ces 
	“Shudra Sales” assimilés au chien et au porc ; et sous 
	ces Parias, les Bannis (PATITA), supérieurs encore cependant 
	aux Étrangers à la Terre Pure, les MLECCHA… du monde entier !</p>

