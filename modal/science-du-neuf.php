			  <h4>Science du Neuf</h4>
			  
			  <p>« La science du neuf s’impose. Elle vient à point nommé, vus les désastres provoqués
par la science barbare, et le caractère aigu du problème écologique. La physique du
neuf est indispensable au Comm-Anar qui, en Économie, “fusionne” communisme
(parental, faut pas confondre !) et capitalisme (ce qu’on a appelé de ce nom n’en est
que la forme conséquente). La nouvelle économie, de Besoins/Capacités n’est pas la
NEP de Lénine, ni non plus le “Socialisme de Marché” de Deng.</p>
<p>Le Comm-Anar achève de nous sortir de <b>l’animalité</b> ; et simultanément il nous
débarrasse du mot animal comme une insulte (comme des mots “gonzesse” pour les
hommes, et “dinde” pour les femmes) ! Notre animalité devient assumée
sereinement, écologiquement, à la honte des curés pharisiens et de la Slip-analyse des
freudiens.</p>
<p>La Science du Neuf fait que <b>la Morale</b> devient rigoureuse, comme l’était la
Physique dans la ci-devant civilisation, avant les Fractales, Théories du Chaos, etc. La
Science du Neuf fait de <b>la Physique</b> l’arme d’une véritable écologie, qui n’a plus rien
à voir avec une préservation des « équilibres de la Nature » qui n’ont jamais existé !
La Physique, qui fut le grand espoir de déchaîner les loisirs par le machinisme, doit
absolument cesser de nous EFFRAYER comme elle le fait maintenant, depuis l’atome
jusqu’aux maladies nosocomiales ; et c’est ce que nous ferons en la rendant aussi
exigeante socialement qu’était autrefois la Morale (je veux dire Naturisme Parental
“néo”).<p>
<p>La Science globale refondue, le Comm-Anar apportera <b>l’Union</b> humaine
planétaire que jamais on ne connut ; et avec cela la <b>Communion</b> humaine avec nos
prédécesseurs gentilices-bourgeois dont nous séparaient le préjugé et l’ingratitude (et
avec cela, nous déroulons le tapis de l’unisson pour nos successeurs).
Chose instructive. C’est maladroit de dire : moraliser la physique et physiquer la
morale. On a l’air de tourner en rond dans le civilisé, et donc de retomber dans
l’utopie. Toujours le même problème : on n’a pas les mots pour dire. Mais si on en est
conscient, c’est gagné !</p>
<p>C’est quoi, au fait, <b>le NEUF de notre Science</b> ? Concernant la <b>démarche</b>, on
part à fond avec la pensée d’avoir à maîtriser de l’incomparable, de l’inédit (en
dernière analyse). Ceci, afin de ne pas être “en retard d’une guerre” comme on a dit à
propos de la ligne Maginot ! Concernant <b>les faits-événements</b> qui nous
interpellent, on ne leur donne plus de “noms” que suspensifs, provisoires. Le mot
“nom” lui-même doit être “mis en examen”, car on ne “définit” plus rien strictement.
C’est ainsi que, de façon inattendue, dans tous les domaines on se retrouve en face
que quelque chose qui a à voir avec la Dérivation en Math et Balistique, et finalement avec le problème de П ! La Quadrature ! Il y a une différence quand même : tout cela
fonctionnait avec des CONSTANTES en amont, et on disait que ça n’était rigoureux
qu’« en laboratoire ». Ex : constante “g” de l’Attraction (Newton – 1683) ; constante
“e” de la vitesse de la lumière (Römer – 1676). Pour Newton, Dieu doit corriger de
temps à autre la machine du monde. Pour Römer, on n’a que la vitesse dans le “vide”.
Je ne parle pas de la douzaine de “constantes” d’à présent, qui se contredisent et dont
le fruit est l’anti-matière, les trous noirs, du big-bang, de la 4ème dimension… et autres
fantaisies n’empêchant pas qu’on nous donne la “date de naissance de l’univers” !
Ceci dit, j’avoue volontiers être aussi “nul” en physique qu’en hébreu. Ya du pain sur
la planche…</p>
<p>Pourquoi la matière de la science est de l’inédit « en dernière analyse » ? Autrefois,
chez nos grands-parents, on changeait de nom aux “rites de passage”, et en plus on
avait un nom “secret”, le vrai, en plus du nom audible. J’ai connu une coréenne
gravement malade dans son enfance, à qui un “sorcier” fit changer de nom et adopter
un nom de garçon, ce qui la fit guérir. Il y eut aussi Saraï qui devint Sarah, et Saül qui
devint Paul. <b>Maintenant</b>, nous disons qu’à chaque “instant” “à la limite”, il faudrait
changer le nom de tout ; ce qui <b>n’empêche pas</b> que d’un autre côté tout est pareil,
mérite le nom de réalité minuscule, “participant de” la Réalité majuscule au même
titre, nom qui reste attaché dans les 2 “morts” : celle d’avant la “vie” et celle d’après la
“vie”. Nous sommes donc Immortels en tant que constitutifs de l’Eternel. C’est <b>ainsi</b>
que « l’Absolu est dans le Relatif », comme disait Engels.
<p>On pourra nous taxer de Panthéisme ? Cela ne marche qu’avec Dieu (Théos) ; de
plus il y a 2 panthéismes : spéculatif et sensualiste. Et c’est quoi le “contraire” de
Panthéisme ? Avec une Réalité biface, rapport, donc <b>elle-même</b> “relative”, on
possède du en-Soi même qui est un Absolu tout nouveau échappant aux vieilles
étiquettes. Certes, il n’y a pas de Mystère-Secret “derrière” la Réalité, et c’est tout ce
qui motivait l’accusation.</p>
<p>On pourra revenir à la charge et dire : Pourquoi y a-t-il la Réalité plutôt que rien ?
Le mot “pourquoi” nous ramène dans l’horizon civilisé de Cause-Effet. Nous sommes,
quant à nous dans l’« AutoGène » (cf. p. 6), ce que “frôla” Marx… et même Kant. Moi
qui suis la Réalité au fin fond de moi, comment puis-je “douter” d’elle ?! »</p>

