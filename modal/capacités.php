<p><b>• Ouvrir le Communisme :</b></p>
<p>Quant au contenu, le Communisme Historique n’a plus guère de chose à voir avec
le Socialisme des Rouges et de Marx, qui attendaient d’un capitalisme collectif qu’il
neutralise “l’exploitation de l’homme par l’homme”. Qu’espérait-on du Socialisme ?
C’était une profusion de Choses anciennes offertes aux anciennes Personnes ; si
l’abondance de marchandises changeait les nouveaux marchands associés, c’était
essentiellement en leur ouvrant largement des Loisirs, qu’ils occupaient en se dotant
d’une Culture encyclopédique. Le Communisme Historique aborde la chose très
différemment.</p>
<p>• “De chacun selon ses Capacités” signifie se découvrir, s’inventer, des Capacités
inouïes absolument, car devant être conformes au Volontariat ;</p>
<p>• “À chacun selon ses Besoins” signifie se découvrir, s’inventer, des Besoins
absolument inédits, car devant être conformes à la Gratuité.</p>
<p>Ce n’est pas implicitement à partir des Choses, comme auparavant, que le
Communisme Historique se définit, mais délibérément à partir des Personnes. Ainsi,
le Communisme Historique est-il essentiellement l’avènement de la 3ème espèce de
la race humaine. Et c’est pour cette raison même que l’Humanité nouvelle doit
vouloir une Nature nouvelle, et que le Communisme Historique inaugure tout un
Monde nouveau, intrinsèquement Écologique.</p>
<p>La Disparition de la Contradiction antagonique du Parasitisme capitaliste est
simultanément une Transformation de la contradiction de la Préhistoire humaine en
Rapport Communiste, lequel n’inaugure rien moins que le processus de
Transfiguration du Monde par la 3ème espèce humaine.</p>
<p>“La mort n’est qu’une transfiguration glorieuse”, dit-on. On peut le dire de
l’humanité Préhistorique qui doit se faire Communiste. L’image de cette Palingénésie
de l’Humanité peut nous en être donnée par la Transfiguration du “Fils de l’Homme”,
le “Second Adam” Jésus-Christ, qui connut cet état Glorieux un an avant sa Passion,
après la Pâque de l’an 32, sur le mont Hermon. L’Évangile rapporte : “Le visage de
Jésus resplendissait comme le Soleil, et ses vêtements étaient blancs comme la Neige”
(Matth. XIII).</p>


