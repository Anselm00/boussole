      <h4><b>Besoins</b></h4>
	  		
	  <p> « <b><u>LE NOUVEL HOMME</u></b></p><img class="img-in-text-right" src="img/jc.jpg"/>
<p>Ça fait longtemps qu’on en parle ! Jésus n’était-il pas le Nouvel Adam ? Les
marxistes parlèrent d’« Homme Total », « Polytechnique », du régime social « De
chacun selon ses capacités, à chacun selon ses besoins ». L’ERM, elle, met en route
l’Homme Convenable…</p>
<p>N’avait-on donc pas d’homme Convenable auparavant ? Aussitôt ressurgit la
question du Progrès, avec le risque de nous enfermer dans l’horizon civilisé. Je
décompose la question :</p>
<p>- Nous n’avons pas, actuellement, l’homme convenable pour relever le défi que
nous lance <b>notre</b> époque. Et nous <b>nous</b> faisons homme convenable, nous
dépouillons le vieil homme, en même temps que nous empoignons la question sociale posée, et que nous la résolvons. C’est en forgeant qu’on devient forgerons. Aide-toi, le
<img class="img-in-text-left" src="img/forge.jpg"/>ciel t’aidera. C’est le singe lui-même qui a fait de ses quatre membres des pieds et
mains terminant des jambes et des bras. Car le singe initial n’était pas vraiment
“quadrumane”, ses mains finales n’étant plus les mêmes qu’au début. La situation
“immédiate” est la suivante : contre la société de Mort qu’il me faut à tout prix
anéantir (et pas réformer ou transformer), je <b>me fais</b> en quelque sorte ex-nihilo,
homme Convenable. Il s’agit d’une “création”, quelque chose de tout à fait neuf. D’où
vient que le système est anti-social, en quoi consistait le vieil homme en voie
d’abolition totale, sont des questions hors sujet dans la mesure où il ne s’agit que de
créer-anéantir. Si bien qu’une quelconque humanité VIVANTE autre que celle
Convenable en cours de production n’entre pas dans mes préoccupations. <b>Ni
progrès ni régrès</b> ne sont concernés dans ma tâche. Je ne me souci ni d’un Paradis
Céleste après ma mort, ni d’un Paradis Terrestre avant ma naissance. <b>J’assume ma
vie</b>. Et s’il m’arrive une information de sociétés Parentale et Policée VIVANTES
d’autrefois, je dis qu’elles étaient <b>en tout point comme moi</b> : elles répondaient
positivement à la situation qui était la leur. Et je ne sais même si, “plus tard”, il n’y
aura pas encore plein d’autres sociétés “convenables”. En en restant là, « rien de
nouveau sous le soleil » ! (Qo, 1 : 9).  »</p>



