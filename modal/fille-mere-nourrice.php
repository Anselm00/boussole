			  <p>« Chez nous, il est un maître incontournable du judaïsme : Ernest GUGENHEIM (1916-1977), 
				  qui fut directeur du Séminaire israélite de France. En 1961, il publie “Le judaïsme dans la vie quotidienne” 
				  (toujours réédité), préfacé par le Grand Rabbin Kaplan. En 1982, on édite un deuxième volume à titre posthume 
				  (toujours réédité), préfacé par le Grand Rabbin Sirat. Et il fut lui-même Grand Rabbin. Notre maître pose 
				  la question : est-ce que les juifs sont phallocrates ? On pourrait s’y tromper, avoue-t-il, puisque les mâles 
				  chez nous doivent remercier YHOAH chaque jour de ne pas les avoir faits “Étranger, Esclave, ou Femelle” 
				  (Étranger doit être compris comme “de sang délaissé” dans le monde ; et Esclave comme ceux de ces déjetés 
				  mis sous la coupe du peuple élu). Alors, cette femelle juive ? Pourquoi la mêler à l’étranger et à l’esclave ? 
				  Et puis, elle n’est pas marquée par le signe de l’Alliance (circoncision) ; elle ne peut être ni juge ni témoin, 
				  ni rabbin, ni reine ; à la synagogue, elle est reléguée dans la galerie ou dissimulée par un rideau au rez-de-chaussée, 
				  ne participe pas à l’office ; on oublie de lui imposer pratiquement tous les Commandements Positifs (246 sur 248, 
				  face aux 365 Négatifs ; total 613). Tout cela est trompeur, dit le rabbin, et même le contraire de la vérité. 
				  “La Tora fut d’abord donnée aux Juives”. La preuve ? YHOAH dit à Moïse : “Tu diras à la Maison de Jacob, puis 
				  répèteras aux Fils d’Israël…”. Or, “Maison de Jacob” veut dire les Femmes. Et puis : en Israël, une fille-mère 
				  est honorée, puisqu’elle met bas ; et l’épouse peut avoir l’initiative du divorce si son mari exerce un métier 
				  “malpropre” (impur). Sa place à la synagogue ? C’est sans importance, parce que le culte juif est avant tout familial. 
				  Et la circoncision, les Commandements Positifs ? “La Juive n’en a pas besoin, parce qu’elle porte Biologiquement 
				  son appartenance au Peuple-Bijou, et elle a une connaissance Intuitive, Directe, de la Loi, qui fait défaut au mâle juif”. 
				  Vous voyez bien que les cervelles “grecques” comprennent complètement de travers l’identité et le mode de vie juifs… 
				  Comme le “monothéisme strict” est méconnu !</p>
