			  <h4>Existence</h4>
			  
			  <p>« Que se passe-t-il au départ de la mentalité parentale ? 
				  Il y a d’abord une ethnie dont <b>l’Ancêtre (Abraham)</b> 
				  atteste qu’elle groupe les “vrais hommes”, ethnie choisie par 
				  la Puissance Matérielle, par Vie-Permanente (“Moi Existence”, 
				  “Moi change pas” – Exode 3 : 14), parmi les branches d’Adam, 
				  les Noachides. Pourquoi cette élection ? Parce que l’ethnie 
				  ne peut subsister sans s’attribuer la détention de la Loi 
				  Naturelle exerçant son hégémonie sur la communauté humaine, 
				  c’est-à-dire la secrète liaison entre Vie-Permanente et 
				  Vie-Intermittente au monde présent. Et ce que manifeste 
				  le Pacte de Sang privilégié, noué entre Vie-Permanente et 
				  les vrais hommes, est le rite de <b>Circoncision</b> des mâles de 
				  la communauté (y compris les esclaves domestiques), en même 
				  temps que la descendance pure garantie par la <b>filiation Maternelle</b>. 
				  L’élection ethnique, entretenue par le cerveau collectif de la 
				  communauté, a une portée cosmique : c’est la responsabilité 
				  d’obéissance stricte aux Commandements vraiment Naturels qui 
				  doivent régler Vie-Intermittente, faute de quoi la Catastrophe 
				  matérielle du monde s’ensuivrait. »</p>



