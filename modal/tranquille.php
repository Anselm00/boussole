				  <p>« On est Tranquille dans le Comm-Anar parce que la Science du Neuf – qui suppose
la disparition des <b>Ingénieurs</b> – nous fait faire la “révolution culturelle”
constamment, dans tous les actes de tout le monde.</p>
<p>L’<b>Hégémonisme</b> réactionnaire de la société parentale, et l’Hégémonisme
révolutionnaire de la société politique menaient à un Drame final terrible, tournant
peu après à une Tragédie barbare épouvantable. Or, c’était que depuis leur berceau
ces deux humanités n’étaient PAS tranquilles ; ce qui était confirmé par le fait que
dans leur RêRêve ou leur Idéal elles entretenaient l’espoir d’autre chose que ce qu’elles
vivaient*. »</p>

