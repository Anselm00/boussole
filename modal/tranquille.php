			  <h4>Tranquille</h4>
			  
			  <p>« Soyons “soufis” ; des exaltés tranquilles !</p>
<p>Nous, Réalistes, en faisant notre devoir, nous n’avons décidé que de bien remplir notre vie ; sans attendre d’autre récompense parce que rien de mieux ne peut nous être offert.</p>
<p>Que peut bien vouloir dire “bien remplir sa vie”, sinon la concevoir comme indissociable du bien du peuple, de l’humanité et du monde ?</p>
<p>L’Histoire est notre justification absolue, et le Peuple son agent invincible ; c’est tout ce qu’il faut pour être exaltés-tranquilles.</p>
<p>Nous ne sommes rien, mais la Caste Noire, même sans Église Réaliste, n’a jamais dormi que d’un oeil depuis 150 ans, tant la Masse l’épouvante ; que sera-ce avec un Front, et puis un grand Peuple ?</p>
<p>Nous, ce “rien”, y aurons été pour quelque chose ! »</p></p>
