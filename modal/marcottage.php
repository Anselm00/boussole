			  <h4>Marcottage</h4>
			  
			  <p>« • Chers camarades, vous connaissez à présent la teneur de mon Message. Je vous vois pour le moins étonnés, ce qui est tout à fait compréhensible. Peut-être vous aiderai-je en offrant à votre méditation une parabole, celle du marcottage. Voici de quoi il s’agit :
Vous savez peut-être que, quand un arbre approche du moment où il va donner ses derniers fruits, tout jardinier prévoyant se livre à l’opération qui porte ce même nom : 
marcottage. Aux citadins, j’explique comment cela se passe. Notre paysan se saisit d’un rameau vigoureux, le couche en terre sur une partie de sa longueur, et en relève l’extrémité libre contre un tuteur. À ce moment, l’autre extrémité de la branche reste attachée au tronc, c’est-à-dire à la souche-mère. Par la suite, le jardinier prend grand soin de son ouvrage : l’exposition à la lumière, l’arrosage, il veille à l’engrais éventuel, aux agressions d’insectes, etc. Ceci assuré, la partie enterrée du rameau parviendra à prendre racine, dans le même temps où s’opère la séparation de la marcotte d’avec la souche-sevrage. Et voilà comment croît avec succès un tout nouvel arbre prometteur des plus beaux fruits jamais vus.
Par cette image du marcottage, vous pouvez comprendre comment l’arbre du Rapport de la Réalité pourra naître de la Substance de l’Esprit-Père, dans le même mouvement où nous le rappellerons à la Substance de la Matière-Mère. Vous pourrez comprendre à la suite de quel laborieux sevrage, les deux Substances antérieures doivent se conserver et s’effacer à la fois pour se présenter comme la Réalité Unique. Toutes les voies pour ce faire nous sont visiblement préparées ; que manque-t-il seulement ? Nous-mêmes ! Que se lève l’équipe des Jardiniers ! »</p>
