			  <h4>Principe</h4>
			  
			  <p> « Les Premiers Principes de la pensée Rationnelle, comme l’expression l’indique, se
donnaient comme les règles inconditionnées, précédant toute expérience, toute
pensée en acte, et gouvernant nécessairement la pensée vivante. Les Principes formaient la frontière extrême de la Raison, chacun faisant écho aux Facultés
élémentaires de la Raison : à la puissance de l’Intuition était attachée la règle de
l’Identité logique ; et à la puissance de l’Entendement était attachée la règle de
l’Unité mathématique. Mais il est à souligner, comme le schéma ci-contre le figure,
que c’est parce que chaque Faculté butait sur le Principe issu de la Faculté opposée,
que le saut dans la Foi s’imposait dans les deux cas. Ainsi, l’Intuition se trouvait
déconcertée par le principe Mathématique, et l’Entendement déconcerté par le
principe Logique. C’est ce qui explique, par exemple, que des Idéalistes comme
Pythagore, Nicolas de Cuse, ou Leibniz, sont “obsédés” par les Mathématiques. »</p>

