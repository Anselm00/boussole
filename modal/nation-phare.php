			  <h4>Nation Phare</h4>
			  
			  <p>« Il est vrai que <b>pratiquement</b>, dans la civilisation, l’humain particulier ne s’affirme qu’au travers de la <b>Cellule</b> civile qu’est le Ménage, et que l’humanité générale ne s’exprime que par le biais du <b>Corps</b> politique qu’est la Nation ; mais ces limites ne viennent que des contraintes matérielles particulières que nous connaissons ici-bas. Ainsi s’explique la combinaison qui nous est imposée : de <b>Liberté</b> par la procréation Domestique et la production par l’Entreprise privée, et de <b>Monopole</b> par la souveraineté dans la Patrie commune et par l’Administration publique. »</p>
			  <p>__________________________</p>

<p>Voir <a href="https://www.eglise-realiste.org/pdf/Mentalite_Religieuse_ed.pdf" target="_blank"> "La Mentalité Religieuse"</a>, p.5 - Talib Freddy - mars 2004 - Eglise Réaliste Mondiale - Editions de l'Evidence - 2010</p>
			  
<p>__________________________</p>


			  <p><b>« Finalement s’établirent les Nations solidement constituées, c’est-à-dire
un Marché cohérent, que limitent seulement des Frontières dites
“naturelles”, en fait de simples bornes géographiques qui sont l’occasion
de stabiliser la compétition des nations diverses. Et c’est leur rude
émulation respective que les Nations modernes désignent comme le
Genre Humain.</b></p>

<p>Le marché cohérent qui forme la base de la Nation trouve son complément
adéquat dans l’institution de la monarchie Constitutionnelle, représentative. La
superstructure idéologique qui complète l’édifice consiste dans un <i>“gallicanisme”</i>
déiste, dont les prémisses françaises remontent jusqu’à la Pragmatique de St Louis.
Pour finir, l’ensemble national organique est le moule de la langue <i>“nationale”</i>,
ciment des <i>“patriotes”</i>, instrument du <i>“génie scientifique”</i> national et du
rayonnement <i>“universel”</i> des <i>“lettres et des arts”</i> de la Nation. La part de <i>“hasard”</i>
dans la découverte nationale ressort fortement, par exemple, dans le rôle décisif
qu’eut la subordination de la langue d’Oc, consécutive à la croisade contre les
Albigeois, dès le 13ème siècle.</p>

<p>Le rationalisme décadent qui forme la mentalité dominante actuelle,
profondément hostile à toute forme de pensée historique, même sous un
travestissement spiritualiste, étale son impuissance académique à rendre compte de
la réalité nationale. Ainsi s’alignent des versions aussi fantaisistes qu’elles sont
perverses :</p>
<p>• <i>“Les Gaulois ne sont pas nos ancêtres”</i>, c’est au cours des <i>“10 000 siècles de
préhistoire”</i> que <i>“la France a modelé son profil”</i> (M. Ambroise Rendu) ;</p>
<p>• La nation moderne <i>“n’aura été qu’une reconstitution”</i> de la pré-France des
Gaulois, <i>“blessée”</i> par les partages Francs (Bouvier-Ajam) ;</p>
<p>• Un <i>“fond racial”</i> assure la continuité de la Gaule à la France (F. Bourdier) ;</p>
<p>• <i>“Les titres de continuité d’une nation… apparaissent clairement dans la
permanence du peuplement”</i>, lequel manifeste son <i>“immémoriale communion avec
les forces du terroir”</i> (L. Romier).</p>
<p>À tout ce bavardage universitaire mesquin, une seule chose à rétorquer : bien
malin qui, à une époque encore récente, aurait pu mettre sa main à couper que la
Bretagne, le Comtat-Venaissin ou la Corse, seraient partie constituante de ce qu’on
appela plus tard la France. À l’inverse, n’y avait-il pas gros à parier que la Wallonie, la
Rhénanie, ou même la Louisiane et le Québec, en seraient indissociables ?</p>
<p>Faute d’histoire réelle, c’est à l’histoire mythique que nous adhérerions avec
enthousiasme ; par exemple à la version de <b>J. Le Maire des Belges</b> (1509) : les
Français remontent en droite ligne à Noé. Francus, fils d’Hector, descend de Celte,
père de Galatée ; la même Galatée, ayant épousé Hercules de Lybie, ancêtre de
Dardanus – le fondateur de Troie et frère d’Osiris ; les deux frères sont issus de Cam,
né avant le Déluge… Un tel peuple a donc, par délégation divine spéciale, mission de
fonder rien moins que “la Concorde du genre humain” définitive… »</p>



