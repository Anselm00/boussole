			  <h4>Esprit</h4>
			  
			  <p>« 1- Toute la mentalité Civilisée tient au Préjugé Spiritualiste : <b>l’essence
authentique du Monde consiste dans l’Esprit</b>. Par suite, la Matière en laquelle
la Création s’imprime, doit s’aborder comme “privation” d’être au sens
philosophique. Cela veut dire que la Matière (en Au-delà comme en Ici-bas, quoique
sur des modes différents), est PROVOCATION à glorifier l’esprit, obstacle et moyen à
la fois de faire ressortir l’hégémonie inconditionnelle de ce dernier.</p>

<p>Le Préjugé Spiritualiste s’exprime positivement dans le Dogme Fidéiste : Dieu et
l’Âme. Il se traduit par l’exaltation du Travail humain, du fait qu’en l’Humanité réside
l’esprit Actif manifeste, face à l’esprit Passif manifeste qui se donne dans la Nature.
L’Homme civilisé, par son Travail, doit se porter garant de l’Honneur à rendre à
l’Esprit Hégémonique au sein de la Réalité. Le Travail humain, la Foi étant admise, se
place spontanément sous le “doux joug” (Matt. II-30) du principe de Raison.</p>
<p>2- La mentalité Spiritualiste doit bien en arriver, non sans mal cependant, à
proclamer de la manière la plus pure que l’Esprit est Un, que par suite la Raison est
Une, de même que le Principe qui gouverne cette dernière. C’est ce à quoi parvient la
Métaphysique occidentale Moderne, et tout particulièrement la Religion Parfaite de
Kant. Une des conséquences de la Métaphysique Pure, c’est que les Hommes sont
“évidemment” les seules “créatures spirituelles” possibles, les enfants exclusifs de
Dieu, et donc les seuls habitants admissibles de l’Au-delà (Ciel et Enfer), d’où les
Anges disparaissent définitivement.</p>
<p>3- La Raison est le contraire direct de la Foi, dans la mesure où elle “oublie” Dieu
consciemment et se tourne vers le Monde (la Création). De ce point de vue, la Raison
s’insère dans le Travail, dont elle se donne seulement comme la Condition ultime et le
Couronnement.</p>
<p>Dès ce moment cependant, la Raison se dédouble Hiérarchiquement. Il y a :
<p>- Le <b>Principe de raison</b> même, “évident-indémontrable”, qui préside à tout
Travail. Ce Principe s’avérera double à son tour : Identité-Unité.</p>
<p>- La Catégorie subséquente au Principe, commandant le premier Travail explicite,
celui de la Science Pure. La <b>Catégorie de Vérité</b> gouverne cette sphère où la science
“se donne ses propres objets”, le domaine Logique-Mathématique, de Jugement-
Mesure. La Catégorie s’avère elle aussi double : Substance-Cause. (Ultérieurement,
on a : Substance = Forme/Matière ; Cause = Finale/Efficiente). »</p>
