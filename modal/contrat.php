				  <p style="text-align:center;"><b>ANNEXES</b>
				
				  <p style="text-align:center;"><b>DROIT DES GENS SOCIAL</b></p>
<p>Aucune issue ne s’offre au peuple mondial si on ne commence par combattre
résolument l’excroissance du “Droit du Travail”.</p>
<p>En effet, ce chancre du Droit du Travail, que des bureaucrates prostrés et insolents
à la fois, nous font passer pour l’expression des “acquis sociaux”, n’a de sens que
relativement à l’époque de décadence civilisée finale qui s’est ouverte après 1848.
Cette époque “Contemporaine”, à laquelle nous appartenons encore, est celle du
capitalisme parasitaire et de la “démocratie” dictatoriale. Le Droit du Travail n’est
rien d’autre qu’un élément constitutif essentiel de cet état de décomposition sociale
ultime de la civilisation, de l’ordre qui caractérise en même temps la fin de la
préhistoire humaine. Le Droit du Travail proclame l’acceptation de la décadence et la
volonté de l’“aménager”, de l’accompagner dans son approfondissement incessant.
Cette apologie objective du capitalisme parasitaire montre seulement le degré
d’avilissement des “chefs ouvriers” qui s’en font les laudateurs ; elle est
simultanément le pire affront que puisse recevoir le monde salarié, sur lequel repose
tout l’espoir de la République Sociale Universelle.</p>
<p>S’affranchir du Droit du Travail prisonnier de la décadence civilisée, c’est
développer une conception saine et digne du Droit Social, ordonné à l’impératif et à
l’exigence de rompre avec l’ordre préhistorique aujourd’hui à l’agonie dans lequel le
travail est borné par le strict besoin de “survivre”, et qui menace à présent le genre
humain d’autodestruction.</p>
<p>Ce que signifie le Droit Social ainsi envisagé, on peut le montrer par référence au
vieux “droit des gens”, en substituant simplement aux États politiques les Classes
sociales. Une telle “traduction” du droit des gens dans la langue sociale, faite à partir
du Précis de Brentano-Sorel de 1877, nous donne un parfait Abrégé de Droit Social.
Le texte qui suit en fournit la vérification.</p>
<p style="text-align:center;">________</p>
				
				  <p><b> 1- Droit négatif</b></p>

				<p>Les Individus (familles) rassemblés dans l’État ne sont pas en rapport
antagonique ; et pourtant, en tant qu’ils appartiennent tous à un ordre polarisé en
<b>Classes</b> – où les exploiteurs dominent les exploités – des intérêts et des opinions
inconciliables gouvernent leur existence.</p>

<p>“Les (Classes) constituées sont <b>souveraines</b>. (Elles) ne sont égales qu’en théorie.
Ces mots (classes) souveraines désignent des (classes) parfaitement inégales en droits
et en forces. Lorsque les (classes) souveraines contractent entre (elles des
obligations), ce contrat, encore que résultant d’une égalité abstraite, démontre
presque toujours l’inégalité réelle des contractants. En dehors de l’application qui en
est faite, l’égalité des (classes) n’est qu’un mot sans portée. Entre (classes) il n’y a
point de lois communes ; l’égalité n’a d’autre fondement et d’autre garantie que les
moeurs des hommes”.</p>
<p>“Les (classes) ne sont responsables que devant (elles)-mêmes. L’idée d’une
responsabilité réciproque est contradictoire avec l’idée de souveraineté. Les (classes)
sont seules juges de leur responsabilité ; (elles) sont toujours maîtresses de sortir du
régime de droit qui est la paix, pour entrer dans celui de la force et de la nécessité qui
est la guerre. Tels sont les principes ; ils ressortent du caractère même des (classes).
Mais si les (classes) prenaient ces principes à la lettre, (elles) seraient
perpétuellement en guerre. (Elles) ont senti la nécessité de tempérer dans la pratique
l’extrême rigueur de ces effets de la souveraineté. (Chacune) a constaté qu’(elle)
devait, en certaines circonstances, agir comme si (elles) étaient responsables. Il s’est
ainsi établi des pratiques tendant à réparer pacifiquement les violations du droit.
Mais ce n’est pas un droit qu’(elles) se concèdent, car leur souveraineté en serait
altérée, c’est un devoir qu’(elles) s’imposent librement”.</p>
<p>“Il n’y a pas de <b>droit contre le droit</b> ; et la souveraineté des (classes) est un
principe essentiel du droit des gens (social)”.</p>
<p>“Un <b>Code</b> du droit des gens (social) est une <b>oeuvre paradoxale</b>. Les (classes) ne
peuvent y souscrire sans abdiquer leur souveraineté (légitime historique). Un tel code
serait un grand contrat conclu entre les (classes) ; si elles y souscrivaient, elles
contracteraient des engagements que la force des choses les contraindrait, tôt ou tard,
à violer. Il y a un objet que les traités et les conventions ne peuvent atteindre : c’est la
paix perpétuelle (des classes antagoniques). En dehors de leurs conclusions dans des
conditions déterminées, ces traités aboutissent fatalement à l’hégémonie des (classes)
les plus fortes sur les plus faibles. Quelques (idéologues de parti) ont pensé qu’en
développant (la négociation permanente, la législation sociale et la cogestion), l’on se
rapprochait indéfiniment de la paix (sociale) perpétuelle. Il est facile de démontrer
que ces systèmes aboutissent à une contradiction”.</p>
“Il y a une <b>relation constante</b> et nécessaire de l’état de paix (sociale) avec l’état
de guerre (sociale). Les causes de la guerre (sociale) se forment et se développent
pendant la paix (sociale). Entre l’état de paix, qui est la conciliation des intérêts, et
l’état de guerre, qui est la lutte des forces, il y a un état intermédiaire qui est
l’antagonisme des intérêts : la paix n’est qu’apparente, la guerre existe à l’état latent ; 
c’est alors que les causes de la guerre (sociale) se découvrent, et c’est là qu’il faut les
rechercher”.</p>
<p>“<b>Toute affaire</b> où les (classes) jugent leurs intérêts engagés peut devenir entre
elles une cause de guerre ; depuis la nécessité où croit être (une classe) de prévenir
par une attaque le développement d’une puissance qu’elle considère comme
menaçante pour sa sécurité, jusqu’à la nécessité où croit être (l’autre classe)
d’accroître sa puissance et d’étendre (son ascendant)”.</p>
<p>“Le développement de l’éducation a multiplié les causes de conflit, (créé) un
puissant instrument de propagation, provoqué des luttes que les progrès de la science
rendent plus terribles. La science, en multipliant et en facilitant les communications,
propage en même temps les haines qui divisent les hommes : leurs passions
s’échauffent plus vite et éclatent avec plus de violence”.</p>
<p>“La guerre (sociale) est non seulement une conséquence de la paix (sociale) mais,
dans l’état de la civilisation contemporaine, elle est <b>inévitable”</b>.
“Le droit des gens (social) est une sorte d’entente tacite dans la <b>manière
d’employer la force”</b>.</p>
<p>“C’est <b>quand les intérêts se forment</b>, qu’il est le plus facile de les concilier.
C’est dans les oppositions d’intérêts qu’il faut deviner les conflits futurs et qu’il est le
plus aisé de les prévenir. C’est quand les conflits se préparent qu’il est le moins
difficile d’y donner une solution pacifique”.</p>

<p><b>2- La paix sociale</b></p>
<p>“Malgré tant de motifs de désordres que provoque (l’existence de classes
antagoniques), des règles générales de conduite ont été posées. Ces règles ne
prennent une valeur positive qu’autant qu’elles tiennent compte des conditions dans
lesquelles les (classes) sont placées les unes par rapport aux autres. La première de
ces règles, la plus générale, c’est qu’en temps de paix les (classes) <b>se doivent
réciproquement les mêmes égards</b>. Ce n’est point là un principe abstrait, ce
n’est que la définition d’un fait”.</p>
<p>“Les devoirs/droits, les intérêts respectifs des (hommes) et des (classes) <b>se
rencontrent</b> souvent ; les (classes) se sont efforcées de les concilier : elles l’ont fait
en vertu de leur souveraineté, et de là est résulté le droit des gens. Elles procèdent de
ce fait que les (classes) sont souveraines, et elles reposent toutes sur le respect des
obligations réciproques. Le principe du droit des gens en temps de paix est le respect
des devoirs/droits, des intérêts respectifs”.</p>

<p>“Les droits que (les Partis, à la tête des classes) ont déduits du système de
l’équilibre, de la <b>balance des forces</b>, se fondent sur une interprétation arbitraire
des faits historiques, sur une conception fausse des rapports des (classes). Si ce
système avait eu un fondement réel et si les (classes) en avaient fait le principe de leur
politique, il n’aurait pas eu d’autre résultat que de condamner le monde à
l’immobilité, ce qui est contraire à la nature des choses. Le système de l’équilibre ne
changea rien aux faits. Les (classes) continuèrent d’agir d’après les pratiques
anciennes : (elles) se firent la guerre par calcul ou par nécessité, et non en vertu d’un
droit nouveau. Ce système ne peut être qu’une utopie nuisible (à la classe) faible et un
sophisme favorable (à la classe) forte”.</p>
<p>“Les <b>traités</b> et conventions sont les engagements de (classe) par excellence. Ils
expriment toujours un rapport de puissance. Tout traité est une expression des
rapports qui existent au moment où il est conclu, entre les forces morales et
matérielles des (classes) qui le concluent ; il ne signifie rien de plus. Et il ne vaut que
par l’exactitude avec laquelle les négociateurs ont mesuré ce rapport et l’ont exprimé.
Tous les traités ne gardent leur valeur qu’autant que subsistent les causes historiques
qui les ont produits. (Chaque partie) est seule juge de la conduite à tenir. Elle exécute
le devoir que lui impose le traité, dans la mesure que lui conseillent ses intérêts. Il ne
s’ensuit pas qu’elle puisse violer les principes du droit des gens en temps de paix
(sociale), se soustraire entièrement à l’exécution des clauses du traité. Le fait est
qu’elle ne le peut point, et c’est parce qu’elle l’a compris (qu’en nombre de cas, les
parties) ont cherché par une distinction subtile à signer un traité, sans garantir
réellement l’exécution”.</p>
<p>“Les traités encore qu’ils soient qualifiés de perpétuels, s’affaiblissent, tombent et
disparaissent. Les rapports étant changés, le traité a contre lui la force des choses, et
sa raison d’être disparaît. C’est en vain qu’on essaierait de le maintenir. Vouloir qu’il
en soit autrement, c’est méconnaître les lois de l’histoire”.</p>

<p><b>3- La guerre sociale</b></p>
<p>“La guerre (sociale) procède de la contradiction (antagonique) des intérêts
respectifs (des classes)”.</p>
<p>“Les causes de la guerre échappent à l’action et même le plus souvent aux calculs
(des chefs de partis) ; elles disparaissent, renaissent, se transforment suivant le
progrès ou la décadence des (périodes civilisées). Les traités en sont un effet, ils ne les
détruisent pas”.</p>

<p>“Il ne faut pas confondre la guerre (sociale) avec la légitime défense (sociale). La
légitime défense (sociale) peut être un devoir pour (la classe) envers (ses membres) ;
mais la légitime défense n’est pas un devoir (entre classes différentes). Il s’ensuit que
la guerre (sociale) n’est pas un droit pour les (classes). Elle ne résulte d’aucune des
obligations réciproques des (classes) les unes envers les autres. Elle éclate parce que
les (classes) ne peuvent s’accorder sur les droits qu’elles s’attribuent
(unilatéralement), et elle assure nécessairement le succès de la (classe) la plus forte.
Dire qu’elle est un droit pour les (classes) équivaut à dire qu’il n’y a entre les (classes)
d’autre droit que la force”.</p>
<p>“Ce n’est que par un abus de mots qu’on a pu qualifier les guerres de justes ou
injustes. Les guerres ne commencent que là où cesse toute notion de droit et de
justice réciproques. L’histoire prouve que les (hommes et les classes) qui se sont
combattus ont toujours eu, avant d’en arriver à la lutte armée, des torts les uns à
l’égard des autres. La guerre (sociale) est l’acte politique par lequel les (classes), ne
pouvant concilier ce qu’elles croient être leurs devoirs/droits, leurs intérêts,
recourent à la lutte armée, et demandent à cette lutte de décider laquelle d’entre elle,
étant la plus forte, pourra en raison de sa force imposer sa volonté (à l’autre)”.
<p>“La guerre n’a d’autre objet que d’établir <b>le droit du plus fort</b>, c’est-à-dire de
contraindre (la classe) la plus faible à se soumettre aux prétentions du vainqueur”.</p>
<p>“La guerre (sociale), n’a d’autre règle que <b>la nécessité</b>. Les coutumes de la guerre
n’ont pas d’autre fondement. Et (la classe) qui fait la guerre est toujours juge
souverain de l’objet, des nécessités et des coutumes de la guerre. Chaque (classe) en
use selon sa puissance et ses intérêts. Elle est juge et partie à la fois. C’est selon le
degré de civilisation et d’intelligence politique auquel elle est parvenue qu’elle juge
ces faits et dirige sa conduite”.</p>
<p>“Les nécessités de la guerre sont toujours <b>(antagoniques)</b>, elles s’imposent sous
une forme (diamétralement) opposée à (l’exploiteur et l’exploité)”.
“Les coutumes de la guerre <b>prennent en compte</b> l’atmosphère qui sera
engendrée au retour de la paix, les rancunes profondes et germes de guerres
nouvelles (qui peuvent être répandus)”.</p>
<p>“Constater que la guerre (sociale) est une oeuvre <b>sanglante et barbare</b>, c’est
définir le caractère même de la guerre (sociale). La guerre (sociale) arrête la
civilisation et ramène les (hommes) au joug de la force et de la nécessité ; elle met en
question l’existence des (peuples). On n’arrive point à civiliser la guerre (sociale),
l’existence des (hommes) s’y joue”.</p>
<p>“Les opérations de guerre se règleront d’une manière plus conforme à l’objet de la
guerre. Ce n’est pas en codifiant les prétendues lois de la guerre (sociale) que les
(classes) y parviendront, car la guerre est un fait et elle ne connaît pas d’autres lois 
que la nécessité. Le développement d’une civilisation commune est la condition de
tout <b>progrès dans les coutumes de guerre</b>. C’est l’honneur (des chefs de Parti)
de soutenir et d’encourager les (hommes) dans ce progrès. Aucun effort vers ce grand
objet n’est indifférent ou inutile. Les (hommes) font leur droit des gens en temps de
guerre selon leur état de civilisation”.</p>
<p>“Les abus et les excès sont à peu près inévitables dans la guerre (sociale). Il n’y a de
ressource contre les excès que dans la guerre même, et cette ressource consiste
uniquement à opposer la violence à la violence. C’est ce qu’on appelle les représailles.
La (classe) qui manque aux coutumes de la guerre ou en abuse le fait à ses risques et
périls : elle s’expose à voir ses adversaires agir de même à son égard. Les représailles
sont souvent une nécessité pour l’adversaire. C’est dans la manière dont elles
exercent les <b>représailles</b> que les (classes) découvrent leur puissance et leur faiblesse
réelles. Dans les armées bien ordonnées, les chefs sont obligés de commander les
représailles à des combattants qui ne les exécutent qu’à regret. Il n’y a qu’un moyen
de les rendre inutiles : c’est de précipiter la fin de la guerre”.</p>
<p>“Il n’y a point de limites aux représailles, parce qu’il n’y a point de limites aux abus
de la force. La dévastation, la destruction des édifices, la déclaration de ne point faire
de quartier, l’incendie, le pillage, la prise d’otages, la mort sont des représailles. Les
représailles qui sont motivées par une violation des coutumes de la guerre (sociale)
aboutissent à la suppression des coutumes. La substitution de la guerre contre les
individus à la guerre entre les (classes) en est la conséquence. Elles peuvent amener
les (hommes) modernes à se traiter comme le faisaient les (hommes) antiques, qui
massacraient les prisonniers, réduisaient les populations en esclavage et ravageaient
les (propriétés) ennemies. À ce degré, il résulte des ébranlements profonds qui
persistent bien longtemps après la guerre (sociale), des haines que le temps même
n’efface pas toujours”.</p>
<p>“<b>L’ultimatum</b> est une proposition de guerre”.</p>
<p>“Les chefs (de parti des classes belligérantes) sont dans la nécessité de <b>se
renseigner</b> le mieux possible sur les forces et les mouvements de (la classe)
ennemie. Ils sont également dans la nécessité de réprimer énergiquement les efforts
faits par (la classe) ennemie pour se renseigner. Le fait de l’espionnage suppose la
clandestinité”.</p>
<p>“Les (classes) qui se font la guerre <b>ne peuvent détruire</b>, ni la souveraineté des
(classes), ni la dépendance mutuelle des (hommes). Elles ne peuvent davantage
supprimer la civilisation accomplie, les moeurs acquises, les notions intellectuelles
développées par les (hommes)”.</p>
<p>“La force, dont la guerre (sociale) assure le triomphe, n’est pas la force aveugle
des éléments de la nature. Dans la guerre (sociale), les forces aveugles de la nature sont mises au service de l’intelligence des hommes. Ce sont les (classes) qui luttent
entre elles ; ces (classes) représentent les efforts accumulés de l’intelligence et de la
moralité humaines. C’est à travers ces luttes que les (hommes) ont développé les
éléments de la civilisation, ou les germes de décadence qu’ils portaient en euxmêmes”.
<p>“Les forces militaires (aux prises dans la guerre sociale) n’ont rien de commun
avec les <b>pirates et les bandits</b>”.</p>
<p><b>“Pour rester humaine</b>, la guerre (sociale) doit être savante, rapide, décisive. La
coutume dans les opérations de guerre (sociale) consiste à diriger les opérations
contre les forces organisées de la (classe) ennemie, et non contre ses membres non
armés. Les (classes) doivent adoucir autant que possible pour les (hommes) les
conséquences de la guerre. La coutume de la guerre (sociale) interdit les cruautés
inutiles”.</p>
<p>“<b>Les armées</b>, qui sont l’honneur des (classes) pendant la guerre, deviennent les
<b>vraies fondatrices et garantes de la paix</b> (sociale) ; la force dépouille son
caractère brutal et insolent”.</p>

<p><b>4- Illusions</b></p>
<p>“Tous les moyens de maintenir la paix entre les (classes antagoniques) traités,
entrevues de (chefs de partis), arbitrage, conférences et congrès, armement et
désarmement, n’ont de valeur que celle qu’ils puisent dans l’intelligence des (chefs de
partis) qui les mettent en pratique :</p>
<p>• S’ils croient la paix préférable à la guerre (sociale), le droit des gens (social) leur
fournit tous les moyens de la maintenir ;</p>
<p>• S’ils jugent la guerre (sociale) nécessaire, se croient capables de l’entreprendre
avec succès, aucune institution ne peut les empêcher de combattre. Dans ce cas, il n’y
a qu’un recours contre la guerre : c’est la guerre elle-même”.</p>
<p>“L’utopie de la paix perpétuelle se résume en l’établissement d’un Tribunal
suprême, formé de représentants des (Classes) souveraines, et destiné à régler tous
les différents (sociaux). Cependant :</p>
<p>• Les coutumes du droit des gens (social) n’existent que parce que les (classes) les
appliquent, et les (classes) ne les appliquent que parce qu’(elles) sont souveraines,
c’est-à-dire libres de ne pas les appliquer. C’est pourquoi il est impossible de faire des
coutumes du droit des gens (social) un Code qui serait expressément reconnu par les
(classes) et formerait dans tous les cas leur loi commune : ce Code serait la négation 
de l’indépendance des (classes), et elles ne pourraient le reconnaître sans abdiquer
leur souveraineté.</p>
<p>• D’autre part, en supposant ce tribunal constitué, pour que ses sentences eussent
une sanction, il faudrait que les juges disposassent d’une force exécutoire. Cette force
ne pourrait être constituée que par les (classes), et en la constituant, (elles)
établiraient encore un pouvoir supérieur à leur pouvoir propre, ce qui est impossible
sans abdiquer en tout ou en partie leur souveraineté”.</p>
<p>“Combien est chimérique la conception de ceux qui ont cherché dans les
Arbitrages un moyen détourné d’introduire dans le droit des gens (social) le
système de la paix perpétuelle. C’est méconnaître absolument le principe de
l’arbitrage, que de vouloir en faire une institution permanente. L’arbitrage n’est
possible que parce qu’il est un acte de souveraineté ; le rendre “obligatoire” pour les
(classes), c’est lui enlever sa raison d’être et son efficacité. Il est impossible de forcer
les (classes) à recourir à l’arbitrage lorsqu’elles ne le veulent point : en ce cas la guerre
éclaterait sur la question (même) de l’arbitrage ou sur le choix des arbitres. Les
(classes) ne peuvent pas même s’engager d’avance à soumettre à l’arbitrage tous leurs
différents, car il en est, et ce sont les plus graves, que l’arbitrage ne saurait trancher
pacifiquement”.</p>
<p>“Les (dirigeants des classes) songèrent à faire des Congrès une institution destinée
à prévenir et à régler tous les différents entre (classes). C’était revenir par une voie
détournée à l’utopie de la paix perpétuelle. La guerre éclate parce que les (classes) se
croient capables de soutenir leurs prétentions par la force. Lorsqu’(elles) sont
décidées à entreprendre une guerre, qu’(elles) la jugent utile, opportune ou
nécessaire, un congrès ne peut les obliger à y renoncer qu’en les menaçant d’une force
supérieure à celle qu’elles possèdent. Si les (classes) cèdent devant cette menace,
(elles) cèdent devant la force, et le droit que fait prévaloir le congrès est le droit du
plus fort”.</p>
<p>“Après avoir prouvé la vanité ou le danger de tous les systèmes destinés à assurer
la paix, les (classes) en sont revenues au vieil adage romain : vouloir la paix, c’est
préparer la guerre. Le seul avantage du système de la paix armée, c’est de garantir les
(classes) contre les surprises. Cet avantage est si évidemment incertain, les charges
qu’il entraîne sont si accablantes, que l’on a cherché tous les moyens d’en affranchir
les (hommes). De là le système du Désarmement. Ce système n’est point applicable.
Le désarmement ne supprimerait point les causes de la guerre ; il retarderait
simplement le passage de l’état de paix à l’état de guerre. Les (classes) étant moins
préparées à la guerre, les guerres seraient plus longues et partant plus rudes ; et il
faudrait en venir au (terrorisme)”.</p>

<p><b>5- La victoire</b></b>
<p>“Le droit des gens (social) n’est <b>pas un droit abstrait</b>, tiré de la raison pure, au
moyen de déductions logiques. C’est un ensemble de règles qui reposent sur des faits
réels et résultent des rapports nécessaires des (classes). Il ressort des moeurs, des
traditions, de la culture intellectuelle, de la constitution sociale et politique, du
régime économique des (hommes). Le droit des gens (social) qu’une (classe) pratique
à l’égard de l’autre (classe) équivaut toujours à (la discipline organisationnelle)
qu’elle pratique à l’égard d’elle-même. (La conduite respective des classes et leur
mode de discipline interne) trouvent, l’un et l’autre, une sanction supérieure qui leur
est commune, dans la grandeur ou la décadence des (classes), dont ils résument à la
fois les causes et les effets. Plus une (classe) est rapprochée de la barbarie, plus la
force qui la soumet doit être barbare”.</p>
<p>“Dans le droit des gens (social) tout acte emporte ses conséquences inévitables. Ces
conséquences en sont la <b>sanction</b>”.</p>
<p>“Il n’y a entre les belligérants (sociaux) d’autre lien que <b>l’honneur</b> ; briser ce lien
constitue une félonie. Le respect de la parole donnée est le seul moyen qui reste aux
belligérants (sociaux) pour entrer en relation et constater ensemble que le droit du
plus fort est établi. L’honneur, qui est le ressort des armées (sociales), fait la loi des
belligérants pendant la guerre et prépare entre eux le retour à la paix (sociale).
Lorsque les commandants des troupes manifestent le désir d’entrer en relation, ils
déclarent par là-même, qu’ils veulent se placer sous la loi de l’honneur, et avant qu’il
y ait parole donnée, cette loi protège les parlementaires, qui s’annoncent par un
drapeau blanc. Méconnaître ce signe, c’est indiquer que le retour à la paix (sociale)
parait impossible et que la guerre doit prendre un caractère d’extermination”.</p>
<p>“Les (individus) contractent envers (la classe) des obligations proportionnées aux
services qu’ils en reçoivent, et <b>l’assimilation (sociale)</b> s’opère dans la proportion
même où se contractent ces obligations. (La classe victorieuse) n’acquiert donc de
droits véritables que sur les personnes qu’elle assimile et pour lesquelles la (victoire)
devient un bienfait. Si (la domination conquise) est telle que (son empire) ne puisse
se fonder, le vainqueur n’a d’autre moyen que la force pour conserver les résultats
acquis par la force. Il en use et, loin d’assimiler les (personnes renversées de leur
position dominante), il les irrite et les exaspère. L’effort auquel il est contraint pour
conserver sa (domination) fatigue à la longue ses propres (éléments) ; ils se plaignent,
s’agitent, et la (victoire) non seulement ne fonde pas un droit nouveau, mais ébranle
les droits anciens de la (classe). Pour remédier à cela, (la classe) se lance dans de
nouvelles entreprises, et cherche dans des (aventures) nouvelles un remède aux difficultés qui l’embarrassent. Elle répand ainsi ses forces et les dissipe ; elle s’affaiblit
en même temps qu’elle augmente le nombre de mécontents ; elle prépare la révolte
(dans ses rangs) en même temps qu’elle provoque (le soulèvement réactionnaire des
classes déchues). La ruine de (la classe) est toujours la conséquence des
(dominations) abusives”.</p>
<p>“Le vainqueur (peut) rendre <b>hommage au courage</b> déployé par les troupes du
vaincu. (Alors), il leur accorde les honneurs de la guerre, c’est-à-dire le droit de
défiler devant le vainqueur enseignes déployées, avec armes et bagages, et de se
rendre où bon leur semble”.
<p>“La guerre (sociale), pour la consolation des hommes, mérite la gloire dont elle est
entourée”.</p>
<p>“La guerre (sociale) restera toujours la consécration suprême du progrès des uns et
de la décadence des autres”.</p>
<p>Freddy Malot – octobre 1992</p>
