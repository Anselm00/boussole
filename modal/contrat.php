<p>“Le droit des gens (social) est naturellement fondé sur ce principe, que les
diverses (classes) doivent se faire dans la paix le plus de bien, et dans la
guerre le moins de mal qu’il est possible, sans nuire à leurs véritables
intérêts. L’objet de la guerre (sociale), c’est la victoire (de la classe avancée) ;
celui de la victoire, la conquête (de la direction sociale) ; celui de la conquête
(du pouvoir), la conservation (de la domination conquise). De ce principe
doivent dériver toutes les lois qui forment le droit des gens (social)”.</p>
<p>Montesquieu</p>


