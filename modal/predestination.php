			  <h4>Prédestination</h4>
			  
			  <p> « Comme le libre changement du coeur
est, en dépit du mal radical, ordonné
par la loi morale, il doit conséquemment
être possible ; il faut admettre en
faveur de cette possibilité, que la liberté
de ceux qui changent véritablement leur
coeur, est soutenue par une sorte d’assistance
divine, qui n’empiète ni sur la
liberté de l’homme, ni sur la justice de
Dieu, mais est pour nous absolument
incompréhensible. C’est une prédestination
dont tout homme peut espérer être
l’objet, pourvu qu’il fasse sincèrement
son devoir ; elle lui est révélée par la loi
morale, bien qu’elle soit toujours pour
sa raison théorique un mystère impénétrable. »</p>

