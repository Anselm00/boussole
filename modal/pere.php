			  <h4>Père</h4>
			  
			  <p>« <b>Le Père</b> (la Conscience) est la Spontanéité même, le pur privilège de l’Esprit,
même par rapport à la Vie, laquelle au regard du Dynamisme de la pensée se trouve ravalée au rang du Mécanisme. Mais pour affirmer ce privilège de l’Esprit, il faut
précisément poser la Conscience comme pure <b>“puissance” universelle</b>, simple
Père. Le Père est encore Non-Créateur, Mystère sans plus.

<p>• <b>Le Fils</b> est le grand problème, puisque Mystère-Révélé. Il est “négation” du Père.
On ne peut en rendre compte de façon simpliste. L’expression “Dieu-Homme” doit
être précisée : comme Dieu, il est certes le Père même, mais comme Créateur, il est
Verbe éternel et, à ce titre Mystère-Intelligible ; comme Homme, il est certes
totalement Créature, homme “incarné”, de chair et de sang, mais aussi intégralement
auteur de soi-même, ne cessant d’être Dieu jusque dans le sacrifice de son humanité.</p>

<p>Le Fils est négation du Père comme <b>“acte”</b> de l’Esprit, et cet acte est double de
façon essentielle ; il s’oppose à la puissance universelle par son caractère
général/particulier. L’acte constitutif du Fils est “général” comme Conscience
“remplie”, Idée, plan complet de la Création ; il est “particulier” en tant que Mot, idée
exprimée, Parole, et très précisément Évangile (Bonne Nouvelle), réalisation des
prophéties déterminées, Messie advenu, comme fait-événement crucial.</p>

<p>• <b>Le Saint-Esprit</b> redouble la négation divine. Ici, le général/particulier se trouve
nié par le <b>singulier</b>, Dieu <b>“effet”</b> qui est l’opposé simultanément identique de
l’universel. Le Saint-Esprit complète Dieu, dans la mesure où le contenu de la Conscience, l’Idée-Mot se montre alors comme communiquant la Vérité, but véritable
du mouvement de l’Âme. Régressivement, on découvre que le Saint-Esprit était le lien
intime du Père et du Fils, de la conscience vide et de la conscience pleine, du dieu
Abscons (caché) et du dieu Révélé. Le Saint-Esprit, Vérité éternelle de Dieu-Père, est
en même temps Providence présidant à tout le Temps, Vérité “du Fils” en tant que
Chef invisible de l’Église dans tout le cours du “Siècle”.</p>

<p><b>Le Fils</b>, clef de la Religion, dévoile finalement pleinement son caractère : il est
“tout Dieu” exactement comme le Père et le Saint-Esprit, dans la mesure où il est au-dessus du Temps, Éternité même ; il est Fils de façon distinctive, dans la mesure où il s’impose tout spécialement comme Maître du Temps (de la Création) : à son
<b>ORIGINE</b> (Verbe), à sa <b>FIN</b> (Juge), dans la Durée continue et indéfinie du <b>SIÈCLE</b>
(Chef), et dans le Temps en son <b>“MILIEU”</b> (son “centre” significatif), au moment
Incarné (né-mort-ressuscité). »</p>

