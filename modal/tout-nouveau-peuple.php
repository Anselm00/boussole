			  <h4>Tout Nouveau Peuple</h4>
			  
			  <p>« • Cf. <b>Tableau</b> “Essence de la Société Convenable”.</p>
<p>• N’ergotons pas sur <b>les noms</b> donnés aux “Éléments” du Peuple, puisque même ce que nous
appelons “Peuple” n’a pas d’équivalent dans le passé. Saisir ce qu’il y a “derrière” les mots !</p>
<p style="text-align:center;"><b>________________</b></p>
<p>À quoi peut et doit ressembler notre tout nouveau <b>Peuple, qui se sera fait Genre Humain</b> ;
et alors donc en tous points en accord avec la Nature Humaine ?</p>
<p>Peuple et Humanité parvenus à ne faire qu’un, notre Terre n’abritera plus qu’une nouvelle espèce
de notre race “travailleuse”, qu’on ne peut mieux dire que <b>Parentale-Bourgeoise</b> (Traditionnelle-
Civilisée) tout à la fois.</p>
<p>La Préhistoire – hier ! – insistons-y, ne nous a donné aucune expérience, aucune intelligence
possible, d’un tel “composé social” (je construis cette expression à partir de l’étroit “composé
personnel” des thomistes… qu’ils disent composé “humain”). Or, considérez le paradoxe : notre
Nouvel Homme ne semble étrange que par l’Innocence sociale affichée !</p>
<p>En tous cas, nous voilà logiquement avec la Constitution sociale la plus rudimentaire qui soit :
n’admettant pour tissu communautaire que celui fait de membres tout bonnement <b>Pensants-
Sexués</b>, attributs dont on ne peut dépouiller aucun humain ! (Dois-je préciser que “raisonner” n’est
qu’une manière bornée de penser ; ou plutôt de demi-penser !).</p>
<p style="text-align:center;"><b>________________</b></p>
<p>À partir de là, nous avons le <b><u>SQUELETTE</u></b> social naturel suivant : le couple <b>Église <> Assise</b>,
<b>médié par la Passerelle</b> (Assise = Parti-Syndicat ; Passerelle = Vétérans-Jeunes). À chaque couple,
pensons : “les 2 font la paire”, il y a Parité. Bref, tout Hégémonisme Préhistorique s’est envolé.</p>
<p>Pour le reste, c’est-à-dire la <b><u>CHAIR</u></b> sociale, sachons qu’elle consiste en “<b>mille et une
Associations</b>”, entées sur le Squelette, qui se font et se défont sans formalité aucune, pour répondre
aux besoins de la communauté générale, “hic et nunc” et “ad hoc”. »</p>
