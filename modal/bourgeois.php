<p>L’Homme “vrai” est le Bourgeois ; la société Politique est “Naturelle”.</p>
<p style="text-align:center;">• • •</p>
<p>Que dit la toute première phrase de la “Déclaration des Droits de l’Homme et du Citoyen” d’août 1789 ? Ceci :</p>
<p>“Les seules causes des malheurs publics et de la corruption des Gouvernements sont : l’Ignorance, l’Oubli ou le Mépris des droits NATURELS de l’Homme” ; étant Naturels, ces droits sont “Imprescriptibles (ou inaliénables) et Sacrés” ; et on se propose de les “déclarer en présence et sous les auspices de l’Être Suprême.”</p>
<p style="text-align:center;">• • •</p>
<p>C’est-à-dire ?</p>
<p>1- Le Droit Divin (Être Suprême) préside au Droit Naturel et le cautionne de toute ÉTERNITÉ ;</p>
<p>2- Le Droit Naturel veut qu’il n’y ait qu’une seule manière d’être Homme possible et imaginable : c’est le Propriétaire privé, dont émane le Citoyen Public. Bref, le Bourgeois1.</p>
<p>3- Le Droit Naturel étant inaliénable et imprescriptible, il n’y a aucune sorte d’Histoire : 1789 instaure la “vraie” société bourgeoise, valable pour tout le TEMPS du monde, qui fut auparavant seulement Ignorée, Oubliée ou Méprisée.</p>				 	
