<p>« C’est en prenant ce recul – auquel nous sommes contraints à présent – qu’on y voit clair. Qui a jamais compris la Révolution Française comme il faut ?!</p>
<p>Le Spiritualisme considéré historiquement (pour tout le Temps) est FAUX, comme l’idée que le Bourgeois serait “éternel”. On ne peut y trouver et reconnaître un côté <b>impérissable</b> que si on le dépasse, ce qui ne se peut qu’en le mettant EN CONCURRENCE avec son contraire direct, le Matérialisme de la Société Parentale, à qui sa “perfection” entraîna une mésaventure analogue. Alors on découvre leur Identité fondamentale, celle-ci les rangeant ensemble dans la Préhistoire humaine. Ainsi, émancipés de ces deux mentalités, en tant qu’elles ne se justifièrent historiquement qu’à l’état <b>séparé</b> et comme telles <b>périssables</b>, bornées (on n’y reviendra pas !), exclusives l’une de l’autre, nous pouvons enfin leur rendre ce qui leur est dû, les reconnaître comme le support de l’humanité VIVANTE d’hier dont nous sommes <b>débiteurs</b> ».</p>

