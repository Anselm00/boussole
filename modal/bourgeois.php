<p>L’Égalité, dans la civilisation, n’a jamais pu concerner que le Citoyen, le droit Politique, et mobilisait donc préférentiellement
les bourgeois Actifs et les Intellectuels. C’est on ne peut plus explicite dans la Déclaration de 1789, posant le grand principe de
l’Égalité “DEVANT LA LOI”. Ceci concerne très fort le droit des Roturiers d’envahir les emplois publics, en premier lieu le droit
d’être Officiers dans l’armée.</p>
<p>C’est à la naissance de la civilisation, dans l’Antiquité, que l’Égalité semble essentielle. Parce qu’alors, par la force des choses,
la civilisation doit s’insinuer en gardant les dehors “communautaires” de la société Parentale antérieure :
la Cité agglutine des tribus. Ce sont les Citoyens qui dominent directement la Cité, et ils la dominent comme “corps” ;
l’opposé diamétral des Députés des citoyens Modernes éligibles, triés par le cens, qui se forment en Assemblée en 1789.
Comparés aux Nobles du Moyen-Âge, les Citoyens sont Ultra-nobles, directement “divins”, dans l’Antiquité.
Coagulés en “panthéon” terrestre dominant la Cité, ils sont quasi-“racistes” vis-à-vis des Esclaves et des Barbares. La Propriété des Citoyens est “collective” par l’Ager Publicus, et n’est “privée” que comme délégation du “corps” dominant, qu’à titre très précaire (cf. Ostracisme). Patriciens et Plébéiens sont “comme” deux ethnies, pure et impure. Et aussi : La forme d’“adoption” de l’épouse comme une fille dans le Mariage, et les fils et petits-fils tenus “mineurs” tant que les père et grands-père sont vivants. L’Esclave incorporé à la “Familia” du Maître. Le lien Patron/Client, qui est “comme” le Chef tribal et sa “Suite”. Etc. »</p>

