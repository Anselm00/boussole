			  <h4>Liberté Révolutionnaire</h4>
			  
			  <p>« Qu’est la Société Politique, la société Civilisée, 
				  celles des Francs par exemple ?</p>
<p>Un seul mot la définit pratiquement : Liberté. <b>Chose inouïe que 
cette Liberté</b> : méconnue et ostracisée par la communauté parentale 
antérieure, sauf à être invoquée par des pillards hors caste, 
de faux prophètes tel Jésus jugé par le Sanhédrin, ou des primitifs 
vus inférieurs par les Asiates : Sauvages (= hommes des bois) et 
Barbares (= muets, c’est-à-dire au langage incompréhensible). 
Tous ces gens sont maudits parce que portant atteinte à l’intégrité 
parentale, à la Communauté (ou Corporation = “Système” en grec, 
c'est-à-dire corps fait d’organes, un tout fait de parties). 
La Liberté, en sa version étroitement bornée de la civilisation, 
sera tout autant “oubliée” par le Comm-Anar, parce que périmée 
absolument.</p>
<p>Les citoyens de la Société Politique n’ont pas du tout 
voulu cette Liberté constitutive de la seconde 
humanité ! Ils la font naître avec leur rejet absolu du 
régime parental en complète sénescence, et synonyme 
pour eux de “barbarie” haïssable à l’extrême, de “paganisme” 
(c’est la JAHILYA du Coran). De ce fait, ils ne peuvent 
voir le monde qu’à l’inverse des primitifs, constitué 
d’une seule substance : l’<b>Esprit</b>, et cette substance n’ayant 
qu’une expression : la <b>Raison</b> (logos). Cette conception est 
tout à fait fondée, puisque la Réalité comprend réellement 
l’Esprit ; mais la société Civilisée se trouve contrainte 
de disjoindre absolument cet Esprit de son “contraire identique”, 
la Matière ! À la limite, on se trouve dans une situation pire 
qu’auparavant ; et pourtant, quelles prouesses accomplit 
la société Politique durant sa brève existence… Il reste 
qu’il y a une grossière confusion de la part de ceux qui 
prétendent, et que les primitifs avaient une “religion”, 
et qu’on ne peut penser que selon la Raison (laissons 
les occultistes dans leur fange). Une chose est d’établir 
la vérité historique, autre chose est de vanter par tous 
les moyens le régime actuel de Barbarie Intégrale.</p>
<p>On ne trouve de Liberté sociale dans le passé que 
	dans l’humanité Politique (aujourd’hui morte et 
	enterrée dans ce cercueil social nommé “Modernité”). 
	C’est le point dont il ne faut pas démordre. Mais, 
	ceci dit, QUELLE Liberté ? Voilà ce qu’il importe de bien saisir.</p>
<p>Disons que la Liberté Politique est d’abord Négative, 
	et ensuite Progressive.</p>
<p>• Elle est Négative parce que ne présente de 
	Liberté que vis-à-vis des liens du sang emmaillotant 
	la communauté Parentale. Il faut absolument se pénétrer 
	de ce truisme qui résume tout. Car c’est bien sur cette 
	seule base “anti-tribale” que reposera la société Politique, 
	le “Monde Libre”, tout au long de son histoire ; 
	alors qu’on nous agite le mot Liberté comme le fin 
	du fin de l’histoire humaine, ceci pour nous couvrir 
	un totalitarisme établi pire que celui des Brahmanes.</p>
<p>• La société Politique fut ensuite <b>Progressive</b>. Gare à ne 
	pas donner à ce mot son sens laudatif vulgaire que lui 
	donne l’ONU ! C’est le sens historique du terme qui nous 
	intéresse, et c’est le seul digne d’intérêt pour quiconque 
	appartient réellement à l’humanité. La progression – 
	“révolutionnaire” à l’occasion – est bien la démarche 
	Spiritualiste. On va vers le perfectionnement de la 
	société Politique en intensifiant l’hégémonie des conditions 
	sociales selon le “travail”, sur les conditions dites 
	“naturelles”. Bref, l’Industrie “artificielle” doit 
	parvenir à maîtriser les “forces de la nature” en 
	vidant la matière de toute “vertu” et la dévoiler 
	patiemment comme pur Mécanisme. Ceci s’achève lorsque 
	l’agriculture devient elle-même une “industrie”, quand 
	le Landlord doit se contenter de percevoir une rente 
	foncière de la part d’un entrepreneur capitaliste spécial : 
	le Fermier. Du côté de la masse de la population, 
	sous la direction de la mince classe dominante louant 
	l’Être Suprême, on n’a plus qu’une foule d’Esclaves Libres, 
	les <b>Salariés</b> des villes et des champs. Les générations 
	d’esclaves jusque là – esclaves directs antiques et 
	serfs médiévaux – sont devenues “entièrement libres” 
	en forgeant leurs propres chaînes, c’est-à-dire en 
	contribuant assidûment à avilir la valeur de leur force 
	de travail au rythme même du Progrès de l’Industrie.</p>
<p>Quand la Société Politique s’effondra – nous en sommes à 
l’effondrement de l’effondrement –, quand elle devint Asociale 
(1835-1850), l’ancienne <b>Liberté Marchande, simplement Esclavagiste</b>, 
devint monstrueuse : le monde de l’État de Siège constitutionnel 
visant les “classes dangereuses”, du Code du Travail – 
comprendre du Salariat Forcé – avec le “fardeau” des 
Prélèvements Obligatoires destinés aux “Assistés” de 
tous genres, comme sont désignés les membres mêmes de la 
population active. Sous ces derniers, il y a encore les 
“exclus” proprement dits : SDF et autres du Quart-Monde, 
supérieurs encore cependant aux “multitudes” menaçantes du 
Tiers-Monde ! »</p>
