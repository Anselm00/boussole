			  <p>« Cette révolution qu’implique nécessairement le Communisme Historique, on le voit immédiatement, n’est pas simplement “économique” : en tant que victoire du Ménage sur l’Entreprise, elle s’annonce comme un bouleversement de tout <b>l’ordre civil matériel</b>, une subversion générale du Marché.</p>
<p>Ce n’est pas tout. Envisager sérieusement que les Ménages puissent s’emparer de l’Entreprise entraîne immédiatement l’inévitable <b>extinction de la Famille</b>.</p>
<p>Finalement, la fusion Ménage-Entreprise ne peut qu’appeler la refonte totale de l’Ordre Civilisé ; c’est l’évidence même. Ainsi, les deux Cellules du Marché Libre (<b>Ménage</b> et <b>Entreprise</b>) étant remises en question, le deuxième élément de l’armature civilisée, l’<b>État Égalitaire</b>, se trouve attaqué du même coup jusque dans ses fondements. Autrement dit, tandis que les deux Cellules privées, patrimoniales, du Marché sont assignées au tribunal de l’histoire, de même les deux Organes publics, patriotiques, de l’État doivent y comparaître forcément : le <b>Gouvernement Légal</b> et l’<b>Assemblée Nationale</b>. »</p>

