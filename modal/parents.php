			  <h4>Parents</h4>
			  
			  <p>« À la différence du matérialisme philosophique qui est dogmatique, et mécaniste, le
matérialisme primitif naturel est actif.</p>
<p>Le totémisme tribal met la chose en évidence. Le clan est conçu comme attaché à
la terre sacrée. En elle s’enracine l’Ancêtre éponyme. La sève de l’Ancêtre se répand
sous forme des “frères de sang” du clan ; ceux-ci assurent en retour la palingénésie de
l’espèce totémique. Le matérialisme traditionnel se fonde sur le fait que les primitifs
subordonnent spontanément le Travail humain à la Fécondité naturelle générale.
L’activité intelligente se donne à la pensée comme partie intégrante du mouvement
spontané aveugle, inhérent à l’univers. Les ancêtres : “Ce sont ceux qui ont
commencé les choses” (Bantous), dans “le Temps-sans-Bornes” immobile
(Zoroastre).</p>
			  <p><i>“Quand on demande à un indigène quelle est sa “chair”, il donne le nom de son
totem matrilinéaire – Kangourou ou Émeu par exemple. Tous les membres du clan,
de ce fait, se considèrent comme parents” </i>(Elkin : <i>Aborigènes australiens</i>).<i>
“Quand un individu naît, une individualité se reforme. L’héritage du prénom est
la règle. L’individu naît avec son nom, ses fonctions, son blason. Le nombre des
noms est limité dans le clan, et la vie de celui-ci n’est qu’un ensemble de renaissances
d’individus toujours identiques” </i>(Lévy-Bruhl :<i> Sociétés inférieures</i>). <i>“Chez les
Bantous, tout individu constitue un chaînon rattaché à la lignée. Lorsque la mère
commence à sentir la vie en son sein, les noirs se demandent qui est cet homme à
naître. Parfois un ancêtre dispute à un aïeul l’avantage de renaître en l’enfant. Les
noirs disent à la parturiente : tu as enfanté notre grand-père, notre tante, etc. Un
même ancêtre peut être “revenu” en plusieurs membres vivants du clan, chacun
désigné (par exemple) Ngoi ou Ilunga. Les noirs, cependant, savent que la naissance
du petit ne met nullement fin à l’existence du défunt Ngoy, qui deviendra le “ngudi”
– protecteur – du nouveau-né qui est son homonyme (“majina”)” </i>(Tempels :<i>
Philosophie Bantoue</i>). »</p>

