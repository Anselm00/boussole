			  <h4>Théorie</h4>
			  
			  « Le Credo de l’ERM est mieux dit <b>Théorie Évidente</b>, pour le distinguer du Mythe matérialiste
qui invoque le Secret, et du Dogme spiritualiste qui invoque le Mystère.</p>
Notre Théorie comprend 2 volets mutuellement Cohérents : le Réalisme Vrai pour <b>le Mode de
Pensée</b>, et le Comm-Anar pour le <b>Régime Social</b>. Le très-cher <b>Dom Deschamps</b> (1772) disait :
Vérité Métaphysique d’une part et Vérité Morale d’autre part.

<p style="text-align:center;"><b>I- RÉALISME VRAI</b></p>
Le Présupposé de notre Théorie – la Réalité – est <b>Lucide</b> subjectivement et </b>Impartial</b>
objectivement. La Réalité est UNE, mais il convient de l’aborder de deux manières : en Elle-même
(“Absolue”), et pour-Nous (“Relative”).</p>
<p>1/ <b><u>Réalité en Elle-même.</u></b></p>
<p>Cette Réalité à majuscule est la Réalité dernière, ultime, et ne s’appréhende que mentalement. La
grande découverte est qu’elle se donne désormais comme <b>RAPPORT</b> ; de sorte que le Présupposé
théorique ne peut se dire que Matière <> Esprit. Les 2 choses n’en font qu’une réellement, bien que
“contraires” nominalement. D’où l’axiome : “<b>Le Relatif se trouve dans l’Absolu</b>”. Avec notre
Rapport, il y a Adéquation totale du “mot” et de la “chose”.</p>
<p>Nous n’avons d’autre prise que théorique sur la Réalité tout court. Par suite, ce qu’on peut en
“<b>déduire</b>”, bien que non sans intérêt, reste invinciblement formel.</p>
<p>2/ <b><u>Réalité pour-Nous.</u></b></p>
<p>Notre action a une incidence “prochaine” (directe) sur la Réalité pour-Nous ; non plus absolue
mais simplement générale. Cette dernière est faite d’un tissu de réalités à minuscule (particulières).
Il s’agit de faits <=> événements, “à la limite” <b>Indiscernables</b> (espace) et en même temps <b>Inédits</b>
(temps). Car un endroit peut tendre vers le point (sans dimension), et un moment peut tendre
vers l’instant (sans durée). Bref, qualité-quantité et continu-discret sont des paires de contraires
identiques en dernière analyse.</p>
<p>Le mot Science a fini par ne s’attacher qu’à la science de la Nature, aux <b>Lois Physiques</b>. La science
de l’Humanité ne donnait que des <b>Modèles Moraux</b> (“Mentor est un MODÈLE de Prudence”).
Nos réalités font désormais l’objet d’une Science unique à deux modalités inverses : <b>Physique
de la Qualité et Morale de la Quantité</b>. Telle est LA science “DU NEUF” à la limite : nous ne
rencontrons que des problèmes foncièrement originaux. D’où l’axiome : “<b>l’Absolu se trouve
dans le Relatif</b>”. Avec notre approche, l’Adéquation se vérifie pour l’essentiel : les pensées bien
menées “collent” aux faits-événements dont elles veulent rendre compte, et le critère décisif en est
la confirmation PRATIQUE (Gare ! rien à voir avec le Pragmatisme-Opportunisme des Barbares
dominants !).</p>
<p>Notre nature travailleuse nous fait Responsables terrestres, et “une contradiction chasse l’autre”
(Mao). Montrons-nous engagés, militants, dans la foule des solutions à “<b>induire</b>” sans cesse pour
faire face aux problèmes, et dans leur traduction décisive en actes. »</p>

