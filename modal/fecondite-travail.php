			  <h4>Fécondité/Travail</h4>
			  
			  <p>« L’expression pratique du Matérialisme primitif est que la source propre 
			  de la Richesse n’est pas vue dans le Travail humain, mais dans la Fécondité 
			  naturelle. Cela apparaît aux civilisés comme un culte délibéré de la Paresse 
			  cultivé par les Primitifs, et cela est justifié en un sens, mais qui est tout 
			  autre que celui que s’imaginent les civilisés. Car le culte du Travail de la 
			  civilisation n’est pas étranger à la domination présente du Parasitisme Intégral ;
			   autrement dit : notre Humanisme-Travaillisme à l’agonie se révèle tout autant borné et
			    préhistorique que le vieux Naturalisme-Fertilisme ! Et on oublie que ce n’était pas 
				rien que de gérer la primauté déclarée de la Fécondité sur le Travail. La Paresse 
				Active était tout un art et elle eut une riche histoire.</p>
<p>Une grande étape fut franchie dans le développement du Matérialisme primitif, quand fut 
	découverte la participation particulière du mâle à la Fécondité générale, et c’est cette 
	même chose qui s’exprima plus tard par le culte “phallique”. Initialement cependant, cela 
	fut acquis dès la scission entre Éleveurs nomades et Horticulteurs sédentaires. Ceci ne 
	doit pas du tout être rattaché à ce que Engels donne pour “la grande défaite historique 
	du sexe féminin” ; au contraire ! Évidemment, le phénomène peut paraître paradoxal, mais 
	il fut l’occasion d’une promotion importante de la croyance en la Mère Fondamentale, et 
	d’un perfectionnement très net du Matriarcat. Probablement que la crise qu’occasionna le 
	passage d’une étape à l’autre donna le jour, passagèrement, à des bandes de jeunes guerriers 
	brisant les liens du sang, pillards et “phallocrates” ; mais cela ne put être durable et ne put avoir
	le caractère de l’avènement du “patriarcat” au sens du Maritalat civilisé. Cette erreur de perspective, 
	qui anticipe trop vite sur le “sens de l’histoire”, fait penser à une erreur comparable d’Engels, quand 
	il dit que les Hellènes (gréco-romains) n’étaient arrivés qu’à l’idée d’une “religion nationale”, et que 
	ce sont les chrétiens et bouddhistes qui, plus tard, rendirent d’actualité le “monothéisme”. Je laisse de côté 
	la question du bouddhisme. Le christianisme, lui, fut un perfectionnement puissant du “Monothéisme”, qu’assurément 
	les Hellènes établirent dès la première heure. D’ailleurs, “monothéisme” n’est pas un concept à conserver, étant 
	donné que dès qu’il y a Dieu, Religion, il y a évidemment Un seul Dieu souverain, en dépit de toutes les apparences 
	“polythéistes”. La Religion, à chaque stade où elle se purifie, relativement au stade précédent, est évidemment 
	portée à voir le stade antérieur comme “polythéiste”. Ainsi, les Évangélistes du 16ème siècle virent dans le 
	Catholicisme lui-même, avec ses Saints et ses Anges, un genre de polythéisme ou d’idolâtrie. »</p>

