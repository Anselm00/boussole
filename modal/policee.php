			  <h4>Policée</h4>
			  
			  <p>« Alors, en quoi <b>le Pur est le Pire</b> en matière de Préjugé – Privilège. Je prends
l’exemple Civilisé. Au sommet Moderne (1760-1805), les “Lumières” (les vraies, pas
celles des Laïcs du Grand-Orient en particulier) ont pour mot d’ordre ABOLITION du
Préjugé/Privilège. Ils se dupent eux-mêmes et le doivent, car il n’est pas d’autre
moyen en Préhistoire de promouvoir la société vivante. Ce qu’ils veulent dire, se
trouvant borné dans l’horizon policé, c’est : abolissons la dernière
INCONSÉQUENCE du Préjugé – Privilège, et… portons-le à son comble !! <b>Préjugé à
son comble</b> : nous admettons qu’il peut y avoir des athées très moraux dans la vie ;
aidons-les à découvrir eux-mêmes que cette conduite mène à Dieu s’ils sont
cohérents. Ainsi le Pur théorique désarme l’extrémisme de gauche ; il désarme de
même l’extrémisme de droite des Exaltés avec le projet d’Église de Kant, qui s’annexe
la perspective Millénariste. <b>Privilège à son comble</b> : liberté du travail proclamée, à
bas les Corporations, place à l’Esclave Libre, le Salarié, et au Maître Contraint (par
l’Église apatride et la Loi impartiale) de se concerter avec ses concurrents (concourants,
compétiteurs) pour veiller au bien commun par le juste prix et la
bienfaisance. Or, c’est là qu’intervient en quelque sorte le pire du pire : en pratique, la
vieille société policée plus que bimillénaire recule effrayée devant les exigences du
Parfait de <b>son</b> Pur. Les cadres du système s’écrient : Kant et ses pareils nous
proposent rien moins qu’un programme d’euthanasie des bases mêmes de la
Civilisation : dogmatisme – intellectualisme * moralisme – élitisme ! Et nous voilà
embarqués dans la monstruosité sociale dont nous vivons les ultimes épisodes… »</p>

<p>__________________________</p>

			  <p>Voir <a href="https://www.eglise-realiste.org/pdf/Se_Transformer_ed.pdf" target="_blank"> "Se Transformer"</a>, p.4 - Talib Freddy  - 29.11.2010</p>

			    




