			  <h4>Nécessité</h4>
			  
		 <p>« JE vais parler de la Nature ;<br>
Je vais dévoiler l’imposture<br>
Qui règne à l’ombre des Autels.<br>
Qu’à mes yeux le grand livre s’ouvre,<br>
Et que la raison m’y découvre<br>
L’erreur qui séduit les Mortels.</p>

<p>PAR-TOUT la Nature est la même ;<br>
Sans nuages et sans emblême,<br>
On la reconnoît à ses traits ;<br>
Insecte, Animal, Homme ou Plante,<br>
Pas un Etre qui ne ressente<br>
Et ses rigueurs et ses bienfaits.</p>

<p>GARDONS-NOUS de la croire injuste ;<br>
En combinant sa marche auguste<br>
On pénêtre la vérité.<br>
La foule des maux qu’on rencontre,<br>
En nous accablant ne nous montre<br>
Que Statique et Nécessité.  »</p>



