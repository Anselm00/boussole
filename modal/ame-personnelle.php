	  <p>« L’Âme Suprême de Dieu n’“apaise” pas les âmes infimes de ses enfants les croyants, de la façon confortable que connaissent
les “fidèles” à deux sous. Voyons cela.</p>
<p>Si la Raison claudique inévitablement, du fait de l’hégémonie de l’Intuition sur
l’Entendement, Dieu n’assure l’équilibre, ne ferme toute discussion, que pour une
Raison peu exigeante qui se livre, soit à la sensiblerie émotive, soit au simplisme logique. En effet, même la <b>Théologie Positive</b>, déductive et démonstrative, si elle
ne se dupe pas elle-même, doit reconnaître qu’elle n’apporte de “preuves” de Dieu
qu’a posteriori, qu’au titre de confirmation de la Foi. Par-dessus le marché, la
théologie positive conséquente débouche sur un Dieu “double” ! Certes, la Raison
divine n’est pas boiteuse, à la différence de la raison humaine : en Dieu, <b>Intuition et
Entendement ne font “absolument” qu’Un</b>. C’est pour cela que Dieu “boucle”
effectivement les difficultés rencontrées par la pensée civilisée. Seulement, dans
l’Âme Suprême, la Raison divine fait directement face au Mystère divin (ce qui prend
la place de la Foi dans nos petites âmes). C’est là qu’il y a un problème : LE problème,
pour le croyant sérieux, consciencieux, qui ne triche pas avec sa Foi. La Raison divine
“unifiée” est bien apaisante, mais elle ne nous donne Dieu que par le petit bout de la
lorgnette : Dieu comme Sujet Absolu, Dieu pour-Nous, Dieu Créateur. S’en tenir là
serait faire de Dieu l’esclave du Monde et finalement une Idole des Hommes ! La
grande affaire, c’est évidemment l’autre face de Dieu, Dieu en-Lui-même, qui n’a que
faire en tant que tel de Créer ; et c’est aussi l’union, l’identité totale, des deux faces de
Dieu, c’est-à-dire pourquoi il est à la fois en-Lui et pour-Nous, pourquoi il crée bien
que se suffisant, comment peut se concevoir l’Esprit pour qui la Matière est pur
Néant. C’est alors qu’on comprend l’histoire vraie de la Religion, comment d’une part,
seule la Foi surmontait le handicap de notre Raison ; et d’autre part l’héroïsme des
grands Saints chargés de cette Foi civilisatrice, stupéfaits devant le Mystère et
angoissés de leur propre Salut.</p>
<p>La Foi “solutionnant” l’hégémonie interne à notre Raison fut simultanément un
combat de manière essentielle, d’une autre manière encore plus profonde. Le Dieu
pur-Sujet et pur-Mystère mentionné plus haut, n’est que le Dieu de la religion
Parfaite de la civilisation Moderne, le Dieu des “Déistes” de 1760-1795. Combien
“impur”, “superstitieux”, “grossier”, le Dieu du passé apparut alors aux déistes ! C’est
que c’est toute une histoire que celle de la révélation complète de Dieu. Or, il faut bien
remarquer une chose à ce propos, chose d’importance absolument décisive :
parallèlement à la purification historique que la civilisation accomplit de l’idée de
Dieu, se produisit la purification complémentaire de la Raison ! Et Foi-Raison
purifiées ensemble, donnèrent ensemble une idée sans cesse purifiée de l’Âme et, par
suite de l’union âme-corps. Comment se fait-il que nos Agnostiques dominants –
cléricaux autant que libre-penseurs – “s’insurgent” contre le prétendu “dualisme
cartésien” appliqué à la Personne humaine ? Ces messieurs ne voulant pas de la Foi,
perdent la raison ! »</p>

