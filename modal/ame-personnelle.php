<p>• Et cela par le <b><a class="modal-trigger " href="#travail-fecondite">Travail</a></b> Rationnel mis en route : nouveaux besoins, feu domestiqué donne viande cuite, action mutuelle de la Main et du Cerveau donnant l’outil, langage articulé…</p>
