      <h4><b>Âme Personnelle</b></h4>


	  <p>« L’Âme des hommes incarne (oui !) électivement l’<a class="modal-trigger " href="#esprit">Esprit</a> Relatif, nos <a class="modal-trigger " href="#societe-de-personnes">Personnes</a> ayant par leur âme le privilège d’être des sujets au monde. Ainsi, l’âme des hommes est le “vrai Moi” des personnes, leur part Immortelle faisant écho à l’Éternité de Dieu. Vis-à-vis de l’Âme, le Corps est strictement instrumental, subordonné. Ceci souligne que la Création est inconcevable sans que soit posée ce que nous appelons la Matière, qui est comme le tain, le dos, de la Nature. Réciproquement, que le Monde soit nécessairement physique, corporel, explique que les âmes humaines soient “nombrées”, multiples et non pas Une. En tout état de cause, le <a class="modal-trigger " href="#croyant">croyant</a> ne doit pas se laisser prendre au mirage qui consisterait à taxer la Matière de Substance ; <a class="modal-trigger " href="#neant2">Néant</a> en Dieu, elle est <b>Non-être</b> au Monde, simple marque fondamentale de la Dépendance de la <a class="modal-trigger " href="#creation">Création</a> vis-à-vis du Créateur, de la Transcendance de Dieu. Ceci a pour complément, ne l’oublions pas, le fait que la Matière ouvre son champ à l’hégémonie de l’Humanité sur la Nature, déploie le domaine des Choses, de l’esprit “passif” offert aux agents sociaux que nous sommes.</p>
<p>Attention ! La “dualité” Âme-Corps qui constitue les Personnes donne lieu à de graves méprises.</p>
<p>• Relativement à Dieu en tant qu’Objet du Mystère, les âmes mêmes ne se distinguent en rien des corps ; étant simples créatures au sens absolu, elles sont comme n’étant pas, néant de Dieu.</p>
<p>• Relativement à Dieu en tant que Sujet Absolu – donc Créateur –, la réalité des Personnes est affirmée et exposée comme suit :</p>
<p>- <b>Nos âmes</b> sont “divines”. Ceci accuse simplement l’affinité directe qu’il y a entre les Personnes et Dieu, contrairement aux Choses. C’est donc un aspect relatif au sein du Relatif (du monde) qui est mis en relief. La “divinité” de nos âmes n’est qu’une analogie ; l’âme même du Prince des Apôtres n’est en aucune façon une “parcelle” de l’Esprit Absolu ; professer cela, c’est sombrer dans le Panthéisme. D’ailleurs, le caractère “divin” de nos âmes ne préjuge nullement de leur destinée future : soit la vie immortelle, soit la mort perpétuelle.</p>
<p>- <b>Nos corps</b> relèvent du Non-être matériel, nous rappellent que nous sommes en dernier ressort simples “balayures du monde” (Saint Paul), et sont ce qui nous expose à la tentation fondamentale d’élever la Matière au rang de Substance. Cependant, il est tout à fait impie de prétendre que nos corps sont l’oeuvre de Satan ; au contraire, ils sont l’“accident nécessaire” qui fait de nous des créatures de Dieu ; professer le contraire, c’est sombrer dans le Manichéisme. Que notre corps, dans le siècle présent, soit périssable est une chose ; cela n’en fait pas moins une part divine secondaire de notre personne, et on doit seulement avoir en tête qu’un corps vraiment adéquat à notre âme, incorruptible, attend les Bienheureux. Nos païens* ne cessent d’incriminer le Moyen-âge de la “haine de la chair”, et en même temps ils se disent scandalisés de ce que le Moyen-âge condamnait le suicide ! De fait, Saint Thomas dit : “L’homicide de soi-même l’emporte en gravité sur les autres homicides” ; et la législation de l’époque livrait, après procès, le cadavre du suicidé au supplice, son meurtrier étant tenu pour asocial.</p>

<p>_______________</p>
NOTE: *En effet, les curés appuient sans faille les bouffe-curés dans la répudiation de la spiritualité médiévale (cf. Croisades, Inquisition, etc.). Ils y procèdent seulement avec la mièvre, sournoise et implacable onctuosité consommée qui les distingue ; mais se réservent bien sûr bec et ongles ce qui pourrait attenter à leur ministère ô combien “temporel” de la bondieuserie. »</p>


