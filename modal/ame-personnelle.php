		<p>« Ici-Bas, <b>l’homme “ordinaire”</b>, même s’il est fidèle et
non pas un impie, a toujours quelque mal mélé au bien
(Il y a bien d’autre anomalies : les enfants morts-nés, etc, etc.).
<b>De même la nature “ordinaire”</b>, bien que reluisant d’esprit divin
par sa diversité harmonieuse, laisse voir des “monstres”,
le “tremblement de terre de Lisbonne” (Voltaire), des choses
inconnues et inexpliquées. C’est qu’ici-bas existent des Personnes
(Âme-Corps) et des Choses (Individu-Espèce) ; non pas des Sujets et
Objets (“substances séparées”).</p>
<p>Aussi, le Saint même ne l’est pas à ses propres yeux ; et le Chef-d’œuvre est entaché
d’imperfections aux yeux de son Auteur.</p>
<p>Néanmoins, pour les gens ordinaires et les autorités (pour celles-ci, le Jugement de
Dieu étant réservé), les Saints et les Chef-d’œuvres apparaissent comme des “modèles”,
des <b>Personnes et Choses exceptionnelles</b>. »</p>

